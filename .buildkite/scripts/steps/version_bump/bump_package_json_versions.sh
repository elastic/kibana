#!/usr/bin/env bash

set -euo pipefail

echo --- Bump package.json versions

sed -i "s/\"version\": \"[0-9]\+\.[0-9]\+\.[0-9]\+\"/\"version\": \"${NEW_VERSION}\"/g" package.json

sed -i "s/\"version\": \"[0-9]\+\.[0-9]\+\.[0-9]\+\"/\"version\": \"${NEW_VERSION}\"/g" x-pack/package.json

echo --- Committing version bump changes

git config --global user.name kibanamachine
git config --global user.email '42973632+kibanamachine@users.noreply.github.com'

branch="bump_package_json_versions_$(date +%F_%H-%M-%S)"
git checkout -b "$branch"
git add package.json x-pack/package.json
git commit -m "[package.json version bump] Bump package.json versions to ${NEW_VERSION}"

git push origin "$branch"

prUrl=$(gh pr create --repo elastic/kibana --base main --head "$branch" --title "[package.json version bump] $(date +%F) Bump package.json versions to ${NEW_VERSION}" --body "Generated by $BUILDKITE_BUILD_URL" --label "release_note:skip" --label "backport:skip")
echo "Opened PR: $prUrl"

gh pr merge --repo elastic/kibana --auto --squash "$prUrl"



