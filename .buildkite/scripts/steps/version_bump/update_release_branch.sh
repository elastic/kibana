#!/usr/bin/env bash

set -euo pipefail

source "$(dirname "$0")/wait_for_pr_merge.sh"

echo --- Update release branch configuration

git config --global user.name kibanamachine
git config --global user.email '42973632+kibanamachine@users.noreply.github.com'

git fetch origin "$BRANCH"
git checkout -B "$BRANCH" "origin/$BRANCH"

echo "Updating branch property in package.json to '$BRANCH'"
jq --arg branch "$BRANCH" '.branch = $branch' package.json > package.json.tmp && mv package.json.tmp package.json

# See https://github.com/elastic/kibana/pull/199404
# Prevent backport assignments
printf '\n# See https://github.com/elastic/kibana/pull/199404\n# Prevent backport assignments\n* @kibanamachine \n' >> .github/CODEOWNERS

head_branch="update-release-branch-$(date +%F_%H-%M-%S)"
git checkout -b "$head_branch"
git add package.json .github/CODEOWNERS
git commit -m "[release branch setup] Set branch to ${BRANCH}"

git push origin "$head_branch"

prUrl=$(gh pr create --repo elastic/kibana --base "$BRANCH" --head "$head_branch" \
  --title "[Auto version bump] $(date +%F) Set branch to ${BRANCH}" \
  --body "Generated by ${BUILDKITE_BUILD_URL}" \
  --label "release_note:skip" --label "backport:skip")
echo "Opened PR: $prUrl"

if [ "${DRY_RUN:-}" = "true" ]; then
  echo "DRY_RUN is enabled — skipping auto-merge and merge wait"
else
  gh pr merge --repo elastic/kibana --auto --squash --delete-branch "$prUrl"

  wait_for_pr_merge "$prUrl"
fi

echo "Release branch '$BRANCH' updated successfully"
