#!/usr/bin/env bash

set -euo pipefail

source "$(dirname "$0")/wait_for_pr_merge.sh"

echo --- Update pipeline resource definitions

git fetch origin main
git checkout -B main origin/main

FILES=(
  ".buildkite/pipeline-resource-definitions/kibana-console-definitions-sync.yml"
  ".buildkite/pipeline-resource-definitions/kibana-es-snapshots.yml"
  ".buildkite/pipeline-resource-definitions/kibana-on-merge.yml"
  ".buildkite/pipeline-resource-definitions/kibana-on-merge-unsupported-ftrs.yml"
)

for file in "${FILES[@]}"; do
  echo "Updating branch_configuration in $file"
  sed -i "s/branch_configuration: main/branch_configuration: main ${BRANCH}/" "$file"
done

echo --- Committing pipeline resource definition changes

git config --global user.name kibanamachine
git config --global user.email '42973632+kibanamachine@users.noreply.github.com'

head_branch="update-pipeline-resource-defs-$(date +%F_%H-%M-%S)"
git checkout -b "$head_branch"
git add "${FILES[@]}"
git commit -m "[pipeline resource definitions] Add branch ${BRANCH} to branch_configuration"

git push origin "$head_branch"

prUrl=$(gh pr create --repo elastic/kibana --base main --head "$head_branch" \
  --title "[Auto version bump] $(date +%F) Add ${BRANCH} to pipeline resource definitions" \
  --body "Generated by $BUILDKITE_BUILD_URL" \
  --label "release_note:skip" --label "backport:skip")
echo "Opened PR: $prUrl"

if [ "${DRY_RUN:-}" = "true" ]; then
  echo "DRY_RUN is enabled — skipping auto-merge and merge wait"
else
  gh pr merge --repo elastic/kibana --auto --squash --delete-branch "$prUrl"

  wait_for_pr_merge "$prUrl"
fi
