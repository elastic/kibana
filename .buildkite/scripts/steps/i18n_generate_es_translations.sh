#!/usr/bin/env bash
set -euo pipefail

# Generates/updates the Spanish (es) translation file for Discover by diffing
# current i18n strings against the committed es.json and translating only the delta.
# Runs weekly on Sunday at 6am UTC (24h before Monday serverless QA promotion).
# Opens a PR via kibanamachine if any strings changed.

TRANSLATIONS_DIR="x-pack/platform/plugins/private/translations/translations"
GIT_SCOPE="$TRANSLATIONS_DIR/es.json $TRANSLATIONS_DIR/es.source.json $TRANSLATIONS_DIR/ru-RU.json $TRANSLATIONS_DIR/ru-RU.source.json"

report_main_step () {
  echo "--- $1"
}

main () {
  cd "$KIBANA_DIR"

  report_main_step "Bootstrapping Kibana"
  .buildkite/scripts/bootstrap.sh

  report_main_step "Generating Spanish and Russian translations"
  node scripts/generate_discover_es_translation.mjs --locale es
  node scripts/generate_discover_es_translation.mjs --locale ru-RU

  # Check for differences
  report_main_step "Checking for changes"
  set +e
  git diff --exit-code --quiet $GIT_SCOPE
  if [ $? -eq 0 ]; then
    echo "No translation changes found. Exiting."
    exit 0
  fi
  set -e

  report_main_step "Translation changes detected. Checking for existing PR."

  KIBANA_MACHINE_USERNAME="kibanamachine"
  git config --global user.name "$KIBANA_MACHINE_USERNAME"
  git config --global user.email '42973632+kibanamachine@users.noreply.github.com'

  PR_TITLE='[i18n] Update Spanish (es) translations for Discover'

  # If a PR already exists from a previous run, update it instead of creating a new one
  pr_search_result=$(gh pr list \
    --search "$PR_TITLE" \
    --state open \
    --author "$KIBANA_MACHINE_USERNAME" \
    --limit 1 \
    --json title -q ".[].title")

  BRANCH_NAME="i18n_es_translations_$(date +%s)"
  git checkout -b "$BRANCH_NAME"
  git add $GIT_SCOPE

  # Include flagged strings report if present
  FLAGGED_FILE="x-pack/platform/plugins/private/translations/translations/es.flagged.json"
  if [ -f "$FLAGGED_FILE" ]; then
    git add "$FLAGGED_FILE"
  fi

  git commit -m "[i18n] Update Spanish (es) translations for Discover"

  git push origin "$BRANCH_NAME"

  if [ "$pr_search_result" == "$PR_TITLE" ]; then
    echo "PR already exists — pushed updated branch. PR will update automatically."
    exit 0
  fi

  report_main_step "Creating pull request"

  FLAGGED_COUNT=0
  if [ -f "$FLAGGED_FILE" ]; then
    FLAGGED_COUNT=$(node -e "const f=require('./$FLAGGED_FILE'); console.log(f.length)" 2>/dev/null || echo 0)
  fi

  PR_BODY="Automated weekly update of Spanish (es) translations for the Discover plugin.

Generated by \`scripts/generate_discover_es_translation.mjs\` using Claude Sonnet.

**What changed:** New or modified \`defaultMessage\` strings since the last translation run.

**Flagged for review:** $FLAGGED_COUNT strings were uncertain — see \`es.flagged.json\` in this PR for the list with reasons.

All structural checks (ICU placeholder syntax, unused key removal) will be validated by \`i18n_check\` in CI.

> This PR was generated automatically 24h before the Monday serverless QA promotion."

  gh pr create \
    --title "$PR_TITLE" \
    --body "$PR_BODY" \
    --base main \
    --head "$BRANCH_NAME" \
    --label 'release_note:skip' \
    --label 'Team:Localization'
}

main
