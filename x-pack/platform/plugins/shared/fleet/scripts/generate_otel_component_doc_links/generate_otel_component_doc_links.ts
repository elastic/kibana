import fs from 'fs';
import path from 'path';

import pMap from 'p-map';
import { ToolingLog } from '@kbn/tooling-log';

const logger = new ToolingLog({ level: 'info', writeTo: process.stdout });

const REPOS = [
  { owner: 'open-telemetry', repo: 'opentelemetry-collector', label: 'core' as const },
  { owner: 'open-telemetry', repo: 'opentelemetry-collector-contrib', label: 'contrib' as const },
];

const COMPONENT_CLASSES = ['receiver', 'processor', 'exporter', 'connector'];
const CONCURRENCY = 20;
const OUTPUT_PATH = path.resolve(
  __dirname,
  '../../public/components/otel_ui/collector_config_view/component_detail/component_doc_links.generated.ts'
);

const LICENSE_HEADER = `/*
 * Copyright Elasticsearch B.V. and/or licensed to Elasticsearch B.V. under one
 * or more contributor license agreements. Licensed under the Elastic License
 * 2.0; you may not use this file except in compliance with the Elastic License
 * 2.0.
 */`;

const METADATA_PATH_RE = new RegExp(`^(${COMPONENT_CLASSES.join('|')})/([^/]+)/metadata\\.yaml$`);

const githubFetch = async (url: string): Promise<Response> => {
  const headers: Record<string, string> = { 'User-Agent': 'generate-otel-component-doc-links' };
  const token = process.env.GITHUB_TOKEN;
  if (token) {
    headers.Authorization = `token ${token}`;
  }
  const res = await fetch(url, { headers });
  if (!res.ok) {
    throw new Error(`HTTP ${res.status} for ${url}`);
  }
  return res;
};

const getMetadataPaths = async (owner: string, repo: string) => {
  const url = `https://api.github.com/repos/${owner}/${repo}/git/trees/main?recursive=1`;
  logger.info(`Fetching tree for ${owner}/${repo}...`);
  const { tree } = (await (await githubFetch(url)).json()) as {
    tree: Array<{ path: string }>;
  };

  return tree.flatMap(({ path: p }) => {
    const m = p.match(METADATA_PATH_RE);
    if (!m || !m[2].endsWith(m[1])) return [];
    return [{ componentClass: m[1], dirName: m[2], path: p }];
  });
};

const fetchMetadata = async (
  owner: string,
  repo: string,
  filePath: string
): Promise<{ typeName: string; displayName?: string } | null> => {
  const content = await (
    await githubFetch(`https://raw.githubusercontent.com/${owner}/${repo}/main/${filePath}`)
  ).text();

  const typeName = content.match(/^type:\s*(.+)$/m)?.[1]?.trim();
  if (!typeName) return null;

  const displayName = content.match(/^display_name:\s*(.+)$/m)?.[1]?.trim();
  return { typeName, displayName };
};

interface ComponentEntry {
  key: string;
  url: string;
  displayName?: string;
  repo: string;
}

const fetchAllEntries = async (): Promise<ComponentEntry[]> => {
  const entries: ComponentEntry[] = [];

  for (const { owner, repo, label } of REPOS) {
    const metaPaths = await getMetadataPaths(owner, repo);
    logger.info(`Found ${metaPaths.length} components in ${repo}`);

    const results = await pMap(
      metaPaths,
      async ({ componentClass, dirName, path: filePath }): Promise<ComponentEntry | null> => {
        try {
          const meta = await fetchMetadata(owner, repo, filePath);
          if (!meta) {
            logger.warning(`No type: field in ${filePath}`);
            return null;
          }
          return {
            key: `${componentClass}:${meta.typeName}`,
            url: `https://github.com/${owner}/${repo}/blob/main/${componentClass}/${dirName}/README.md`,
            displayName: meta.displayName,
            repo: label,
          };
        } catch (err: unknown) {
          logger.warning(
            `Failed to fetch ${filePath}: ${err instanceof Error ? err.message : err}`
          );
          return null;
        }
      },
      { concurrency: CONCURRENCY }
    );

    entries.push(...results.filter((r): r is ComponentEntry => r !== null));
  }

  return entries.sort((a, b) => a.key.localeCompare(b.key));
};

const generateTypeScript = (entries: ComponentEntry[]): string => {
  const entryLines = entries.map((e) => {
    const displayLine = e.displayName
      ? `\n    displayName: '${e.displayName.replace(/'/g, "\\'")}',`
      : '';
    return `  '${e.key}': {\n    url: '${e.url}',${displayLine}\n    repo: '${e.repo}',\n  },`;
  });

  return `${LICENSE_HEADER}

// Auto-generated by scripts/generate_otel_component_doc_links
// Do not edit manually. Re-run: node scripts/generate_otel_component_doc_links/index.js
//
// Generated: ${new Date().toISOString()}
// Components: ${entries.length}

export interface ComponentDocLink {
  url: string;
  displayName?: string;
  repo: 'core' | 'contrib';
}

const COMPONENT_DOC_LINKS: Record<string, ComponentDocLink> = {
${entryLines.join('\n')}
};

// Aliases for components whose upstream type name differs from the
// name commonly used in OTel Collector configs.
const ALIASES: Record<string, string> = {
  'exporter:otlp': 'exporter:otlp_grpc',
  'exporter:otlphttp': 'exporter:otlp_http',
};

export const getComponentDocUrl = (
  type: string,
  name: string
): ComponentDocLink | null => {
  const baseName = name.split('/')[0];
  const key = \`\${type}:\${baseName}\`;
  return COMPONENT_DOC_LINKS[key] ?? COMPONENT_DOC_LINKS[ALIASES[key] ?? ''] ?? null;
};
`;
};

export const run = async () => {
  const entries = await fetchAllEntries();
  logger.info(`Total components: ${entries.length}`);

  const content = generateTypeScript(entries);
  fs.mkdirSync(path.dirname(OUTPUT_PATH), { recursive: true });
  fs.writeFileSync(OUTPUT_PATH, content, 'utf8');
  logger.info(`Written to ${OUTPUT_PATH}`);
};
