/**
 * Derives a human-readable "adjective-noun" label from the first two bytes of
 * an effective_config_hash (64-char hex SHA-256). Same mapping as fleet-server's
 * LabelFromHash. Returns '' for empty or malformed input.
 */
export function labelFromHash(hexHash: string): string {
  if (hexHash.length < 4) return '';
  const b0 = parseInt(hexHash.slice(0, 2), 16);
  const b1 = parseInt(hexHash.slice(2, 4), 16);
  if (isNaN(b0) || isNaN(b1)) return '';
  return `${LABEL_ADJECTIVES[b0]}-${LABEL_NOUNS[b1]}`;
}

export function buildConfigLabelRuntimeField() {
  const adjList = LABEL_ADJECTIVES.map((w) => `'${w}'`).join(',');
  const nounList = LABEL_NOUNS.map((w) => `'${w}'`).join(',');
  const source =
    `if (!doc.containsKey('effective_config_hash') || doc['effective_config_hash'].empty) return;` +
    `String hash = doc['effective_config_hash'].value;` +
    `if (hash.length() < 4) return;` +
    `int b0 = Integer.parseInt(hash.substring(0, 2), 16);` +
    `int b1 = Integer.parseInt(hash.substring(2, 4), 16);` +
    `def adj = [${adjList}];` +
    `def noun = [${nounList}];` +
    `emit(adj[b0] + '-' + noun[b1]);`;
  return {
    effective_config_label: {
      type: 'keyword' as const,
      script: {
        lang: 'painless' as const,
        source,
      },
    },
  };
}
