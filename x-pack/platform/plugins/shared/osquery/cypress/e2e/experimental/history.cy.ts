import { navigateTo } from '../../tasks/navigation';
import { checkResults, inputQuery, selectAllAgents, submitQuery } from '../../tasks/live_query';
import {
  loadLiveQuery,
  addTagsToLiveQuery,
  loadPack,
  cleanupPack,
  getPackSavedObject,
  loadScheduledResponse,
  cleanupScheduledResponse,
} from '../../tasks/api_fixtures';
import {
  UNIFIED_HISTORY_TABLE,
  HISTORY_SEARCH_INPUT,
  HISTORY_DATE_PICKER,
  HISTORY_RUN_BY_FILTER,
  HISTORY_SOURCE_FILTER,
  HISTORY_TAGS_FILTER,
  UNIFIED_HISTORY_PAGINATION,
} from '../../screens/experimental';
import { ServerlessRoleName } from '../../support/roles';

describe(
  'EXPERIMENTAL - History (queryHistoryRework)',
  {
    tags: ['@ess', '@experimental'],
    env: {
      ftrConfig: {
        kbnServerArgs: [
          `--xpack.osquery.enableExperimental=${JSON.stringify([
            'queryHistoryRework',
            'unifiedDataTable',
          ])}`,
        ],
      },
    },
  },
  () => {
    let packId: string;
    let scheduledResponseDocId: string;

    before(() => {
      cy.login(ServerlessRoleName.SOC_MANAGER);

      // Create diverse live queries for filter testing
      // Query 1: uptime query tagged with "investigation"
      loadLiveQuery({
        agent_all: true,
        query: 'select * from uptime;',
        kuery: '',
      }).then((data) => {
        addTagsToLiveQuery(data.action_id, ['investigation', 'host-info']);
      });

      // Query 2: processes query tagged with "incident"
      loadLiveQuery({
        agent_all: true,
        query: 'select * from processes;',
        kuery: '',
      }).then((data) => {
        addTagsToLiveQuery(data.action_id, ['incident']);
      });

      // Query 3: os_version query with no tags
      loadLiveQuery({
        agent_all: true,
        query: 'select * from os_version;',
        kuery: '',
      });

      // Create a pack and a mock scheduled response so the Scheduled source filter has data
      loadPack({
        enabled: true,
        queries: {
          scheduled_uptime: {
            ecs_mapping: {},
            interval: 3600,
            query: 'select * from uptime;',
          },
        },
      }).then((pack) => {
        packId = pack.saved_object_id;
        // Read the raw saved object to get the schedule_id generated by the server
        getPackSavedObject(pack.saved_object_id).then((packSO) => {
          const scheduleId = packSO?.attributes.queries[0]?.schedule_id;
          // eslint-disable-next-line @typescript-eslint/no-unused-expressions
          expect(scheduleId, 'Scheduled query schedule_id should exist').to.be.ok;
          if (scheduleId) {
            // Write a mock scheduled response doc to ES with the real schedule_id
            loadScheduledResponse({
              scheduleId,
              packId: pack.saved_object_id,
              executionCount: 1,
              agentId: 'mock-agent-001',
              resultCount: 10,
            }).then((response) => {
              scheduledResponseDocId = response.body._id;
            });
          }
        });
      });
    });

    after(() => {
      if (scheduledResponseDocId) {
        cleanupScheduledResponse(scheduledResponseDocId);
      }

      if (packId) {
        cleanupPack(packId);
      }
    });

    beforeEach(() => {
      cy.login(ServerlessRoleName.SOC_MANAGER);
      navigateTo('/app/osquery/history');
      cy.getBySel(UNIFIED_HISTORY_TABLE, { timeout: 60000 }).should('exist');
    });

    it('should render the history table with columns, rows, and pagination', () => {
      // Table has rows
      cy.getBySel(UNIFIED_HISTORY_TABLE).find('tbody tr').should('have.length.above', 0);

      // Expected columns are present
      cy.getBySel(UNIFIED_HISTORY_TABLE).within(() => {
        cy.contains('Query').should('exist');
        cy.contains('Tags').should('exist');
        cy.contains('Results').should('exist');
        cy.contains('Source').should('exist');
        cy.contains('Agents').should('exist');
        cy.contains('Created at').should('exist');
        cy.contains('Run by').should('exist');
      });

      // Action buttons on each row
      cy.getBySel(UNIFIED_HISTORY_TABLE).within(() => {
        cy.get('tbody tr')
          .first()
          .within(() => {
            cy.get('[aria-label="Details"]').should('exist');
          });
      });

      // Pagination controls
      cy.getBySel(UNIFIED_HISTORY_PAGINATION).should('exist');

      // Date picker with default range
      cy.getBySel(HISTORY_DATE_PICKER).should('exist');
      cy.getBySel(HISTORY_DATE_PICKER).within(() => {
        cy.contains('Last 24 hours').should('exist');
      });
    });

    it('should filter history via search input', () => {
      // Search input exists with correct placeholder
      cy.getBySel(HISTORY_SEARCH_INPUT)
        .should('exist')
        .and('have.attr', 'placeholder', 'Search by query or pack name');

      // Filter to "uptime" queries
      cy.getBySel(HISTORY_SEARCH_INPUT).type('uptime{enter}');
      cy.getBySel(UNIFIED_HISTORY_TABLE).find('tbody tr').should('have.length.above', 0);
      cy.getBySel(UNIFIED_HISTORY_TABLE)
        .find('tbody tr')
        .each(($row) => {
          expect($row.text()).to.include('uptime');
        });

      // Filter to "processes" queries
      cy.getBySel(HISTORY_SEARCH_INPUT).clear().type('processes{enter}');
      cy.getBySel(UNIFIED_HISTORY_TABLE).find('tbody tr').should('have.length.above', 0);
      cy.getBySel(UNIFIED_HISTORY_TABLE)
        .find('tbody tr')
        .each(($row) => {
          expect($row.text()).to.include('processes');
        });

      // Non-matching search shows empty state
      cy.getBySel(HISTORY_SEARCH_INPUT).clear().type('zzz_nonexistent_query_zzz{enter}');
      cy.getBySel(UNIFIED_HISTORY_TABLE).contains('No items found').should('exist');

      // Clearing search restores all results
      cy.getBySel(HISTORY_SEARCH_INPUT).clear().type('{enter}');
      cy.getBySel(UNIFIED_HISTORY_TABLE).find('tbody tr').should('have.length.above', 1);
    });

    it('should filter history by source type', () => {
      // Source filter button exists
      cy.getBySel(HISTORY_SOURCE_FILTER).should('exist');

      // Popover shows Live, Rule, and Scheduled options
      cy.getBySel(HISTORY_SOURCE_FILTER).click();
      cy.getBySel('history-source-filter-popover').should('exist');
      cy.getBySel('history-source-filter-live').should('exist');
      cy.getBySel('history-source-filter-rule').should('exist');
      cy.getBySel('history-source-filter-scheduled').should('exist');

      // Filter by Live source
      cy.getBySel('history-source-filter-live').click();
      cy.get('body').type('{esc}');
      cy.getBySel(UNIFIED_HISTORY_TABLE).find('tbody tr').should('have.length.above', 0);
      cy.getBySel(UNIFIED_HISTORY_TABLE).find('tbody tr').first().should('contain', 'Live');

      // Reset: reload the page to clear filters
      navigateTo('/app/osquery/history');
      cy.getBySel(UNIFIED_HISTORY_TABLE, { timeout: 60000 }).should('exist');

      // Filter by Scheduled source
      cy.getBySel(HISTORY_SOURCE_FILTER).click();
      cy.getBySel('history-source-filter-scheduled').click();
      cy.get('body').type('{esc}');
      cy.getBySel(UNIFIED_HISTORY_TABLE).find('tbody tr').should('have.length.above', 0);
      cy.getBySel(UNIFIED_HISTORY_TABLE).find('tbody tr').first().should('contain', 'Scheduled');

      // Reset
      navigateTo('/app/osquery/history');
      cy.getBySel(UNIFIED_HISTORY_TABLE, { timeout: 60000 }).should('exist');

      // Filter by Rule source — no rule-based queries exist
      cy.getBySel(HISTORY_SOURCE_FILTER).click();
      cy.getBySel('history-source-filter-rule').click();
      cy.get('body').type('{esc}');
      cy.getBySel(UNIFIED_HISTORY_TABLE).contains('No items found').should('exist');
    });

    it('should filter history by tags', () => {
      // Tags filter button exists
      cy.getBySel(HISTORY_TAGS_FILTER).should('exist');

      // Popover shows available tags
      cy.getBySel(HISTORY_TAGS_FILTER).click();
      cy.getBySel('history-tags-filter-popover').should('exist');
      cy.getBySel('history-tags-filter-popover').within(() => {
        cy.contains('investigation').should('exist');
        cy.contains('incident').should('exist');
        cy.contains('host-info').should('exist');
      });

      // Filter by "investigation" tag
      cy.intercept('GET', '/api/osquery/history*').as('historyApi');
      cy.getBySel('history-tags-filter-popover').within(() => {
        cy.contains('investigation').click();
      });
      cy.get('body').type('{esc}');
      cy.wait('@historyApi').its('request.url').should('include', 'investigation');
      cy.getBySel(UNIFIED_HISTORY_TABLE).find('tbody tr').should('have.length.above', 0);
      cy.getBySel(UNIFIED_HISTORY_TABLE)
        .find('tbody tr')
        .each(($row) => {
          cy.wrap($row).should('contain', 'uptime');
        });

      // Clear tag filter and restore all results
      cy.getBySel(HISTORY_TAGS_FILTER).click();
      cy.getBySel('history-tags-filter-clear').click();
      cy.get('body').type('{esc}');
      cy.getBySel(UNIFIED_HISTORY_TABLE).find('tbody tr').should('have.length.above', 1);

      // Filter by "incident" tag
      cy.intercept('GET', '/api/osquery/history*').as('historyApiIncident');
      cy.getBySel(HISTORY_TAGS_FILTER).click();
      cy.getBySel('history-tags-filter-popover').within(() => {
        cy.contains('incident').click();
      });
      cy.get('body').type('{esc}');
      cy.wait('@historyApiIncident').its('request.url').should('include', 'incident');
      cy.getBySel(UNIFIED_HISTORY_TABLE).find('tbody tr').should('have.length.above', 0);
      cy.getBySel(UNIFIED_HISTORY_TABLE)
        .find('tbody tr')
        .each(($row) => {
          cy.wrap($row).should('contain', 'processes');
        });
    });

    it('should filter by run-by user and combine filters', () => {
      // Run by filter button exists and opens popover
      cy.getBySel(HISTORY_RUN_BY_FILTER).should('exist');
      cy.getBySel(HISTORY_RUN_BY_FILTER).click();
      cy.getBySel('history-run-by-filter-popover').should('exist');

      // Select the current user
      cy.getBySel('history-run-by-filter-popover').within(() => {
        cy.get('[role="option"]').first().click();
      });
      cy.get('body').type('{esc}');
      cy.getBySel(UNIFIED_HISTORY_TABLE).find('tbody tr').should('have.length.above', 0);

      // Reset
      navigateTo('/app/osquery/history');
      cy.getBySel(UNIFIED_HISTORY_TABLE, { timeout: 60000 }).should('exist');

      // Combined filters: search + tag
      cy.getBySel(HISTORY_SEARCH_INPUT).type('uptime{enter}');
      cy.getBySel(UNIFIED_HISTORY_TABLE).find('tbody tr').should('have.length.above', 0);

      cy.getBySel(HISTORY_TAGS_FILTER).click();
      cy.getBySel('history-tags-filter-popover').within(() => {
        cy.contains('investigation').click();
      });
      cy.get('body').type('{esc}');

      cy.getBySel(UNIFIED_HISTORY_TABLE).find('tbody tr').should('have.length.above', 0);
      cy.getBySel(UNIFIED_HISTORY_TABLE)
        .find('tbody tr')
        .each(($row) => {
          cy.wrap($row).should('contain', 'uptime');
        });
    });

    it('should navigate to details and run a new query visible in history', () => {
      // Click details button navigates to detail view
      cy.getBySel(UNIFIED_HISTORY_TABLE).within(() => {
        cy.get('tbody tr')
          .first()
          .within(() => {
            cy.get('[aria-label="Details"]').click();
          });
      });
      cy.url().should('include', '/app/osquery/history/');

      // Run a new query and verify it appears in history
      navigateTo('/app/osquery/new');
      selectAllAgents();
      inputQuery('select * from os_version;');
      submitQuery();
      checkResults();

      navigateTo('/app/osquery/history');
      cy.getBySel(UNIFIED_HISTORY_TABLE, { timeout: 60000 }).should('exist');
      cy.getBySel(UNIFIED_HISTORY_TABLE).find('tbody tr').first().should('contain', 'os_version');
    });
  }
);
