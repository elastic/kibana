import type { ElasticsearchClient } from '@kbn/core-elasticsearch-server';
import type { Logger } from '@kbn/logging';

/**
 * A memory entry as stored in the main memory index.
 */
export interface MemoryEntry {
  /** Unique ID */
  id: string;
  /** Wiki-style path, e.g. "ops/runbooks/deploy-checklist" */
  path: string;
  /** Human-readable title */
  title: string;
  /** Markdown content */
  content: string;
  /** Parent directory path, e.g. "ops/runbooks" */
  parent_path: string;
  /** Monotonically increasing version per entry */
  version: number;
  /** Tags for classification */
  tags: string[];
  created_at: string;
  updated_at: string;
  created_by: string;
  updated_by: string;
}

/**
 * A version history record for a memory entry.
 */
export interface MemoryVersionRecord {
  id: string;
  entry_id: string;
  version: number;
  path: string;
  title: string;
  content: string;
  change_type: MemoryChangeType;
  change_summary: string;
  created_at: string;
  created_by: string;
}

export type MemoryChangeType = 'create' | 'update' | 'delete' | 'move';

/**
 * A node in the memory tree hierarchy.
 */
export interface MemoryTreeNode {
  path: string;
  title: string;
  id?: string;
  has_children: boolean;
  children: MemoryTreeNode[];
}

/**
 * A memory search result with relevance score.
 */
export interface MemorySearchResult {
  id: string;
  path: string;
  title: string;
  snippet: string;
  score: number;
  updated_at: string;
  updated_by: string;
  tags: string[];
}

/** Parameters for creating a memory entry */
export interface CreateMemoryParams {
  path: string;
  title: string;
  content: string;
  tags?: string[];
  user: string;
}

/** Parameters for updating a memory entry */
export interface UpdateMemoryParams {
  id: string;
  content?: string;
  title?: string;
  tags?: string[];
  path?: string;
  user: string;
  changeSummary?: string;
}

/** Parameters for searching memory */
export interface SearchMemoryParams {
  query: string;
  tags?: string[];
  parentPath?: string;
  size?: number;
}

/**
 * An open question generated by a background agent about memory quality or gaps.
 */
export interface MemoryQuestion {
  /** Unique ID */
  id: string;
  /** The question text */
  question: string;
  /** Category: 'quality' for confusing/duplicate state, 'gap' for missing knowledge */
  category: MemoryQuestionCategory;
  /** IDs of related memory entries */
  related_entries: string[];
  /** Why this question was generated */
  context: string;
  /** Current status */
  status: MemoryQuestionStatus;
  /** User's answer (filled when answered) */
  answer?: string;
  created_at: string;
  created_by: string;
}

export type MemoryQuestionCategory = 'quality' | 'gap';
export type MemoryQuestionStatus = 'open' | 'answered' | 'dismissed';

/** Parameters for creating a question */
export interface CreateQuestionParams {
  question: string;
  category: MemoryQuestionCategory;
  relatedEntries: string[];
  context: string;
  user: string;
}

/** Dependencies for the memory service */
export interface MemoryServiceDeps {
  logger: Logger;
  esClient: ElasticsearchClient;
}

/**
 * Memory service interface — manages the persistent knowledge base.
 * Memory is global (space-agnostic).
 */
export interface MemoryService {
  // CRUD
  create(params: CreateMemoryParams): Promise<MemoryEntry>;
  get(params: { id: string }): Promise<MemoryEntry>;
  getByPath(params: { path: string }): Promise<MemoryEntry | undefined>;
  update(params: UpdateMemoryParams): Promise<MemoryEntry>;
  delete(params: { id: string; user: string }): Promise<void>;
  move(params: { id: string; newPath: string; user: string }): Promise<MemoryEntry>;

  // Search & browse
  search(params: SearchMemoryParams): Promise<MemorySearchResult[]>;
  listChildren(params: { parentPath: string }): Promise<MemoryEntry[]>;
  listAll(): Promise<MemoryEntry[]>;
  getTree(): Promise<MemoryTreeNode[]>;

  // History
  getHistory(params: { entryId: string; size?: number }): Promise<MemoryVersionRecord[]>;
  getVersion(params: { entryId: string; version: number }): Promise<MemoryVersionRecord>;
  rollback(params: { entryId: string; version: number; user: string }): Promise<MemoryEntry>;

  // Recent changes across all entries
  getRecentChanges(params: { size?: number }): Promise<MemoryVersionRecord[]>;

  // Questions
  createQuestion(params: CreateQuestionParams): Promise<MemoryQuestion>;
  getOpenQuestions(params: { size?: number }): Promise<MemoryQuestion[]>;
  answerQuestion(params: { id: string; answer: string }): Promise<MemoryQuestion>;
  dismissQuestion(params: { id: string }): Promise<void>;
}
