import type { RootSchema, SchemaObject } from '@elastic/ebt/client';
import type { FeatureType } from '@kbn/streams-schema';
import type {
  StreamEndpointLatencyProps,
  StreamsFeatureIdentificationIdentifiedProps,
  StreamsStateErrorProps,
} from './types';

const streamsEndpointLatencySchema: RootSchema<StreamEndpointLatencyProps> = {
  name: {
    type: 'keyword',
    _meta: {
      description: 'The name of the Stream',
    },
  },
  endpoint: {
    type: 'keyword',
    _meta: {
      description: 'The name of the Streams endpoint',
    },
  },
  duration_ms: {
    type: 'long',
    _meta: {
      description: 'The duration of the endpoint in milliseconds',
    },
  },
};

const streamsStateErrorSchema: RootSchema<StreamsStateErrorProps> = {
  error: {
    properties: {
      name: {
        type: 'keyword',
        _meta: {
          description: 'The name of the error class',
        },
      },
      message: {
        type: 'text',
        _meta: {
          description: 'The error message',
        },
      },
      stack_trace: {
        type: 'text',
        _meta: {
          description: 'The error stack trace',
          optional: true,
        },
      },
    },
  },
  status_code: {
    type: 'long',
    _meta: {
      description: 'The HTTP status code associated with the error',
    },
  },
};

const countByTypes: SchemaObject<{ [key in FeatureType]: number }> = {
  _meta: {
    description: 'The count of identified features or significant events by type',
  },
  properties: {
    system: {
      type: 'long',
      _meta: {
        description:
          'The count of system features or significant events generated by system features',
      },
    },
  },
};

const streamsFeatureIdentificationIdentifiedSchema: RootSchema<StreamsFeatureIdentificationIdentifiedProps> =
  {
    count: {
      type: 'long',
      _meta: {
        description: 'The number of features identified',
      },
    },
    count_by_type: countByTypes,
    input_tokens_used: {
      type: 'long',
      _meta: {
        description: 'The number of input tokens used for the generation request',
      },
    },
    output_tokens_used: {
      type: 'long',
      _meta: {
        description: 'The number of output tokens used for the generation request',
      },
    },
    stream_type: {
      type: 'keyword',
      _meta: {
        description: 'The type of the stream: wired or classic',
      },
    },
    stream_name: {
      type: 'keyword',
      _meta: {
        description: 'The name of the Stream',
      },
    },
  };

export {
  streamsEndpointLatencySchema,
  streamsStateErrorSchema,
  streamsFeatureIdentificationIdentifiedSchema,
};
