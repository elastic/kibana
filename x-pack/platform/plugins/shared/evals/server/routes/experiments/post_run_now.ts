import { randomUUID } from 'crypto';
import { API_VERSIONS, INTERNAL_API_ACCESS, buildRouteValidationWithZod } from '@kbn/evals-common';
import { getSpaceIdFromPath } from '@kbn/spaces-utils';
import type { WorkflowExecutionEngineModel, WorkflowYaml } from '@kbn/workflows';
import { WorkflowsManagementApiActions } from '@kbn/workflows/common/privileges';
import { z } from '@kbn/zod';
import { PLUGIN_ID } from '../../../common';
import type { RouteDependencies } from '../register_routes';

const RunExperimentSuiteNowRequestBody = z.object({
  suite_id: z.string().min(1),
  task_connector_id: z.string().min(1),
  judge_connector_id: z.string().min(1),
  suite_params: z.record(z.string(), z.unknown()).optional(),
  repetitions: z.number().int().min(1).optional(),
});

function buildWorkflowYaml(definition: WorkflowYaml): string {
  return `# Generated by the Evals plugin (run now)\n${JSON.stringify(definition, null, 2)}\n`;
}

export const registerRunExperimentSuiteNowRoute = ({
  router,
  logger,
  experimentSuiteRegistry,
  workflowsManagement,
}: RouteDependencies) => {
  router.versioned
    .post({
      path: '/internal/evals/experiments/runs/run_now',
      access: INTERNAL_API_ACCESS,
      security: {
        authz: {
          requiredPrivileges: [PLUGIN_ID, WorkflowsManagementApiActions.execute],
        },
      },
      summary: 'Start an experiment run (run now)',
    })
    .addVersion(
      {
        version: API_VERSIONS.internal.v1,
        validate: {
          request: {
            body: buildRouteValidationWithZod(RunExperimentSuiteNowRequestBody),
          },
        },
      },
      async (_context, request, response) => {
        try {
          if (!workflowsManagement) {
            return response.customError({
              statusCode: 503,
              body: { message: 'workflowsManagement plugin is not available' },
            });
          }

          if (!experimentSuiteRegistry) {
            return response.customError({
              statusCode: 500,
              body: { message: 'Experiment suite registry is not available' },
            });
          }

          const suiteId = request.body.suite_id;
          const suite = experimentSuiteRegistry.getById(suiteId);
          if (!suite) {
            return response.notFound({
              body: { message: `Unknown suite_id: ${suiteId}` },
            });
          }

          const { spaceId } = getSpaceIdFromPath(request.url.pathname);

          const runId = randomUUID();
          const suiteParams = request.body.suite_params ?? {};
          const repetitions = request.body.repetitions;

          const workflowDefinition: WorkflowYaml = {
            version: '1',
            name: `Evals: ${suiteId}`,
            description: suite.description,
            enabled: true,
            triggers: [{ type: 'manual' }],
            steps: [
              {
                name: 'run_suite',
                type: 'evals.runSuite',
                with: {
                  suite_id: suiteId,
                  task_connector_id: request.body.task_connector_id,
                  judge_connector_id: request.body.judge_connector_id,
                  suite_params: suiteParams,
                  ...(repetitions ? { repetitions } : {}),
                },
              },
            ],
          };

          const workflowForExecution: WorkflowExecutionEngineModel = {
            id: `evals.run_now.${runId}`,
            name: workflowDefinition.name,
            enabled: true,
            definition: workflowDefinition,
            yaml: buildWorkflowYaml(workflowDefinition),
          };

          const workflowExecutionId = await workflowsManagement.management.runWorkflow(
            workflowForExecution,
            spaceId,
            {},
            request,
            undefined,
            {
              run_id: runId,
              suite_id: suiteId,
              source: 'experiment',
            }
          );

          return response.ok({
            body: {
              run_id: runId,
              suite_id: suiteId,
              workflow_execution_id: workflowExecutionId,
            },
          });
        } catch (error) {
          logger.error(`Failed to start experiment run (run now): ${error}`);
          return response.customError({
            statusCode: 500,
            body: { message: 'Failed to start experiment run' },
          });
        }
      }
    );
};
