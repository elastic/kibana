import {
  EuiBasicTable,
  EuiButton,
  EuiButtonEmpty,
  EuiCallOut,
  EuiFieldText,
  EuiFlexGroup,
  EuiFlexItem,
  EuiFlyout,
  EuiFlyoutBody,
  EuiFlyoutFooter,
  EuiFlyoutHeader,
  EuiForm,
  EuiFormRow,
  EuiSelect,
  EuiSpacer,
  EuiStepsHorizontal,
  EuiTextArea,
  EuiTitle,
  useEuiTheme,
} from '@elastic/eui';
import { css } from '@emotion/react';
import { i18n } from '@kbn/i18n';
import React, { useCallback, useMemo, useState } from 'react';
import { StreamsAppPageTemplate } from '../streams_app_page_template';

type EntityTypeRow = {
  id: string;
  name: string;
  generatedBy: string;
  category: string;
  entities: number;
  subsets: number;
  managed: boolean;
};

const MOCK_ENTITY_TYPES: EntityTypeRow[] = [
  {
    id: '1',
    name: 'K8s cluster',
    generatedBy: 'Elastic',
    category: 'Kubernetes',
    entities: 24,
    subsets: 5,
    managed: true,
  },
  {
    id: '2',
    name: 'K8s node',
    generatedBy: 'Elastic',
    category: 'Kubernetes',
    entities: 120,
    subsets: 3,
    managed: true,
  },
  {
    id: '3',
    name: 'APM Service',
    generatedBy: 'User',
    category: 'Services',
    entities: 42,
    subsets: 2,
    managed: false,
  },
  {
    id: '4',
    name: 'Host',
    generatedBy: 'Elastic',
    category: 'Host',
    entities: 88,
    subsets: 7,
    managed: true,
  },
];

const EDIT_STEP_IDS = ['general', 'health', 'ownership', 'subsets', 'flyout'] as const;
type EditStepId = (typeof EDIT_STEP_IDS)[number];

const editStepIndex = (id: EditStepId) => EDIT_STEP_IDS.indexOf(id);

export function ManageEntityTypesPage() {
  const { euiTheme } = useEuiTheme();
  const [isCreateFlyoutOpen, setIsCreateFlyoutOpen] = useState(false);
  const [editingRow, setEditingRow] = useState<EntityTypeRow | null>(null);
  const [editStep, setEditStep] = useState<EditStepId>('general');

  const closeCreateFlyout = useCallback(() => setIsCreateFlyoutOpen(false), []);
  const closeEditFlyout = useCallback(() => {
    setEditingRow(null);
    setEditStep('general');
  }, []);

  const horizontalSteps = useMemo(
    () =>
      EDIT_STEP_IDS.map((id, index) => {
        const currentIdx = editStepIndex(editStep);
        let status: 'complete' | 'current' | 'incomplete' = 'incomplete';
        if (index < currentIdx) {
          status = 'complete';
        } else if (index === currentIdx) {
          status = 'current';
        }
        return {
          title: stepTitle(id),
          status,
        };
      }),
    [editStep]
  );

  const columns = useMemo(
    () => [
      {
        field: 'name',
        name: i18n.translate('xpack.streams.manageEntityTypes.column.entityType', {
          defaultMessage: 'Entity type',
        }),
        render: (name: string, row: EntityTypeRow) => (
          <EuiButtonEmpty size="xs" onClick={() => setEditingRow(row)}>
            {name}
          </EuiButtonEmpty>
        ),
      },
      {
        field: 'generatedBy',
        name: i18n.translate('xpack.streams.manageEntityTypes.column.generatedBy', {
          defaultMessage: 'Generated by',
        }),
      },
      {
        field: 'category',
        name: i18n.translate('xpack.streams.manageEntityTypes.column.category', {
          defaultMessage: 'Category',
        }),
      },
      {
        field: 'entities',
        name: i18n.translate('xpack.streams.manageEntityTypes.column.entities', {
          defaultMessage: 'Entities',
        }),
      },
      {
        field: 'subsets',
        name: i18n.translate('xpack.streams.manageEntityTypes.column.subsets', {
          defaultMessage: 'Subsets',
        }),
      },
    ],
    []
  );

  return (
    <>
      <StreamsAppPageTemplate.Header
        bottomBorder="extended"
        css={css`
          background: ${euiTheme.colors.backgroundBasePlain};
        `}
        pageTitle={
          <EuiFlexGroup
            justifyContent="spaceBetween"
            gutterSize="s"
            responsive={false}
            alignItems="center"
          >
            <EuiFlexItem>
              {i18n.translate('xpack.streams.manageEntityTypes.pageTitle', {
                defaultMessage: 'Manage entity types',
              })}
            </EuiFlexItem>
            <EuiFlexItem grow={false}>
              <EuiButton fill iconType="plusInCircle" onClick={() => setIsCreateFlyoutOpen(true)}>
                {i18n.translate('xpack.streams.manageEntityTypes.createButton', {
                  defaultMessage: 'Create new entity type',
                })}
              </EuiButton>
            </EuiFlexItem>
          </EuiFlexGroup>
        }
      />
      <StreamsAppPageTemplate.Body grow>
        <EuiBasicTable<EntityTypeRow>
          tableCaption={i18n.translate('xpack.streams.manageEntityTypes.tableCaption', {
            defaultMessage: 'Entity types',
          })}
          items={MOCK_ENTITY_TYPES}
          columns={columns}
          rowHeader="name"
        />
      </StreamsAppPageTemplate.Body>

      {isCreateFlyoutOpen ? (
        <EuiFlyout
          ownFocus
          onClose={closeCreateFlyout}
          aria-labelledby="streamsCreateEntityTypeFlyoutTitle"
          size="m"
        >
          <EuiFlyoutHeader hasBorder>
            <EuiTitle size="m">
              <h2 id="streamsCreateEntityTypeFlyoutTitle">
                {i18n.translate('xpack.streams.manageEntityTypes.createFlyoutTitle', {
                  defaultMessage: 'Create entity type',
                })}
              </h2>
            </EuiTitle>
          </EuiFlyoutHeader>
          <EuiFlyoutBody>
            <p>
              {i18n.translate('xpack.streams.manageEntityTypes.createFlyoutPlaceholder', {
                defaultMessage: 'This is a placeholder for the create entity type flow.',
              })}
            </p>
          </EuiFlyoutBody>
        </EuiFlyout>
      ) : null}

      {editingRow ? (
        <EuiFlyout
          ownFocus
          onClose={closeEditFlyout}
          aria-labelledby="streamsEditEntityTypeFlyoutTitle"
          size="l"
        >
          <EuiFlyoutHeader hasBorder>
            <EuiTitle size="m">
              <h2 id="streamsEditEntityTypeFlyoutTitle">
                {i18n.translate('xpack.streams.manageEntityTypes.editFlyoutTitle', {
                  defaultMessage: 'Edit {entityTypeName} entity type',
                  values: { entityTypeName: editingRow.name },
                })}
              </h2>
            </EuiTitle>
            <EuiSpacer size="s" />
            <EuiFlexGroup gutterSize="s" wrap responsive={false}>
              <EuiFlexItem grow={false}>
                {i18n.translate('xpack.streams.manageEntityTypes.editFlyoutLastUpdate', {
                  defaultMessage: 'Last update 2024-04-20',
                })}
              </EuiFlexItem>
              {editingRow.managed ? (
                <EuiFlexItem grow={false}>
                  {i18n.translate('xpack.streams.manageEntityTypes.editFlyoutManagedBadge', {
                    defaultMessage: 'Elastic managed',
                  })}
                </EuiFlexItem>
              ) : null}
              <EuiFlexItem grow={false}>
                {i18n.translate('xpack.streams.manageEntityTypes.editFlyoutMatchingEntities', {
                  defaultMessage: '{count} matching entities',
                  values: { count: editingRow.entities },
                })}
              </EuiFlexItem>
              <EuiFlexItem grow={false}>
                {i18n.translate('xpack.streams.manageEntityTypes.editFlyoutSubsetsCount', {
                  defaultMessage: '{count} subsets',
                  values: { count: editingRow.subsets },
                })}
              </EuiFlexItem>
            </EuiFlexGroup>
          </EuiFlyoutHeader>
          <EuiFlyoutBody>
            <EuiStepsHorizontal size="s" steps={horizontalSteps} />
            <EuiSpacer size="l" />
            {editStep === 'general' ? (
              <>
                <EuiCallOut
                  title={i18n.translate('xpack.streams.manageEntityTypes.generalManagedCalloutTitle', {
                    defaultMessage: 'Managed entity types',
                  })}
                  color="primary"
                >
                  {i18n.translate('xpack.streams.manageEntityTypes.generalManagedCalloutBody', {
                    defaultMessage:
                      'Managed entity types general data cannot be all customized.',
                  })}
                </EuiCallOut>
                <EuiSpacer size="m" />
                <EuiForm component="form">
                  <EuiFormRow
                    label={i18n.translate('xpack.streams.manageEntityTypes.field.identifier', {
                      defaultMessage: 'Identifier',
                    })}
                  >
                    <EuiFieldText
                      defaultValue={editingRow.name}
                      disabled={editingRow.managed}
                      data-test-subj="streamsManageEntityTypesIdentifier"
                    />
                  </EuiFormRow>
                  <EuiFormRow
                    label={i18n.translate('xpack.streams.manageEntityTypes.field.dataStream', {
                      defaultMessage: 'Data stream',
                    })}
                    helpText={i18n.translate('xpack.streams.manageEntityTypes.field.dataStreamHelp', {
                      defaultMessage: 'Example: logs-kubernetes.cluster-default',
                    })}
                  >
                    <EuiFieldText
                      defaultValue=""
                      placeholder={i18n.translate(
                        'xpack.streams.manageEntityTypes.field.dataStreamPlaceholder',
                        { defaultMessage: 'Select or enter a data stream' }
                      )}
                      data-test-subj="streamsManageEntityTypesDataStream"
                    />
                  </EuiFormRow>
                  <EuiFormRow
                    label={i18n.translate('xpack.streams.manageEntityTypes.field.description', {
                      defaultMessage: 'Description',
                    })}
                  >
                    <EuiTextArea
                      rows={3}
                      defaultValue=""
                      data-test-subj="streamsManageEntityTypesDescription"
                    />
                  </EuiFormRow>
                  <EuiFormRow
                    label={i18n.translate('xpack.streams.manageEntityTypes.field.category', {
                      defaultMessage: 'Category',
                    })}
                  >
                    <EuiSelect
                      options={[
                        {
                          value: editingRow.category,
                          text: editingRow.category,
                        },
                      ]}
                      value={editingRow.category}
                      disabled
                      data-test-subj="streamsManageEntityTypesCategory"
                      onChange={() => {}}
                    />
                  </EuiFormRow>
                </EuiForm>
              </>
            ) : (
              <p>
                {i18n.translate('xpack.streams.manageEntityTypes.stepPlaceholder', {
                  defaultMessage: 'Content for this step will be defined later.',
                })}
              </p>
            )}
          </EuiFlyoutBody>
          <EuiFlyoutFooter>
            <EuiFlexGroup justifyContent="spaceBetween" alignItems="center">
              <EuiFlexItem grow={false}>
                <EuiButtonEmpty onClick={closeEditFlyout}>
                  {i18n.translate('xpack.streams.manageEntityTypes.cancelButton', {
                    defaultMessage: 'Cancel',
                  })}
                </EuiButtonEmpty>
              </EuiFlexItem>
              <EuiFlexItem grow={false}>
                <EuiFlexGroup gutterSize="s">
                  <EuiButtonEmpty>
                    {i18n.translate('xpack.streams.manageEntityTypes.saveModificationsButton', {
                      defaultMessage: 'Save modifications',
                    })}
                  </EuiButtonEmpty>
                  <EuiButton
                    fill
                    onClick={() => {
                      const next = editStepIndex(editStep) + 1;
                      if (next < EDIT_STEP_IDS.length) {
                        setEditStep(EDIT_STEP_IDS[next]!);
                      }
                    }}
                  >
                    {i18n.translate('xpack.streams.manageEntityTypes.nextStepButton', {
                      defaultMessage: 'Next step',
                    })}
                  </EuiButton>
                </EuiFlexGroup>
              </EuiFlexItem>
            </EuiFlexGroup>
          </EuiFlyoutFooter>
        </EuiFlyout>
      ) : null}
    </>
  );
}

function stepTitle(id: EditStepId): string {
  switch (id) {
    case 'general':
      return i18n.translate('xpack.streams.manageEntityTypes.step.general', {
        defaultMessage: 'General',
      });
    case 'health':
      return i18n.translate('xpack.streams.manageEntityTypes.step.health', {
        defaultMessage: 'Health',
      });
    case 'ownership':
      return i18n.translate('xpack.streams.manageEntityTypes.step.ownership', {
        defaultMessage: 'Ownership',
      });
    case 'subsets':
      return i18n.translate('xpack.streams.manageEntityTypes.step.subsets', {
        defaultMessage: 'Subsets',
      });
    case 'flyout':
      return i18n.translate('xpack.streams.manageEntityTypes.step.flyoutContent', {
        defaultMessage: 'Flyout content',
      });
    default: {
      const _exhaustive: never = id;
      return _exhaustive;
    }
  }
}
