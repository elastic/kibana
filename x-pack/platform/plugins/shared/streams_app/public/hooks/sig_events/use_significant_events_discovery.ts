import { useMutation, useQuery, useQueryClient } from '@kbn/react-query';
import { i18n } from '@kbn/i18n';
import { WorkflowStatus } from '@kbn/streams-schema';
import { useCallback, useEffect, useMemo, useRef, useState } from 'react';
import { useKibana } from '../use_kibana';
import { useSignificantEventsDiscoveryApi } from './use_significant_events_discovery_api';
import { RUNNING_POLL_INTERVAL_MS } from '../../components/sig_events/constants';

const SIGNIFICANT_EVENT_DISCOVERY_NOTIFIED = ['significant_events_discovery_notified'];

const isTerminalStatus = (status: string) =>
  status === WorkflowStatus.Completed ||
  status === WorkflowStatus.Failed ||
  status === WorkflowStatus.Canceled;

interface UseSignificantEventsDiscoveryOptions {
  onComplete?: () => void;
}

export const useSignificantEventsDiscovery = ({
  onComplete,
}: UseSignificantEventsDiscoveryOptions = {}) => {
  const {
    core: {
      notifications: { toasts },
    },
  } = useKibana();

  const queryClient = useQueryClient();

  const onCompleteRef = useRef(onComplete);

  const {
    triggerSignificantEventsDiscovery,
    cancelSignificantEventsDiscovery,
    getSignificantEventsDiscoveryStatus,
  } = useSignificantEventsDiscoveryApi();

  const [significantEventsDiscoveryStatus, setSignificantEventsDiscoveryStatus] =
    useState<WorkflowStatus | null>(null);

  const previousStatusRef = useRef<WorkflowStatus | null>(null);

  const triggeredExecutionIdRef = useRef<string | null>(null);

  const { mutate: triggerMutate } = useMutation({
    mutationFn: triggerSignificantEventsDiscovery,
    onSuccess: ({ executionId }) => {
      triggeredExecutionIdRef.current = executionId;
    },
    onError: (error: Error) => {
      setSignificantEventsDiscoveryStatus(null);
      previousStatusRef.current = null;
      triggeredExecutionIdRef.current = null;
      toasts.addError(error, {
        title: i18n.translate(
          'xpack.streams.SignificantEventsDiscoveryWorkflow.triggerErrorTitle',
          {
            defaultMessage: 'Failed to start discovery pipeline',
          }
        ),
      });
    },
  });

  const { mutate: cancelMutate } = useMutation({
    mutationFn: cancelSignificantEventsDiscovery,
    onError: (error: Error) => {
      toasts.addError(error, {
        title: i18n.translate('xpack.streams.SignificantEventsDiscoveryWorkflow.cancelErrorTitle', {
          defaultMessage: 'Failed to cancel discovery pipeline',
        }),
      });
    },
  });

  // isRunning is declared before useQuery so it can drive refetchInterval.
  // It includes the optimistic InProgress state set by handleRun so polling
  // continues even before the server confirms the new execution.
  const isRunning =
    significantEventsDiscoveryStatus !== null &&
    !isTerminalStatus(significantEventsDiscoveryStatus) &&
    significantEventsDiscoveryStatus !== WorkflowStatus.NotStarted;

  const { data } = useQuery({
    queryKey: ['significant_events_discovery_status'],
    queryFn: getSignificantEventsDiscoveryStatus,
    refetchInterval: isRunning ? RUNNING_POLL_INTERVAL_MS : false,
  });

  useEffect(() => {
    onCompleteRef.current = onComplete;
  }, [onComplete]);

  useEffect(() => {
    const status = data?.status;
    if (!status) return;

    // While optimistic and the poll hasn't reported the new execution id yet,
    // ignore status from the previous run (stale terminal, or pre-creation NotStarted).
    const optimisticRunPending =
      significantEventsDiscoveryStatus === WorkflowStatus.InProgress &&
      (triggeredExecutionIdRef.current === null ||
        data?.executionId !== triggeredExecutionIdRef.current);
    if (optimisticRunPending) return;

    setSignificantEventsDiscoveryStatus(status);

    const previous = previousStatusRef.current;
    previousStatusRef.current = status;

    if (previous === null || !isTerminalStatus(status) || isTerminalStatus(previous)) {
      return;
    }

    // Refresh the caller's time range so new documents generated by the pipeline
    // (whose timestamps may be after the locked absoluteRange.to) become visible.
    onCompleteRef.current?.();

    // Guard against duplicate toasts when multiple hook instances are mounted simultaneously.
    // Uses the shared QueryClient cache so no module-level global is needed.
    if (queryClient.getQueryData<WorkflowStatus>(SIGNIFICANT_EVENT_DISCOVERY_NOTIFIED) === status)
      return;
    queryClient.setQueryData(SIGNIFICANT_EVENT_DISCOVERY_NOTIFIED, status);

    const invalidateAll = () =>
      void Promise.all([
        queryClient.invalidateQueries({ queryKey: ['sigEvents'] }),
        queryClient.invalidateQueries({ queryKey: ['detections'] }),
        queryClient.invalidateQueries({ queryKey: ['discoveriesEntities'] }),
      ]);

    if (status === WorkflowStatus.Completed) {
      // Invalidate immediately, then once more after a short delay to catch
      // data that lands in the index slightly after the status update.
      invalidateAll();
      const timer = setTimeout(invalidateAll, 5 * 1000);

      toasts.addSuccess({
        title: i18n.translate('xpack.streams.SignificantEventsDiscoveryWorkflow.completedTitle', {
          defaultMessage: 'Significant events discovery completed',
        }),
      });

      return () => clearTimeout(timer);
    }

    if (status === WorkflowStatus.Failed) {
      toasts.addDanger({
        title: i18n.translate('xpack.streams.SignificantEventsDiscoveryWorkflow.failedTitle', {
          defaultMessage: 'Significant events discovery failed',
        }),
        text: data?.error,
      });
    } else {
      toasts.addDanger({
        title: i18n.translate('xpack.streams.SignificantEventsDiscoveryWorkflow.canceledTitle', {
          defaultMessage: 'Significant events discovery canceled',
        }),
      });
    }
  }, [
    data?.status,
    data?.executionId,
    data?.error,
    significantEventsDiscoveryStatus,
    toasts,
    queryClient,
  ]);

  const handleRun = useCallback(() => {
    queryClient.removeQueries({ queryKey: SIGNIFICANT_EVENT_DISCOVERY_NOTIFIED });

    // Reset until the trigger resolves with the new id (see optimisticRunPending).    triggeredExecutionIdRef.current = null;
    previousStatusRef.current = WorkflowStatus.InProgress;
    setSignificantEventsDiscoveryStatus(WorkflowStatus.InProgress);
    triggerMutate();
  }, [triggerMutate, queryClient]);

  const handleCancel = useCallback(() => {
    cancelMutate();
  }, [cancelMutate]);

  return useMemo(
    () => ({ isRunning, handleRun, handleCancel }),
    [isRunning, handleRun, handleCancel]
  );
};
