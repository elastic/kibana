import React, { useCallback, useEffect, useRef, useState } from 'react';
import { css } from '@emotion/react';
import { useLayoutContext } from '../../context/layout_context';
import { WorkspacePane } from './workspace_pane';
import { WorkspacePaneResizeHandle } from './workspace_pane_resize_handle';

const MAX_PANES = 4;
const STORAGE_KEY = 'agentBuilder.workspace.panes';

interface PaneState {
  id: string;
  widthFraction: number;
}

const loadPanes = (): PaneState[] => {
  try {
    const stored = localStorage.getItem(STORAGE_KEY);
    if (stored) {
      const parsed = JSON.parse(stored) as PaneState[];
      if (Array.isArray(parsed) && parsed.length > 0 && parsed.length <= MAX_PANES) {
        return parsed;
      }
    }
  } catch {
    // ignore
  }
  return [{ id: 'pane-0', widthFraction: 1.0 }];
};

const savePanes = (panes: PaneState[]): void => {
  try {
    localStorage.setItem(STORAGE_KEY, JSON.stringify(panes));
  } catch {
    // ignore
  }
};

const normalise = (panes: PaneState[]): PaneState[] => {
  const total = panes.reduce((sum, p) => sum + p.widthFraction, 0);
  return panes.map((p) => ({ ...p, widthFraction: p.widthFraction / total }));
};

let paneCounter = Date.now();
const nextPaneId = () => `pane-${++paneCounter}`;

const workspaceStyles = css`
  display: flex;
  flex-direction: row;
  /* Use the concrete layout variable rather than height: 100%, because the EUI page
     template uses min-height (not height) which breaks percentage resolution.
     Generated by layoutVarName('application.content.height') → --kbn-application--content-height */
  height: var(--kbn-application--content-height, 100vh);
  width: 100%;
  overflow: hidden;
`;

export const SplitPaneWorkspace: React.FC = () => {
  const [panes, setPanes] = useState<PaneState[]>(loadPanes);
  const containerRef = useRef<HTMLDivElement>(null);
  const { setIsCondensed } = useLayoutContext();

  // Persist whenever panes change
  useEffect(() => {
    savePanes(panes);
  }, [panes]);

  // Collapse the app sidebar when a second pane is added
  useEffect(() => {
    if (panes.length > 1) {
      setIsCondensed(true);
    } else {
      setIsCondensed(false);
    }
  }, [panes.length, setIsCondensed]);

  const splitPane = useCallback((index: number) => {
    setPanes((prev) => {
      if (prev.length >= MAX_PANES) return prev;
      const next = [...prev];
      const half = next[index].widthFraction / 2;
      next[index] = { ...next[index], widthFraction: half };
      next.splice(index + 1, 0, { id: nextPaneId(), widthFraction: half });
      return normalise(next);
    });
  }, []);

  const closePane = useCallback((index: number) => {
    setPanes((prev) => {
      if (prev.length <= 1) return prev;
      const next = [...prev];
      const removed = next.splice(index, 1)[0];
      // Distribute the closed pane's width to its neighbour (prefer right, fallback left)
      const neighbourIndex = index < next.length ? index : index - 1;
      next[neighbourIndex] = {
        ...next[neighbourIndex],
        widthFraction: next[neighbourIndex].widthFraction + removed.widthFraction,
      };
      return normalise(next);
    });
  }, []);

  const handleFractionsChange = useCallback((newFractions: number[]) => {
    setPanes((prev) =>
      prev.map((p, i) => ({ ...p, widthFraction: newFractions[i] ?? p.widthFraction }))
    );
  }, []);

  const fractions = panes.map((p) => p.widthFraction);

  return (
    <div css={workspaceStyles} ref={containerRef}>
      {panes.map((pane, index) => (
        <React.Fragment key={pane.id}>
          <div style={{ flex: `${pane.widthFraction} ${pane.widthFraction} 0`, minWidth: 0 }}>
            <WorkspacePane
              paneId={pane.id}
              canSplit={panes.length < MAX_PANES}
              canClose={panes.length > 1}
              onSplit={() => splitPane(index)}
              onClose={() => closePane(index)}
            />
          </div>
          {index < panes.length - 1 && (
            <WorkspacePaneResizeHandle
              leftIndex={index}
              containerRef={containerRef}
              fractions={fractions}
              onFractionsChange={handleFractionsChange}
            />
          )}
        </React.Fragment>
      ))}
    </div>
  );
};
