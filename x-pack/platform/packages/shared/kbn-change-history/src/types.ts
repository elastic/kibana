import type { TransportRequestOptionsWithOutMeta } from '@elastic/elasticsearch';
import type {
  QueryDslQueryContainer,
  Refresh,
  SortCombinations,
} from '@elastic/elasticsearch/lib/api/types';

/**
 * Represents a single document in the change history.
 * Using ECS field notation as much as possible.
 * @see https://www.elastic.co/docs/reference/ecs/ecs-field-reference
 */
export interface ChangeHistoryDocument {
  '@timestamp': string;

  user: {
    id?: string; // Unique profile identifier used by auth realm (@see https://www.elastic.co/docs/deploy-manage/users-roles/cluster-or-deployment-auth/user-profiles)
    name: string; // Current login name for user that generated the change.
  };

  event: {
    id: string; // Unique identifier for the event. Autogenerated if not passed in.
    module: string; // Kibana module that the event belongs to, part of the data stream name. (e.g. `security`, etc.)
    dataset: string; // Name of the dataset that the event belongs to (e.g. `alerting-rules`, etc.)
    action: string; // Human readable action performed by the user (`rule_create`, `rule_update`, `rule_delete`, etc.). Some Audit log examples (@see https://www.elastic.co/docs/reference/kibana/kibana-audit-events#xpack-security-ecs-audit-logging)
    type: 'change' | 'creation' | 'deletion'; // ECS Categorization of the event performed (`creation`, `change`, `deletion`)
    outcome: 'success'; // ECS Outcome of the event (`success`)
    reason?: string; // Reason for the change.
    created?: string; // ISO8601 timestamp of the event creation time.
    group?: { id: string }; // ID shared between events that take place as a group.
  };

  object: {
    id: string; // Unique id of the target object in kibana.
    type: string; // Type of the target object in kibana. Allows tracking multiple object types in same stream.
    index?: string; // ES backing index where this object was stored.
    hash: string; // SHA256 hash of the object.snapshot to identify changes in the payload.
    sequence?: string; // Version identifier used for ordering. Increases with each version.
    fields: {
      changed?: string[]; // List of field names that changed.
      masked?: string[]; // List of "sensitive data" fields masked in snapshot
    };
    oldvalues?: Record<string, unknown>; // Previous values for changed fields.
    snapshot: Record<string, unknown>; // Full snapshot after the change.
  };

  // Optional list of tags for the event.
  tags?: string[];

  // Optional metadata about the event.
  // Information that does not form part of the diff or ECS schema.
  metadata?: Record<string, unknown>;

  kibana: {
    space_id: string; // ID of the space that the event belongs to.
    version: string; // Version of Kibana that the event belongs to.
  };
}

export interface ObjectChange {
  id?: string; // The change `event.id`.
  timestamp?: string; // ISO `@timestamp` when the change took place.
  objectType: string; // The `object.type`. Allows multiple object types per data stream.
  objectId: string; // The `object.id`. Uniquely identifies this object in Kibana
  index?: string; // ES backing `_index` where this object was stored.
  sequence?: string; // Any sequential identifier for ordering changes.
  before?: Record<string, any>; // <-- Raw version of the object. Before changes. If available.
  after: Record<string, any>; // <-- Raw copy of the object after changes (ie the `object.snapshot`).
}

export interface LogChangeHistoryOptions {
  action: string; // Human readable action performed by the user (`rule_create`, `rule_update`, `rule_delete`, etc.). Some Audit log examples (@see https://www.elastic.co/docs/reference/kibana/kibana-audit-events#xpack-security-ecs-audit-logging)
  username: string; // Current login name for user that generated the change.
  userProfileId?: string; // Unique profile identifier used by auth realm (@see https://www.elastic.co/docs/deploy-manage/users-roles/cluster-or-deployment-auth/user-profiles)
  spaceId: string; // ID of the space that the event belongs to.
  correlationId?: string; // ID shared between events that take place as a group.
  data?: Partial<Pick<ChangeHistoryDocument, 'event' | 'tags' | 'metadata'>>;
  ignoreFields?: ChangeTrackingIgnoreFields;
  maskFields?: ChangeTrackingDataMaskingFields;
  // Optional diff to be used instead of standard diff calculation
  diffDocCalculation?: (opts: ChangeTrackingDiffOptions) => ChangeTrackingDiff;
  refresh?: Refresh; // Optional indicator to force an ES refresh after changes (affects perfomance)
}

export interface GetChangeHistoryOptions {
  additionalFilters?: QueryDslQueryContainer[];
  sort?: SortCombinations[];
  from?: number;
  size?: number;
  transportOpts?: TransportRequestOptionsWithOutMeta;
}

/**
 * Values to override in the change history log entry
 */
export type LogChangeOverrides = Partial<ChangeHistoryDocument>;

/**
 * Result from a history query.
 */
export interface GetHistoryResult {
  startDate?: Date;
  total: number;
  items: ChangeHistoryDocument[];
}

/**
 * Fields excluded from diff calculation
 */
export interface ChangeTrackingIgnoreFields {
  [Key: string]: boolean | ChangeTrackingIgnoreFields;
}

/**
 * Fields hashed due to sensitive nature (PII, Secret keys, etc)
 */
export interface ChangeTrackingDataMaskingFields {
  [Key: string]: boolean | ChangeTrackingDataMaskingFields;
}

/**
 * Input for the diff calculation
 */
export interface ChangeTrackingDiffOptions {
  a?: Record<string, any>;
  b?: Record<string, any>;
  ignoreFields?: ChangeTrackingIgnoreFields;
}

/**
 * Output of the diff calculation
 */
export interface ChangeTrackingDiff {
  stats: {
    total: number;
    additions: number;
    deletions: number;
    updates: number;
  };
  fieldChanges: Array<string>;
  ignored: Array<string>;
  oldvalues: Record<string, any>;
  newvalues: Record<string, any>;
}
