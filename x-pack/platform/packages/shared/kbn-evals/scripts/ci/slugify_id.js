// Slugify an id into a safe token (lowercase; non-[a-z0-9_-] -> '-'; collapse runs of '-'; trim).
function slugifyId(value) {
  return String(value)
    .toLowerCase()
    .replace(/[^a-z0-9_-]+/g, '-')
    .replace(/-+/g, '-')
    .replace(/^-|-$/g, '');
}

module.exports = { slugifyId };
