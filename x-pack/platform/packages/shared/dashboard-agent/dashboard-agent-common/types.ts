import { z } from '@kbn/zod/v4';
import type { Attachment } from '@kbn/agent-builder-common/attachments';
import { DASHBOARD_ATTACHMENT_TYPE } from './constants';

/**
 * Dashboard grid column count (matches DASHBOARD_GRID_COLUMN_COUNT from dashboard plugin).
 */
const DASHBOARD_GRID_COLUMN_COUNT = 48;

/**
 * Constraints for panel grid positioning and sizing.
 * Mirrors PANEL_GRID_CONSTRAINTS from @kbn/dashboard-plugin/common.
 */
const PANEL_GRID_CONSTRAINTS = {
  width: { min: 1, max: DASHBOARD_GRID_COLUMN_COUNT },
  height: { min: 1 },
  x: { min: 0, max: DASHBOARD_GRID_COLUMN_COUNT - 1 },
  y: { min: 0 },
} as const;

/**
 * Grid dimensions (in dashboard grid units) for layout.
 */
export const panelGridSchema = z.object({
  w: z
    .int()
    .min(PANEL_GRID_CONSTRAINTS.width.min)
    .max(PANEL_GRID_CONSTRAINTS.width.max),
  h: z.number().int().min(PANEL_GRID_CONSTRAINTS.height.min),
  x: z
    .int()
    .min(PANEL_GRID_CONSTRAINTS.x.min)
    .max(PANEL_GRID_CONSTRAINTS.x.max),
  y: z.int().min(PANEL_GRID_CONSTRAINTS.y.min),
});

/**
 * Zod schema for generic (non-Lens) panel entries.
 * The `type` field contains the actual embeddable type.
 */
export const attachmentPanelSchema = z.object({
  /** The actual embeddable type (e.g., 'DASHBOARD_MARKDOWN', 'aiOpsLogRateAnalysis', 'lens') */
  type: z.string(),
  uid: z.string(),
  /** The panel configuration in API format */
  config: z.record(z.string(), z.unknown()),
  /** Layout: width/height and position in dashboard grid units. */
  grid: panelGridSchema,
  /** The visualization attachment ID this panel was resolved from */
  sourceAttachmentId: z.string().optional(),
});

/**
 * A dashboard panel entry.
 */
export type AttachmentPanel = z.infer<typeof attachmentPanelSchema>;

export const sectionGridSchema = z.object({
  y: z.number().int().min(PANEL_GRID_CONSTRAINTS.y.min),
});

/**
 * Zod schema for a dashboard section containing panels.
 */
export const dashboardSectionSchema = z.object({
  /** Stable section identifier generated by the server */
  sectionId: z.string(),
  /** Section title */
  title: z.string(),
  /** Whether the section is collapsed in the UI */
  collapsed: z.boolean(),
  /** Outer section position in dashboard grid coordinates */
  grid: sectionGridSchema,
  /** Panels within this section */
  panels: z.array(attachmentPanelSchema),
});

/**
 * A dashboard section containing panels.
 */
export type DashboardSection = z.infer<typeof dashboardSectionSchema>;

/**
 * Zod schema for dashboard attachment data.
 */
export const dashboardAttachmentDataSchema = z.object({
  title: z.string(),
  description: z.string(),
  /** Optional saved object ID if the dashboard was saved */
  savedObjectId: z.string().optional(),
  /** Array of top-level panel entries */
  panels: z.array(attachmentPanelSchema),
  /** Optional array of sections containing grouped panels */
  sections: z.array(dashboardSectionSchema).optional(),
});

/**
 * Data for a dashboard attachment.
 */
export type DashboardAttachmentData = z.infer<typeof dashboardAttachmentDataSchema>;

/**
 * Origin payload for dashboard attachments is a saved object ID string.
 */
export type DashboardAttachmentOrigin = string;

/**
 * Data payload for a panel added event.
 */
export interface PanelAddedEventData {
  dashboardAttachmentId: string;
  panel: AttachmentPanel;
}

/**
 * Data payload for a panel removed event.
 */
export interface PanelsRemovedEventData {
  dashboardAttachmentId: string;
  panelIds: string[];
}


export type DashboardAttachment = Attachment<
  typeof DASHBOARD_ATTACHMENT_TYPE,
  DashboardAttachmentData
>;
