import Path from 'path';
import Fs from 'fs';
import type { Command } from '@kbn/dev-cli-runner';
import { createFlagError } from '@kbn/dev-cli-errors';
import { DOMAIN_PLUGIN_PATHS } from '../../constants';
import type { SkillDomain } from '../../constants';
import { validateSkillName, validateDomain, toSnakeCase, resolveRepoRoot, ensureDir } from '../../utils';

function extractSkillDefinition(filePath: string): {
  id: string;
  name: string;
  description: string;
  content: string;
  basePath: string;
  tools: string[];
} {
  const source = Fs.readFileSync(filePath, 'utf-8');

  const idMatch = source.match(/id:\s*['"]([^'"]+)['"]/);
  const nameMatch = source.match(/name:\s*['"]([^'"]+)['"]/);
  const basePathMatch = source.match(/basePath:\s*['"]([^'"]+)['"]/);
  const descMatch = source.match(/description:\s*\n?\s*['"`]([^'"`]+)['"`]/s);
  const contentMatch = source.match(/content:\s*`([^`]*)`/s);
  const toolMatches = source.match(/['"]([a-z]+\.[a-z._]+)['"]/g);

  return {
    id: idMatch?.[1] ?? 'unknown',
    name: nameMatch?.[1] ?? 'unknown',
    description: descMatch?.[1]?.trim() ?? '',
    content: contentMatch?.[1]?.trim() ?? '',
    basePath: basePathMatch?.[1] ?? '',
    tools: toolMatches?.map((t) => t.replace(/['"]/g, '')).filter((t) => t.includes('.')) ?? [],
  };
}

function renderSkillMd(def: { name: string; description: string; content: string; tools: string[] }): string {
  const toolsList = def.tools.length
    ? def.tools.map((t) => `- \`${t}\``).join('\n')
    : '- None specified';

  return `---
name: ${def.name}
description: ${def.description}
---

${def.content}

## Tools

${toolsList}
`;
}

function renderExtensionsYaml(def: {
  id: string;
  name: string;
  description: string;
  basePath: string;
  tools: string[];
}): string {
  const toolsYaml = def.tools.length
    ? def.tools.map((t) => `  - ${t}`).join('\n')
    : '  []';

  return `# Auto-generated by agent-builder-skill CLI
# Edit with caution — import will sync changes back to Kibana source

id: ${def.id}
name: ${def.name}
basePath: ${def.basePath}
description: >
  ${def.description}
registryTools:
${toolsYaml}
`;
}

export const exportCmd: Command<void> = {
  name: 'export',
  description: `
  Export a Kibana Agent Builder skill into the universal format (SKILL.md + skill.extensions.yaml).
  The exported format can be used for distribution, review, or import into other systems.

  Examples:
    node scripts/agent_builder_skill export --name alert-triage --domain security
    node scripts/agent_builder_skill export --name alert-triage --domain security --output-dir ./exported-skills
  `,
  flags: {
    string: ['name', 'domain', 'output-dir'],
    help: `
      --name         Skill name [required]
      --domain       Skill domain [required]
      --output-dir   Output directory (default: ./target/exported-skills/<name>)
    `,
  },
  run: async ({ log, flagsReader }) => {
    const name = flagsReader.string('name');
    const domain = flagsReader.string('domain');

    if (!name) {
      throw createFlagError('--name is required');
    }
    if (!domain) {
      throw createFlagError('--domain is required');
    }

    validateSkillName(name);
    validateDomain(domain);

    const repoRoot = resolveRepoRoot();
    const pluginPath = DOMAIN_PLUGIN_PATHS[domain as SkillDomain];
    const snakeName = toSnakeCase(name);
    const skillFile = Path.join(repoRoot, pluginPath, 'skills', `${snakeName}_skill.ts`);

    if (!Fs.existsSync(skillFile)) {
      throw new Error(`Skill file not found: ${skillFile}`);
    }

    const outputDir =
      flagsReader.string('output-dir') ||
      Path.join(repoRoot, 'target', 'exported-skills', name);

    ensureDir(outputDir);

    log.info(`Exporting skill "${name}" from ${Path.relative(repoRoot, skillFile)}...`);

    const def = extractSkillDefinition(skillFile);

    const skillMdPath = Path.join(outputDir, 'SKILL.md');
    Fs.writeFileSync(skillMdPath, renderSkillMd(def), 'utf-8');
    log.success(`  Written: ${Path.relative(repoRoot, skillMdPath)}`);

    const extensionsPath = Path.join(outputDir, 'skill.extensions.yaml');
    Fs.writeFileSync(extensionsPath, renderExtensionsYaml(def), 'utf-8');
    log.success(`  Written: ${Path.relative(repoRoot, extensionsPath)}`);

    log.info('');
    log.info(`Exported to: ${Path.relative(repoRoot, outputDir)}`);
    log.info('');
    log.info('The exported skill contains:');
    log.info('  SKILL.md               — Human-readable skill content');
    log.info('  skill.extensions.yaml  — Machine-readable metadata for import');
    log.info('');
    log.info('To import changes back:');
    log.info(`  node scripts/agent_builder_skill import --name ${name} --domain ${domain} --input-dir ${Path.relative(repoRoot, outputDir)}`);
  },
};
