import type { EuiTabbedContentProps } from '@elastic/eui';
import { useMemo } from 'react';
import { useKibana } from '@kbn/kibana-react-plugin/public';
import React from 'react';
import { EuiSpacer } from '@elastic/eui';
import { i18n } from '@kbn/i18n';
import type { DataSchemaFormat } from '@kbn/metrics-data-access-plugin/common';
import type { ApmPluginStartDeps } from '../../../../plugin';
import { HOST_NAME, CONTAINER_ID } from '../../../../../common/es_fields/apm';
import { buildKqlFilter } from './build_kql_filter';

type Tab = NonNullable<EuiTabbedContentProps['tabs']>[0] & {
  id: 'containers' | 'pods' | 'hosts';
  hidden?: boolean;
};

export enum InfraTab {
  containers = 'containers',
  pods = 'pods',
  hosts = 'hosts',
}

export function useTabs({
  containerIds,
  podNames,
  hostNames,
  schema,
  start,
  end,
}: {
  containerIds: string[];
  podNames: string[];
  hostNames: string[];
  schema: DataSchemaFormat;
  start: string;
  end: string;
}) {
  const { services } = useKibana<ApmPluginStartDeps>();
  const { metricsDataAccess } = services;
  const HostMetricsTable = metricsDataAccess?.HostMetricsTable;
  const ContainerMetricsTable = metricsDataAccess?.ContainerMetricsTable;
  const PodMetricsTable = metricsDataAccess?.PodMetricsTable;

  const timerange = useMemo(
    () => ({
      from: start,
      to: end,
    }),
    [start, end]
  );

  const k8sFilterFields = useMemo(() => {
    if (schema === 'semconv') {
      return 'k8s.pod.name';
    }
    return 'kubernetes.pod.name';
  }, [schema]);

  const hostsFilter = useMemo(() => buildKqlFilter(HOST_NAME, hostNames), [hostNames]);
  const podsFilter = useMemo(
    () => buildKqlFilter(k8sFilterFields, podNames),
    [k8sFilterFields, podNames]
  );
  const containersFilter = useMemo(
    () => buildKqlFilter(CONTAINER_ID, containerIds),
    [containerIds]
  );

  const containerMetricsTable = (
    <>
      <EuiSpacer />
      {ContainerMetricsTable &&
        ContainerMetricsTable({
          timerange,
          kuery: containersFilter,
          schema,
          semconvRuntime: podNames.length > 0 ? 'k8s' : 'docker',
        })}
    </>
  );

  const podMetricsTable = (
    <>
      <EuiSpacer />
      {PodMetricsTable &&
        PodMetricsTable({
          timerange,
          kuery: podsFilter,
          schema,
        })}
    </>
  );

  const hostMetricsTable = (
    <>
      <EuiSpacer />
      {HostMetricsTable &&
        HostMetricsTable({
          timerange,
          kuery: hostsFilter,
          schema,
        })}
    </>
  );

  const tabs: Tab[] = [
    {
      id: InfraTab.containers,
      name: i18n.translate('xpack.apm.views.infra.tabs.containers', {
        defaultMessage: 'Containers',
      }),
      content: containerMetricsTable,
      hidden: containerIds && containerIds.length <= 0,
    },
    {
      id: InfraTab.pods,
      name: i18n.translate('xpack.apm.views.infra.tabs.pods', {
        defaultMessage: 'Pods',
      }),
      content: podMetricsTable,
      hidden: podNames && podNames.length <= 0,
    },
    {
      id: InfraTab.hosts,
      name: i18n.translate('xpack.apm.views.infra.tabs.hosts', {
        defaultMessage: 'Hosts',
      }),
      content: hostMetricsTable,
    },
  ];

  return tabs
    .filter((t) => !t.hidden)
    .map(({ id, name, content }) => ({
      id,
      name,
      content,
    }));
}
