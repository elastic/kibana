import type { Evaluator } from '@kbn/evals';

interface ModelUsageOutput {
  modelUsage?: {
    input_tokens?: number;
    output_tokens?: number;
    llm_calls?: number;
  };
}

/**
 * CODE evaluator that reports the number of input tokens consumed by the
 * agent during a single task. Reads from `output.modelUsage.input_tokens`
 * populated by the Agent Builder API response.
 *
 * Unlike the trace-based `inputTokens` evaluator in `@kbn/evals`, this
 * does not require OTel trace indices (`traces-*`) or a separate
 * `traceEsClient`. It works in local Scout stacks where EDOT collection
 * is not wired up.
 */
export function createInputTokensEvaluator(): Evaluator {
  return {
    name: 'Input Tokens',
    kind: 'CODE',
    evaluate: async ({ output }) => {
      const tokens = (output as ModelUsageOutput)?.modelUsage?.input_tokens;
      if (typeof tokens !== 'number') {
        return {
          score: 0,
          label: 'missing',
          explanation: 'No input token usage returned by the agent API.',
        };
      }
      return {
        score: tokens,
        label: 'reported',
        explanation: `Agent consumed ${tokens} input tokens.`,
      };
    },
  };
}

