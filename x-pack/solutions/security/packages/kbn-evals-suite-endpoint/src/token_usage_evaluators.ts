/**
 * CODE evaluator that counts tool calls by inspecting `output.steps`.
 *
 * Replaces the trace-based `toolCalls` evaluator which requires OTel
 * traces in a `traces-*` index. In local Scout stacks that index does not
 * exist, so the trace-based evaluator silently returns "—". This version
 * counts the number of steps that have a non-empty `tool` property,
 * which is the same semantic (each step maps to one tool invocation).
 */
export function createToolCallsCodeEvaluator(): Evaluator {
  return {
    name: 'Tool Calls',
    kind: 'CODE',
    evaluate: async ({ output }) => {
      const steps = (output as { steps?: Array<{ tool?: string }> })?.steps ?? [];
      const toolCalls = steps.filter((s) => typeof s.tool === 'string' && s.tool.length > 0).length;
      return {
        score: toolCalls,
        label: 'reported',
        explanation: `Agent invoked ${toolCalls} tool(s).`,
      };
    },
  };
}
