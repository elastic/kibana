import type {
  DefaultEvaluators,
  EvalsExecutorClient,
  EvaluationDataset,
  Evaluator,
  Example,
} from '@kbn/evals';
import { createPrompt, type BoundInferenceClient } from '@kbn/inference-common';
import { executeUntilValid } from '@kbn/inference-prompt-utils';
import type { ToolingLog } from '@kbn/tooling-log';
import pRetry from 'p-retry';
import { z } from '@kbn/zod';
import type { SecurityEvalChatClient } from './chat_client';

/** Task output from SecurityEvalChatClient.converse */
export interface ConverseTaskOutput {
  messages: Array<{ message: string }>;
  steps?: unknown[];
  errors: unknown[];
  traceId?: string;
}

export interface EntityAnalyticsExample extends Example {
  input: {
    question: string;
  };
  output: {
    criteria: string[];
    expectedEsql?: string;
  };
}

export type EvaluateEntityAnalyticsDataset = (options: {
  dataset: {
    name: string;
    description: string;
    examples: EntityAnalyticsExample[];
  };
}) => Promise<void>;

const ESQL_EQUIVALENCE_EVALUATOR_NAME = 'ES|QL Functional Equivalence';

const esqlEquivalencePrompt = createPrompt({
  name: 'esql_equivalence_evaluation',
  description: 'Evaluate ES|QL query equivalence between gold and generated queries',
  input: z.object({
    ground_truth: z.string(),
    prediction: z.string(),
  }),
})
  .version({
    system: {
      mustache: {
        template:
          'You are a helpful agent that can assess how equivalent a candidate ES|QL query is to a gold ES|QL query.\n\nYou are given a gold ES|QL query and a candidate ES|QL query generated by the LLM model.\n\nYour task is to check if the candidate ES|QL query is functionally equivalent to the gold ES|QL query.\n\nPlease rate how equivalent the candidate ES|QL query is to the gold ES|QL query based on the following scale:\n- No: The candidate ES|QL query is not equivalent to the gold ES|QL query.\n- Yes: The candidate ES|QL query is equivalent to the gold ES|QL query.',
      },
    },
    template: {
      mustache: {
        template:
          'Here is the actual task:\n\nGold ES|QL query:\n```esql\n{{{ground_truth}}}\n```\n\nCandidate ES|QL query:\n```esql\n{{{prediction}}}\n```\n',
      },
    },
    toolChoice: {
      function: 'evaluate',
    },
    tools: {
      evaluate: {
        description:
          'Assess the functional equivalence of the generated ES|QL query to the gold query.',
        schema: {
          type: 'object',
          properties: {
            equivalent: {
              type: 'string',
              enum: ['Yes', 'No'],
              description:
                'Whether the generated ES|QL query is functionally equivalent to the gold query.',
            },
            reason: {
              type: 'string',
              description: 'Briefly explain the reasoning behind your judgement.',
            },
          },
          required: ['equivalent', 'reason'],
        },
      },
    },
  } as const)
  .get();

/** Minimal shape for a step that may be a tool call with results (converse API). */
interface StepLike {
  type?: string;
  results?: Array<{ type?: string; data?: { esql?: string; query?: string } }>;
}

function extractEsqlFromSteps(steps: unknown[]): string {
  for (let i = steps.length - 1; i >= 0; i--) {
    const step = steps[i] as StepLike | undefined;
    if (step?.type === 'tool_call' && Array.isArray(step.results)) {
      for (let j = step.results.length - 1; j >= 0; j--) {
        const r = step.results[j];
        if (r?.data) {
          // QueryResult: type 'query', data.esql
          const fromQuery =
            r.type === 'query' && typeof r.data.esql === 'string' && r.data.esql.trim();
          if (fromQuery && fromQuery.toUpperCase().startsWith('FROM ')) return fromQuery;
          // EsqlResultsResult: type 'esql_results', data.query
          const fromEsqlResults =
            r.type === 'esql_results' && typeof r.data.query === 'string' && r.data.query.trim();
          if (fromEsqlResults && fromEsqlResults.toUpperCase().startsWith('FROM ')) {
            return fromEsqlResults;
          }
        }
      }
    }
  }
  return '';
}

function extractEsqlFromMessageText(lastMessage: string): string {
  // Prefer ```esql ... ``` block
  const esqlBlock = lastMessage.match(/```(?:esql)?\s*([\s\S]*?)```/i);
  if (esqlBlock) {
    const candidate = esqlBlock[1].trim();
    if (candidate.toUpperCase().startsWith('FROM ')) return candidate;
  }
  // Fallback: first line starting with FROM until end or next blank line / code block
  const fromMatch = lastMessage.match(/(?:^|\n)(FROM\s[\s\S]*?)(?=\n\n|```|$)/i);
  if (fromMatch) return fromMatch[1].trim();
  return '';
}

/**
 * Extracts ES|QL query from converse response: first from steps (tool results), then from last message.
 * Steps may contain tool_call entries with results of type 'query' (data.esql) or 'esql_results' (data.query).
 */
export function extractEsqlFromConverseOutput(output: ConverseTaskOutput): string {
  if (Array.isArray(output.steps) && output.steps.length > 0) {
    const fromSteps = extractEsqlFromSteps(output.steps);
    if (fromSteps) return fromSteps;
  }

  const lastMessage = output.messages?.length
    ? output.messages[output.messages.length - 1]?.message
    : '';
  if (typeof lastMessage !== 'string') return '';

  return extractEsqlFromMessageText(lastMessage);
}

export function createEntityAnalyticsCriteriaEvaluator({
  evaluators,
}: {
  evaluators: DefaultEvaluators;
}): Evaluator {
  return {
    name: 'Criteria',
    kind: 'LLM' as const,
    evaluate: async ({ expected, ...rest }) => {
      const criteria: string[] = (expected as EntityAnalyticsExample['output'])?.criteria ?? [];
      return evaluators.criteria(criteria).evaluate({ expected, ...rest });
    },
  };
}

/**
 * ES|QL equivalence evaluator that skips (score 1) when expected.expectedEsql is missing.
 */
function createOptionalEsqlEquivalenceEvaluator({
  inferenceClient,
  log,
}: {
  inferenceClient: BoundInferenceClient;
  log: ToolingLog;
}): Evaluator {
  return {
    name: ESQL_EQUIVALENCE_EVALUATOR_NAME,
    kind: 'LLM' as const,
    evaluate: async (params) => {
      const prediction = extractEsqlFromConverseOutput(params.output as ConverseTaskOutput);
      const groundTruth = (params.expected as EntityAnalyticsExample['output'])?.expectedEsql;

      if (!groundTruth?.trim()) {
        return {
          score: 1,
          label: 'Skipped (no ground truth ES|QL)',
          explanation: 'Example has no expectedEsql; ES|QL equivalence not evaluated.',
          metadata: { skipped: true },
        };
      }
      const normalizedGroundTruth = groundTruth.trim();

      if (!prediction?.trim()) {
        return {
          explanation: 'Missing prediction or ground truth query',
          label: 'No',
          score: 0,
          metadata: {
            equivalent: false,
            reason: 'Missing prediction or ground truth query for comparison',
          },
        };
      }

      async function runEsqlEquivalenceAnalysis(): Promise<{ equivalent: string; reason: string }> {
        const response = await executeUntilValid({
          prompt: esqlEquivalencePrompt,
          inferenceClient,
          input: {
            ground_truth: normalizedGroundTruth,
            prediction,
          },
          finalToolChoice: {
            function: 'evaluate',
          },
          maxRetries: 3,
          toolCallbacks: {
            evaluate: async (toolCall) => {
              const { equivalent, reason } = toolCall.function.arguments;
              return {
                response: {
                  equivalent,
                  reason,
                },
              };
            },
          },
        });

        return response.toolCalls[0].function.arguments;
      }

      const { equivalent, reason } = await pRetry(runEsqlEquivalenceAnalysis, {
        retries: 3,
        onFailedAttempt: (error) => {
          const isLastAttempt = error.retriesLeft === 0;

          if (isLastAttempt) {
            log.error(
              new Error(
                `Failed to retrieve ES|QL equivalence analysis after ${error.attemptNumber} attempts (no valid tool call or malformed response)`,
                {
                  cause: error,
                }
              )
            );
          } else {
            log.warning(
              new Error(
                `ES|QL equivalence analysis returned an invalid or missing tool call on attempt ${error.attemptNumber}; retrying...`,
                {
                  cause: error,
                }
              )
            );
          }
        },
      });

      const isEquivalent = equivalent?.trim().toLowerCase() === 'yes';
      return {
        explanation: reason,
        label: isEquivalent ? 'Equivalent ES|QL query' : 'Non-equivalent ES|QL query',
        score: Number(isEquivalent),
        metadata: {
          equivalent,
          reason,
        },
      };
    },
  };
}

export function createEvaluateEntityAnalyticsDataset({
  evaluators,
  executorClient,
  chatClient,
  inferenceClient,
  log,
}: {
  evaluators: DefaultEvaluators;
  executorClient: EvalsExecutorClient;
  chatClient: SecurityEvalChatClient;
  inferenceClient: BoundInferenceClient;
  log: ToolingLog;
}): EvaluateEntityAnalyticsDataset {
  return async function evaluateEntityAnalyticsDataset({
    dataset: { name, description, examples },
  }: {
    dataset: {
      name: string;
      description: string;
      examples: EntityAnalyticsExample[];
    };
  }) {
    const dataset = {
      name,
      description,
      examples,
    } satisfies EvaluationDataset<EntityAnalyticsExample>;

    const evaluatorsList: Evaluator[] = [
      createEntityAnalyticsCriteriaEvaluator({ evaluators }),
      createOptionalEsqlEquivalenceEvaluator({ inferenceClient, log }),
    ];

    await executorClient.runExperiment(
      {
        dataset,
        task: async ({ input }) => {
          const response = await chatClient.converse({ message: input.question });
          return {
            messages: response.messages,
            steps: response.steps,
            errors: response.errors,
            traceId: response.traceId,
          };
        },
      },
      evaluatorsList
    );
  };
}
