/**
 * Check if a case title is generic/auto-generated (i.e., should be replaced by AD title)
 */
const isGenericCaseTitle = (title: string): boolean => {
  const genericPatterns = [
    /^new case$/i,
    /^case$/i,
    /^untitled$/i,
    /^alert grouping case/i,
    /^grouped alerts/i,
    /^security case/i,
    /^\[auto\]/i,
    /^case-\d+$/i,
    /^alert group:/i,
    // Matches titles auto-generated by alert grouping pipeline:
    // "Credential Theft on host-1 (26 alerts)", "Reconnaissance & Discovery on host-2 (3 alerts)", etc.
    /^.+ on .+ \(\d+ alerts?\)$/i,
    // Matches "Attack on host: Tactic1 → Tactic2 (N alerts)"
    /^attack on .+:.+\(\d+ alerts?\)$/i,
  ];
  return genericPatterns.some((pattern) => pattern.test(title.trim()));
};

/**
 * Register case observable extraction route
 */
export const registerCaseObservableExtractionRoute = (
  router: IRouter<ElasticAssistantRequestHandlerContext>
): void => {
  router.versioned
    .post({
      access: 'internal',
      path: `${ALERT_GROUPING_CASE_BASE}/{case_id}/_extract_observables`,
    })
    .addVersion(
      {
        version: API_VERSIONS.internal.v1,
        validate: {
          request: {
            params: buildRouteValidationWithZod(ExtractObservablesRequestParams),
            body: buildRouteValidationWithZod(ExtractObservablesRequestBody),
          },
        },
      },
      async (context, request, response): Promise<IKibanaResponse> => {
        const resp = buildResponse(response);
        const assistantContext = await context.elasticAssistant;
        const logger: Logger = assistantContext.logger;

        try {
          const esClient = (await context.core).elasticsearch.client.asCurrentUser;
          const { case_id: caseId } = request.params;
          const {
            alertIds,
            alertsIndexPattern,
            existingObservables = [],
            entityTypes,
            maxObservablesPerType,
          } = request.body;

          logger.debug(`Extracting observables for case ${caseId} from ${alertIds.length} alerts`);

          // Fetch alerts
          const alertsResponse = await esClient.search({
            index: alertsIndexPattern,
            size: alertIds.length,
            query: {
              terms: {
                _id: alertIds,
              },
            },
          });

          const alerts = alertsResponse.hits.hits.map((hit) => ({
            _id: hit._id!,
            _source: hit._source as Record<string, unknown>,
          }));

          // Create extractor with custom config
          const extractor = new ObservableAutoExtractor({
            logger,
            config: {
              enabled: true,
              entityTypes: entityTypes as string[] | undefined,
              maxObservablesPerType,
              deduplicateExisting: true,
            },
          });

          // Convert existing observables to expected format
          const existingObservablesFormatted = existingObservables.map((obs) => ({
            typeKey: obs.typeKey as string,
            value: obs.value,
            createdAt: new Date().toISOString(),
            source: { automatic: false },
          }));

          // Extract observables
          const result = extractor.extractObservablesFromAlerts(
            alerts,
            existingObservablesFormatted as Parameters<
              typeof extractor.extractObservablesFromAlerts
            >[1]
          );

          return response.ok({
            body: {
              case_id: caseId,
              observables: result.observables,
              total_extracted: result.totalExtracted,
              duplicates_skipped: result.duplicatesSkipped,
              entities_by_type: result.entitiesByType,
            },
          });
        } catch (err) {
          logger.error(err);
          const error = transformError(err);
          return resp.error({
            body: error.message,
            statusCode: error.statusCode,
          });
        }
      }
    );
};

/**
 * Register case trigger management routes
 */
export const registerCaseTriggerRoutes = (
  router: IRouter<ElasticAssistantRequestHandlerContext>
): void => {
  // Create trigger for a case
  router.versioned
    .post({
      access: 'internal',
      path: `${ALERT_GROUPING_CASE_BASE}/{case_id}/triggers`,
    })
    .addVersion(
      {
        version: API_VERSIONS.internal.v1,
        validate: {
          request: {
            params: buildRouteValidationWithZod(TriggerRequestParams),
            body: buildRouteValidationWithZod(CreateTriggerRequestBody),
          },
        },
      },
      async (context, request, response): Promise<IKibanaResponse> => {
        const resp = buildResponse(response);
        const assistantContext = await context.elasticAssistant;
        const logger: Logger = assistantContext.logger;

        try {
          const soClient = assistantContext.savedObjectsClient;
          const spaceId = (await context.elasticAssistant).getSpaceId();
          const authenticatedUser = await assistantContext.getCurrentUser();

          if (!authenticatedUser) {
            return resp.error({
              body: 'Authenticated user not found',
              statusCode: 401,
            });
          }

          const { case_id: caseId } = request.params;

          const dataClient = new WorkflowDataClient({
            soClient,
            spaceId,
            currentUser: authenticatedUser.username ?? authenticatedUser.profile_uid ?? 'unknown',
          });

          const trigger = await dataClient.createTrigger({
            caseId,
            triggerType: request.body.triggerType,
            action: request.body.action,
            enabled: request.body.enabled,
            debounceSeconds: request.body.debounceSeconds,
            config: request.body.config ?? {},
          });

          return response.ok({
            body: trigger,
          });
        } catch (err) {
          logger.error(err);
          const error = transformError(err);
          return resp.error({
            body: error.message,
            statusCode: error.statusCode,
          });
        }
      }
    );

  // List triggers for a case
  router.versioned
    .get({
      access: 'internal',
      path: `${ALERT_GROUPING_CASE_BASE}/{case_id}/triggers`,
    })
    .addVersion(
      {
        version: API_VERSIONS.internal.v1,
        validate: {
          request: {
            params: buildRouteValidationWithZod(TriggerRequestParams),
          },
        },
      },
      async (context, request, response): Promise<IKibanaResponse> => {
        const resp = buildResponse(response);
        const assistantContext = await context.elasticAssistant;
        const logger: Logger = assistantContext.logger;

        try {
          const soClient = assistantContext.savedObjectsClient;
          const spaceId = (await context.elasticAssistant).getSpaceId();
          const authenticatedUser = await assistantContext.getCurrentUser();

          if (!authenticatedUser) {
            return resp.error({
              body: 'Authenticated user not found',
              statusCode: 401,
            });
          }

          const { case_id: caseId } = request.params;

          const dataClient = new WorkflowDataClient({
            soClient,
            spaceId,
            currentUser: authenticatedUser.username ?? authenticatedUser.profile_uid ?? 'unknown',
          });

          const { results } = await dataClient.findTriggers({
            page: 1,
            perPage: 100,
          });

          // Filter triggers for this case
          const caseTriggers = results.filter((t) => t.caseId === caseId);

          return response.ok({
            body: {
              case_id: caseId,
              triggers: caseTriggers,
              total: caseTriggers.length,
            },
          });
        } catch (err) {
          logger.error(err);
          const error = transformError(err);
          return resp.error({
            body: error.message,
            statusCode: error.statusCode,
          });
        }
      }
    );

  // Delete a trigger
  router.versioned
    .delete({
      access: 'internal',
      path: `${ALERT_GROUPING_CASE_BASE}/{case_id}/triggers/{trigger_id}`,
    })
    .addVersion(
      {
        version: API_VERSIONS.internal.v1,
        validate: {
          request: {
            params: buildRouteValidationWithZod(DeleteTriggerParams),
          },
        },
      },
      async (context, request, response): Promise<IKibanaResponse> => {
        const resp = buildResponse(response);
        const assistantContext = await context.elasticAssistant;
        const logger: Logger = assistantContext.logger;

        try {
          const soClient = assistantContext.savedObjectsClient;
          const spaceId = (await context.elasticAssistant).getSpaceId();
          const authenticatedUser = await assistantContext.getCurrentUser();

          if (!authenticatedUser) {
            return resp.error({
              body: 'Authenticated user not found',
              statusCode: 401,
            });
          }

          const { trigger_id: triggerId } = request.params;

          const dataClient = new WorkflowDataClient({
            soClient,
            spaceId,
            currentUser: authenticatedUser.username ?? authenticatedUser.profile_uid ?? 'unknown',
          });

          await dataClient.deleteTrigger(triggerId);

          return response.ok({
            body: { success: true },
          });
        } catch (err) {
          logger.error(err);
          const error = transformError(err);
          return resp.error({
            body: error.message,
            statusCode: error.statusCode,
          });
        }
      }
    );
};

/**
 * Register case event notification route
 * This route is called by Cases plugin to notify about case events
 */
export const registerCaseEventNotificationRoute = (
  router: IRouter<ElasticAssistantRequestHandlerContext>
): void => {
  router.versioned
    .post({
      access: 'internal',
      path: `${ALERT_GROUPING_CASE_BASE}/{case_id}/_notify`,
    })
    .addVersion(
      {
        version: API_VERSIONS.internal.v1,
        validate: {
          request: {
            params: buildRouteValidationWithZod(NotifyCaseEventParams),
            body: buildRouteValidationWithZod(NotifyCaseEventRequestBody),
          },
        },
      },
      async (context, request, response): Promise<IKibanaResponse> => {
        const resp = buildResponse(response);
        const assistantContext = await context.elasticAssistant;
        const logger: Logger = assistantContext.logger;

        try {
          const soClient = assistantContext.savedObjectsClient;
          const spaceId = (await context.elasticAssistant).getSpaceId();
          const authenticatedUser = await assistantContext.getCurrentUser();

          if (!authenticatedUser) {
            return resp.error({
              body: 'Authenticated user not found',
              statusCode: 401,
            });
          }

          const { case_id: caseId } = request.params;
          const { eventType, data } = request.body;

          logger.info(`Received case event notification: case=${caseId}, type=${eventType}`);

          // Create event trigger service
          const triggerService = CaseEventTriggerService.create({
            logger,
            soClient,
            spaceId,
            currentUser: authenticatedUser.username ?? authenticatedUser.profile_uid ?? 'unknown',
          });

          // Create case event
          const event: CaseEvent = {
            type: eventType as CaseEventType,
            caseId,
            spaceId,
            timestamp: new Date().toISOString(),
            data: data as CaseEvent['data'],
          };

          // Track triggered actions
          const triggeredActions: TriggerActionRequest[] = [];

          // Handle the event
          await triggerService.handleEvent(event, async (actionRequest) => {
            triggeredActions.push(actionRequest);
            // In a real implementation, this would execute the action
            // For now, we just log it
            logger.info(
              `Trigger action ${actionRequest.action} scheduled for case ${actionRequest.caseId}`
            );
          });

          return response.ok({
            body: {
              case_id: caseId,
              event_type: eventType,
              triggered_actions: triggeredActions.map((a) => a.action),
              processed: true,
            },
          });
        } catch (err) {
          logger.error(err);
          const error = transformError(err);
          return resp.error({
            body: error.message,
            statusCode: error.statusCode,
          });
        }
      }
    );
};

/**
 * Register case Attack Discovery generation route
 * This route generates Attack Discovery for all alerts in a case and optionally attaches
 * the result to the case.
 */
export const registerCaseAttackDiscoveryRoute = (
  router: IRouter<ElasticAssistantRequestHandlerContext>
): void => {
  router.versioned
    .post({
      access: 'public',
      path: `${ALERT_GROUPING_CASE_BASE}/{case_id}/_generate_attack_discovery`,
      security: {
        authz: {
          requiredPrivileges: [ATTACK_DISCOVERY_API_ACTION_ALL],
        },
      },
    })
    .addVersion(
      {
        version: API_VERSIONS.public.v1,
        validate: {
          request: {
            params: buildRouteValidationWithZod(GenerateAttackDiscoveryRequestParams),
            body: buildRouteValidationWithZod(GenerateAttackDiscoveryRequestBody),
          },
        },
      },
      async (context, request, response): Promise<IKibanaResponse> => {
        const performChecksContext = await context.resolve([
          'core',
          'elasticAssistant',
          'licensing',
        ]);
        const resp = buildResponse(response);
        const assistantContext = await context.elasticAssistant;
        const logger: Logger = assistantContext.logger;

        const checkResponse = await performChecks({
          context: performChecksContext,
          request,
          response,
        });

        if (!checkResponse.isSuccess) {
          return checkResponse.response;
        }

        try {
          const authenticatedUser = await assistantContext.getCurrentUser();
          if (!authenticatedUser) {
            return resp.error({ body: 'Authenticated user not found', statusCode: 401 });
          }

          const { case_id: caseId } = request.params;
          const { connectorId, actionTypeId, alertsIndexPattern, attachToCase, updateCaseDetails } =
            request.body;

          // Get Cases client
          const casesPlugin = assistantContext.getCases();
          if (!casesPlugin) {
            return resp.error({
              body: 'Cases plugin not available',
              statusCode: 500,
            });
          }

          const casesClient = await casesPlugin.getCasesClientWithRequest(request);

          // Get the case to verify it exists and get alerts
          const theCase = await casesClient.cases.get({ id: caseId, includeComments: false });
          if (!theCase) {
            return resp.error({
              body: `Case ${caseId} not found`,
              statusCode: 404,
            });
          }

          // Get alerts attached to the case
          // Response format: { id: string, index: string, attached_at: string }[]
          const alertsResponse = await casesClient.attachments.getAllDocumentsAttachedToCase({
            caseId,
            attachmentTypes: ['alert'],
          });

          // Extract alert IDs from the response
          const alertIds = alertsResponse.map((alert) => alert.id);

          if (alertIds.length === 0) {
            return resp.error({
              body: `No alerts attached to case ${caseId}`,
              statusCode: 400,
            });
          }

          logger.info(
            `Generating Attack Discovery for case ${caseId} with ${alertIds.length} alerts`
          );

          // Get connector configuration
          let effectiveConnectorId = connectorId;
          let effectiveActionTypeId = actionTypeId;

          // If no connector specified, try to get default from UI settings
          if (!effectiveConnectorId) {
            try {
              const coreContext = await context.core;
              const uiSettingsClient = coreContext.uiSettings.client;
              const defaultConnectorSetting = await uiSettingsClient.get<string | undefined>(
                'securitySolution:defaultAIConnector'
              );
              if (defaultConnectorSetting && defaultConnectorSetting !== 'NO_DEFAULT_CONNECTOR') {
                effectiveConnectorId = defaultConnectorSetting;
              }
            } catch (err) {
              logger.debug(`Failed to get default connector from UI settings: ${err}`);
            }
          }

          if (!effectiveConnectorId) {
            return resp.error({
              body: 'No connector specified and no default connector configured',
              statusCode: 400,
            });
          }

          // Get connector details if actionTypeId not provided
          if (!effectiveActionTypeId) {
            const actions = assistantContext.actions;
            const actionsClient = await actions.getActionsClientWithRequest(request);
            try {
              const connector = await actionsClient.get({ id: effectiveConnectorId });
              effectiveActionTypeId = connector.actionTypeId;
            } catch (err) {
              return resp.error({
                body: `Failed to get connector details for ${effectiveConnectorId}: ${err}`,
                statusCode: 400,
              });
            }
          }

          // Get anonymization fields
          const spaceId = assistantContext.getSpaceId();
          const anonymizationFields = getDefaultAnonymizationFields(spaceId);

          // Build filter for specific alert IDs
          // Note: We don't use workflow_status or time range filters here
          // because we want to analyze ALL alerts attached to the case regardless
          // of their current status or when they were created
          const alertFilter = {
            bool: {
              must: [{ terms: { _id: alertIds } }],
            },
          };

          // Get required clients
          const esClient = (await context.core).elasticsearch.client.asCurrentUser;
          const actions = assistantContext.actions;
          const actionsClient = await actions.getActionsClientWithRequest(request);
          const soClient = (await context.core).savedObjects.client;

          // Generate Attack Discovery
          // Use very wide date range to ensure we get all alerts regardless of age
          // The default query uses now-24h which may miss older alerts
          logger.info(
            `Calling generateAttackDiscoveries for case ${caseId} with ${alertIds.length} alerts`
          );

          const result = await generateAttackDiscoveries({
            actionsClient,
            config: {
              alertsIndexPattern,
              anonymizationFields,
              apiConfig: {
                connectorId: effectiveConnectorId,
                actionTypeId: effectiveActionTypeId,
              },
              filter: alertFilter,
              size: alertIds.length,
              // Use a very wide date range to get all alerts regardless of age
              start: 'now-10y',
              end: 'now',
              // Skip workflow status filter - we want all alerts attached to the case
              // regardless of whether they are open, acknowledged, or closed
              allowAllWorkflowStatuses: true,
            },
            esClient,
            logger,
            savedObjectsClient: soClient,
          });

          // Log the result for debugging
          logger.info(
            `generateAttackDiscoveries result for case ${caseId}: ${JSON.stringify({
              hasResult: !!result,
              attackDiscoveriesCount: result?.attackDiscoveries?.length ?? 0,
              attackDiscoveriesIsNull: result?.attackDiscoveries === null,
              anonymizedAlertsCount: result?.anonymizedAlerts?.length ?? 0,
              firstDiscoveryTitle: result?.attackDiscoveries?.[0]?.title ?? 'none',
            })}`
          );

          // Process results
          const discoveries = result?.attackDiscoveries ?? [];
          if (!Array.isArray(discoveries)) {
            logger.warn(`Attack Discovery returned non-array for case ${caseId}`);
            return response.ok({
              body: {
                case_id: caseId,
                execution_status: 'completed',
                attack_discoveries: [],
                alerts_analyzed: alertIds.length,
                alerts_in_discoveries: 0,
                message: 'Attack Discovery returned no results',
              },
            });
          }

          // Extract all alert IDs referenced in discoveries
          const alertsInDiscoveries = new Set<string>();
          for (const discovery of discoveries) {
            if (discovery?.alertIds && Array.isArray(discovery.alertIds)) {
              for (const alertId of discovery.alertIds) {
                alertsInDiscoveries.add(alertId);
              }
            }
          }

          // Update case title and description if they are generic and we have discoveries
          let caseUpdated = false;
          if (updateCaseDetails && discoveries.length > 0) {
            const primaryDiscovery = discoveries[0];
            const shouldUpdateTitle = isGenericCaseTitle(theCase.title);
            const shouldUpdateDescription =
              !theCase.description ||
              theCase.description.trim() === '' ||
              theCase.description.includes('Automatically grouped by alert clustering pipeline');

            if (shouldUpdateTitle || shouldUpdateDescription) {
              try {
                const updatePayload: { title?: string; description?: string } = {};

                if (shouldUpdateTitle && primaryDiscovery.title) {
                  updatePayload.title = primaryDiscovery.title;
                  logger.info(
                    `Updating case ${caseId} title from "${theCase.title}" to "${primaryDiscovery.title}"`
                  );
                }

                if (shouldUpdateDescription && primaryDiscovery.summaryMarkdown) {
                  updatePayload.description = primaryDiscovery.summaryMarkdown;
                  logger.info(`Updating case ${caseId} description with Attack Discovery summary`);
                }

                if (Object.keys(updatePayload).length > 0) {
                  await casesClient.cases.bulkUpdate({
                    cases: [
                      {
                        id: caseId,
                        version: theCase.version,
                        ...updatePayload,
                      },
                    ],
                  });
                  caseUpdated = true;
                  logger.info(`Case ${caseId} updated with Attack Discovery details`);
                }
              } catch (updateErr) {
                logger.warn(`Failed to update case ${caseId} details: ${updateErr}`);
                // Continue - this is not a critical failure
              }
            }
          }

          // Attach Attack Discoveries to case as proper external reference attachments
          let attachedCount = 0;
          if (attachToCase && discoveries.length > 0) {
            try {
              // Get the Attack Discovery data client to create alert documents
              const adDataClient = await assistantContext.getAttackDiscoveryDataClient();

              if (!adDataClient) {
                logger.warn(`Attack Discovery data client not available for case ${caseId}`);
              } else {
                // Get connector details for storing with the alert
                let connectorName = 'Unknown Connector';
                try {
                  const connector = await actionsClient.get({ id: effectiveConnectorId });
                  connectorName = connector.name;
                } catch (connErr) {
                  logger.debug(`Could not get connector name: ${connErr}`);
                }

                const generationUuid = `case-${caseId}-${Date.now()}`;
                let alertsToAttach: Array<{
                  id: string;
                  backingIndex?: string;
                  title?: string;
                  timestamp?: string;
                }> = [];

                // Try to create Attack Discovery alert documents
                try {
                  const createdAlerts = await adDataClient.createAttackDiscoveryAlerts({
                    authenticatedUser,
                    createAttackDiscoveryAlertsParams: {
                      alertsContextCount: alertIds.length,
                      anonymizedAlerts: result.anonymizedAlerts ?? [],
                      apiConfig: {
                        connectorId: effectiveConnectorId,
                        actionTypeId: effectiveActionTypeId,
                      },
                      attackDiscoveries: discoveries,
                      connectorName,
                      enableFieldRendering: false,
                      generationUuid,
                      replacements: result.replacements ?? {},
                      withReplacements: false,
                    },
                  });
                  logger.info(
                    `Created ${createdAlerts.length} Attack Discovery alerts for case ${caseId}`
                  );
                  alertsToAttach = createdAlerts;
                } catch (createErr) {
                  // If creation failed (likely version conflict), try to find existing alerts
                  logger.warn(
                    `Failed to create AD alerts, trying to find existing ones: ${createErr}`
                  );

                  try {
                    // Find existing AD alerts by searching with the security alert IDs that were part of these discoveries
                    // This is more reliable than title search since the LLM can generate different titles
                    const existingAlerts = await adDataClient.findAttackDiscoveryAlerts({
                      authenticatedUser,
                      esClient,
                      findAttackDiscoveryAlertsParams: {
                        // Use alert IDs that are part of the discoveries
                        alertIds: Array.from(alertsInDiscoveries).slice(0, 10), // Use up to 10 alert IDs as filters
                        perPage: discoveries.length * 2,
                        sortField: '@timestamp',
                        sortOrder: 'desc',
                        enableFieldRendering: false,
                        withReplacements: false,
                      },
                      logger,
                    });

                    if (existingAlerts?.data && existingAlerts.data.length > 0) {
                      logger.info(
                        `Found ${existingAlerts.data.length} existing Attack Discovery alerts for case ${caseId}`
                      );
                      alertsToAttach = existingAlerts.data.map((alert) => ({
                        id: alert.id,
                        backingIndex: alert.backingIndex,
                        title: alert.title,
                        timestamp: alert.timestamp,
                      }));
                    } else {
                      logger.warn(`No existing AD alerts found for case ${caseId}`);
                    }
                  } catch (findErr) {
                    logger.error(`Failed to find existing AD alerts: ${findErr}`);
                  }
                }

                // Now attach each alert to the case as an external reference attachment
                logger.info(
                  `Attempting to attach ${alertsToAttach.length} AD alerts to case ${caseId}`
                );
                if (alertsToAttach.length > 0) {
                  const {
                    ATTACK_DISCOVERY_ATTACHMENT_TYPE,
                    ExternalReferenceStorageType,
                    AttachmentType,
                  } = await import('@kbn/cases-plugin/common');

                  logger.info(
                    `Using ATTACK_DISCOVERY_ATTACHMENT_TYPE: ${ATTACK_DISCOVERY_ATTACHMENT_TYPE}, ExternalReferenceStorageType: ${JSON.stringify(
                      ExternalReferenceStorageType
                    )}, AttachmentType.externalReference: ${AttachmentType.externalReference}`
                  );

                  for (const alert of alertsToAttach) {
                    try {
                      logger.info(
                        `Attaching AD alert ${alert.id} with title "${alert.title}" to case ${caseId}`
                      );
                      await casesClient.attachments.add({
                        caseId,
                        comment: {
                          type: AttachmentType.externalReference,
                          externalReferenceId: alert.id,
                          externalReferenceStorage: {
                            type: ExternalReferenceStorageType.elasticSearchDoc,
                          },
                          externalReferenceAttachmentTypeId: ATTACK_DISCOVERY_ATTACHMENT_TYPE,
                          externalReferenceMetadata: {
                            attackDiscoveryAlertId: alert.id,
                            index: alert.backingIndex ?? '',
                            generationUuid,
                            title: alert.title ?? 'Attack Discovery',
                            timestamp: alert.timestamp ?? new Date().toISOString(),
                          },
                          owner: theCase.owner,
                        },
                      });
                      attachedCount++;
                      logger.info(
                        `Successfully attached Attack Discovery alert ${alert.id} to case ${caseId}`
                      );
                    } catch (attachErr) {
                      // Check if already attached
                      const errMsg = String(attachErr);
                      logger.error(`Attachment error for ${alert.id}: ${errMsg}`);
                      if (errMsg.includes('already attached') || errMsg.includes('duplicate')) {
                        logger.info(
                          `Attack Discovery alert ${alert.id} already attached to case ${caseId}`
                        );
                        attachedCount++;
                      } else {
                        logger.error(
                          `Failed to attach Attack Discovery alert ${alert.id} to case ${caseId}: ${attachErr}`
                        );
                      }
                    }
                  }
                } else {
                  logger.warn(`No AD alerts to attach for case ${caseId}`);
                }
              }
            } catch (err) {
              logger.error(
                `Failed to process Attack Discovery attachments for case ${caseId}: ${err}`
              );
            }
          }

          // ── Alert Rejection: detach alerts NOT referenced in any AD ──
          // Alerts that were in the case but were NOT included by any discovery
          // are considered "irrelevant" for this attack chain. We:
          //   1. Detach them from the case
          //   2. Remove the 'llm-triaged' tag so they can be re-grouped in the next round
          const rejectedAlertIds = alertIds.filter((id) => !alertsInDiscoveries.has(id));
          let alertsDetached = 0;
          if (rejectedAlertIds.length > 0 && discoveries.length > 0) {
            logger.info(
              `Rejecting ${rejectedAlertIds.length} alerts from case ${caseId} ` +
                `(not referenced by any Attack Discovery)`
            );

            // 1. Detach rejected alerts from the case
            try {
              // Find the attachment IDs for these alerts so we can delete them
              const allAttachments = await casesClient.attachments.getAll({
                caseID: caseId,
              });
              const alertAttachments = (
                allAttachments as Array<{ id: string; type: string; alertId?: string }>
              ).filter(
                (att) =>
                  att.type === 'alert' && att.alertId && rejectedAlertIds.includes(att.alertId)
              );

              for (const att of alertAttachments) {
                try {
                  await casesClient.attachments.delete({
                    caseID: caseId,
                    attachmentID: att.id,
                  });
                  alertsDetached++;
                } catch (detachErr) {
                  logger.warn(
                    `Failed to detach alert attachment ${att.id} from case ${caseId}: ${detachErr}`
                  );
                }
              }
              logger.info(
                `Detached ${alertsDetached} rejected alert attachments from case ${caseId}`
              );
            } catch (detachErr) {
              logger.warn(`Failed to detach rejected alerts from case ${caseId}: ${detachErr}`);
            }

            // 2. Remove 'llm-triaged' tag from rejected alerts so they are re-processed
            try {
              const rejectedAlertIdsForUpdate = rejectedAlertIds.slice(0, 500); // Safety limit
              await esClient.updateByQuery({
                index: '.alerts-security.alerts-*',
                refresh: true,
                body: {
                  query: {
                    terms: { _id: rejectedAlertIdsForUpdate },
                  },
                  script: {
                    source: `
                      if (ctx._source.containsKey('kibana.alert.workflow_tags')) {
                        ctx._source['kibana.alert.workflow_tags'].removeIf(tag -> tag == 'llm-triaged');
                      }
                    `,
                    lang: 'painless',
                  },
                },
              });
              logger.info(
                `Removed 'llm-triaged' tag from ${rejectedAlertIdsForUpdate.length} rejected alerts ` +
                  `so they can be re-grouped in the next alert grouping round`
              );
            } catch (tagErr) {
              logger.warn(`Failed to remove llm-triaged tag from rejected alerts: ${tagErr}`);
            }

            // 3. Add a comment to the case explaining the rejection with clickable alert links
            try {
              // Fetch alert details for the rejected alerts to build a rich comment
              let rejectedAlertDetails: Array<{
                id: string;
                ruleName: string;
                timestamp: string;
                index: string;
              }> = [];

              try {
                const rejectedAlertsResponse = await esClient.search({
                  index: '.alerts-security.alerts-*',
                  size: rejectedAlertIds.length,
                  body: {
                    query: { terms: { _id: rejectedAlertIds } },
                    _source: ['kibana.alert.rule.name', '@timestamp'],
                  },
                });

                rejectedAlertDetails = (rejectedAlertsResponse.hits?.hits ?? []).map((hit) => {
                  const source = hit._source as Record<string, unknown>;
                  return {
                    id: hit._id as string,
                    ruleName: (source['kibana.alert.rule.name'] as string) ?? 'Unknown Rule',
                    timestamp: (source['@timestamp'] as string) ?? '',
                    index: hit._index ?? '.alerts-security.alerts-default',
                  };
                });
              } catch (fetchErr) {
                logger.warn(`Failed to fetch rejected alert details: ${fetchErr}`);
                // Fall back to just IDs
                rejectedAlertDetails = rejectedAlertIds.map((id) => ({
                  id,
                  ruleName: 'Unknown',
                  timestamp: '',
                  index: '.alerts-security.alerts-default',
                }));
              }

              // Build a markdown table with clickable alert links
              const alertRows = rejectedAlertDetails.map((alert) => {
                const alertLink = `/app/security/alerts/redirect/${
                  alert.id
                }?index=${encodeURIComponent(alert.index)}&timestamp=${encodeURIComponent(
                  alert.timestamp
                )}`;
                const shortId =
                  alert.id.length > 12
                    ? `${alert.id.substring(0, 8)}...${alert.id.substring(alert.id.length - 4)}`
                    : alert.id;
                const formattedTime = alert.timestamp
                  ? new Date(alert.timestamp).toLocaleString('en-US', {
                      month: 'short',
                      day: 'numeric',
                      hour: '2-digit',
                      minute: '2-digit',
                      second: '2-digit',
                      hour12: false,
                    })
                  : '—';

                return `| [${shortId}](${alertLink}) | ${alert.ruleName} | ${formattedTime} |`;
              });

              const commentMarkdown =
                `**Alert Rejection by Attack Discovery**\n\n` +
                `Attack Discovery analyzed **${alertIds.length}** alerts and determined that ` +
                `**${rejectedAlertIds.length}** alert(s) are not part of the identified attack chain.\n\n` +
                `These alerts have been detached from this case and their \`llm-triaged\` tag ` +
                `has been removed so they can be re-processed in the next alert grouping round ` +
                `and potentially form their own case.\n\n` +
                `| Alert | Rule | Timestamp |\n` +
                `|-------|------|----------|\n${alertRows.join('\n')}`;

              await casesClient.attachments.add({
                caseId,
                comment: {
                  type: 'user' as const,
                  comment: commentMarkdown,
                  owner: theCase.owner,
                },
              });
            } catch (commentErr) {
              logger.warn(`Failed to add rejection comment to case ${caseId}: ${commentErr}`);
            }
          }

          logger.info(
            `Attack Discovery for case ${caseId}: generated ${discoveries.length} discoveries, ` +
              `${alertsInDiscoveries.size}/${alertIds.length} alerts referenced, ` +
              `${attachedCount} attached to case, ${alertsDetached} alerts rejected/detached`
          );

          return response.ok({
            body: {
              case_id: caseId,
              execution_status: 'completed',
              attack_discoveries: discoveries.map((d) => ({
                id: d.id,
                title: d.title,
                summary: d.summaryMarkdown,
                details: d.detailsMarkdown,
                mitre_tactics: d.mitreAttackTactics,
                risk_score: d.riskScore,
                alert_ids: d.alertIds,
                entity_summary: d.entitySummaryMarkdown,
              })),
              alerts_analyzed: alertIds.length,
              alerts_in_discoveries: alertsInDiscoveries.size,
              alerts_rejected: rejectedAlertIds.length,
              rejected_alert_ids: rejectedAlertIds,
              alerts_detached_from_case: alertsDetached,
              attached_to_case: attachedCount,
              case_updated: caseUpdated,
            },
          });
        } catch (err) {
          logger.error(`Failed to generate Attack Discovery for case: ${err}`);
          const error = transformError(err);
          return resp.error({
            body: error.message,
            statusCode: error.statusCode,
          });
        }
      }
    );
};
