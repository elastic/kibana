import { z } from '@kbn/zod/v4';
import { ToolType, ToolResultType } from '@kbn/agent-builder-common';
import type { BuiltinToolDefinition } from '@kbn/agent-builder-server';
import { getToolResultId } from '@kbn/agent-builder-server/tools';
import type { Logger } from '@kbn/logging';
import { getAgentBuilderResourceAvailability } from '../utils/get_agent_builder_resource_availability';
import { securityTool } from './constants';
import type { SecuritySolutionPluginCoreSetupDependencies } from '../../plugin_contract';

const reportSectionsSchema = z.object({
  executive_summary: z
    .string()
    .describe('High-level summary of the incident for executive stakeholders'),
  timeline: z
    .string()
    .describe(
      'Chronological sequence of events during the incident, including timestamps and key actions'
    ),
  mitre_mapping: z
    .string()
    .optional()
    .describe(
      'MITRE ATT&CK technique and tactic mappings relevant to the incident (e.g., T1059 - Command and Scripting Interpreter)'
    ),
  impact_assessment: z
    .string()
    .optional()
    .describe(
      'Assessment of the impact including affected systems, data exposure, business impact, and blast radius'
    ),
  recommendations: z
    .string()
    .optional()
    .describe('Recommended remediation steps, hardening measures, and follow-up actions'),
});

const reportGenerateSchema = z.object({
  title: z.string().describe('Title of the incident report'),
  sections: reportSectionsSchema.describe('Structured sections of the incident report'),
  format: z
    .enum(['markdown', 'json'])
    .optional()
    .describe('Output format for the report. Defaults to markdown.'),
});

export const SECURITY_REPORT_GENERATE_TOOL_ID = securityTool('report_generate');

/**
 * Formats the report sections into a structured markdown document
 */
const formatAsMarkdown = (
  title: string,
  sections: z.infer<typeof reportSectionsSchema>
): string => {
  const timestamp = new Date().toISOString();
  const parts: string[] = [];

  parts.push(`# Incident Report: ${title}`);
  parts.push('');
  parts.push(`**Generated:** ${timestamp}`);
  parts.push(`**Status:** Draft`);
  parts.push('');
  parts.push('---');
  parts.push('');

  // Executive Summary
  parts.push('## Executive Summary');
  parts.push('');
  parts.push(sections.executive_summary);
  parts.push('');

  // Timeline
  parts.push('## Incident Timeline');
  parts.push('');
  parts.push(sections.timeline);
  parts.push('');

  // MITRE ATT&CK Mapping
  if (sections.mitre_mapping) {
    parts.push('## MITRE ATT&CK Mapping');
    parts.push('');
    parts.push(sections.mitre_mapping);
    parts.push('');
  }

  // Impact Assessment
  if (sections.impact_assessment) {
    parts.push('## Impact Assessment');
    parts.push('');
    parts.push(sections.impact_assessment);
    parts.push('');
  }

  // Recommendations
  if (sections.recommendations) {
    parts.push('## Recommendations');
    parts.push('');
    parts.push(sections.recommendations);
    parts.push('');
  }

  parts.push('---');
  parts.push('');
  parts.push('*This report was generated by the Elastic AI SOC Agent Builder.*');

  return parts.join('\n');
};

/**
 * Formats the report sections into a structured JSON object
 */
const formatAsJson = (
  title: string,
  sections: z.infer<typeof reportSectionsSchema>
): Record<string, unknown> => {
  return {
    title,
    generated_at: new Date().toISOString(),
    status: 'draft',
    sections: {
      executive_summary: sections.executive_summary,
      timeline: sections.timeline,
      ...(sections.mitre_mapping && { mitre_mapping: sections.mitre_mapping }),
      ...(sections.impact_assessment && { impact_assessment: sections.impact_assessment }),
      ...(sections.recommendations && { recommendations: sections.recommendations }),
    },
  };
};

export const reportGenerateTool = (
  core: SecuritySolutionPluginCoreSetupDependencies,
  logger: Logger
): BuiltinToolDefinition<typeof reportGenerateSchema> => {
  return {
    id: SECURITY_REPORT_GENERATE_TOOL_ID,
    type: ToolType.builtin,
    description:
      'Generate structured incident reports from investigation context with sections for executive summary, technical timeline, MITRE ATT&CK mapping, impact assessment, and recommendations.',
    schema: reportGenerateSchema,
    availability: {
      cacheMode: 'space',
      handler: async ({ request }) => {
        return getAgentBuilderResourceAvailability({ core, request, logger });
      },
    },
    handler: async ({ title, sections, format }) => {
      const outputFormat = format ?? 'markdown';

      logger.debug(
        `${SECURITY_REPORT_GENERATE_TOOL_ID} tool called with title: "${title}", format: ${outputFormat}`
      );

      try {
        const sectionCount = [
          'executive_summary',
          'timeline',
          sections.mitre_mapping ? 'mitre_mapping' : null,
          sections.impact_assessment ? 'impact_assessment' : null,
          sections.recommendations ? 'recommendations' : null,
        ].filter(Boolean).length;

        if (outputFormat === 'json') {
          const jsonReport = formatAsJson(title, sections);

          logger.debug(
            `Successfully generated JSON incident report "${title}" with ${sectionCount} sections`
          );

          return {
            results: [
              {
                tool_result_id: getToolResultId(),
                type: ToolResultType.other,
                data: {
                  format: 'json',
                  report: jsonReport,
                  section_count: sectionCount,
                  message: `Incident report "${title}" generated successfully in JSON format with ${sectionCount} sections.`,
                },
              },
            ],
          };
        }

        const markdownReport = formatAsMarkdown(title, sections);

        logger.debug(
          `Successfully generated markdown incident report "${title}" with ${sectionCount} sections`
        );

        return {
          results: [
            {
              tool_result_id: getToolResultId(),
              type: ToolResultType.other,
              data: {
                format: 'markdown',
                report: markdownReport,
                section_count: sectionCount,
                message: `Incident report "${title}" generated successfully in markdown format with ${sectionCount} sections.`,
              },
            },
          ],
        };
      } catch (error) {
        const errorMessage = error instanceof Error ? error.message : String(error);
        logger.error(`Error in ${SECURITY_REPORT_GENERATE_TOOL_ID} tool: ${errorMessage}`);
        return {
          results: [
            {
              tool_result_id: getToolResultId(),
              type: ToolResultType.error,
              data: {
                message: `Error generating report: ${errorMessage}`,
              },
            },
          ],
        };
      }
    },
    tags: ['security', 'reporting', 'incident-response'],
  };
};
