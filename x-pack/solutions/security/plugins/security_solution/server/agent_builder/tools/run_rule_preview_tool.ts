import { z } from '@kbn/zod/v4';
import dateMath from '@kbn/datemath';
import { parseDuration } from '@kbn/alerting-plugin/common';
import { ToolType } from '@kbn/agent-builder-common';
import { ToolResultType } from '@kbn/agent-builder-common/tools/tool_result';
import { ConfirmationStatus } from '@kbn/agent-builder-common/agents/prompts';
import type { BuiltinToolDefinition, StaticToolRegistration } from '@kbn/agent-builder-server';
import { EsqlRuleCreateProps } from '../../../common/api/detection_engine/model/rule_schema';
import type { RulePreviewRequestBody } from '../../../common/api/detection_engine';
import { SecurityAgentBuilderAttachments } from '../../../common/constants';
import { securityTool } from './constants';
import type { RunRulePreviewDeps } from '../../lib/detection_engine/rule_preview/api/preview_rules/run_rule_preview';
import { runRulePreview } from '../../lib/detection_engine/rule_preview/api/preview_rules/run_rule_preview';

export const SECURITY_RUN_RULE_PREVIEW_TOOL_ID = securityTool('run_rule_preview');

const DEFAULT_TIMEFRAME_START = 'now-1h';
const DEFAULT_TIMEFRAME_END = 'now';
const DEFAULT_INTERVAL = '1h';
const DEFAULT_RULE_FROM = 'now-1h';
const DEFAULT_RULE_TO = 'now';

/**
 * Ask the user to confirm before running a preview with more than this many
 * executor invocations.  Beyond this count the preview can take a significant
 * amount of time and should be intentional.
 */
const INVOCATION_COUNT_CONFIRMATION_THRESHOLD = 10;

/**
 * Fields required by `EsqlRuleCreateProps` that do not affect whether/which alerts a
 * preview generates. We default them here so the agent only has to provide the fields
 * that actually matter for a preview (the ES|QL query and, optionally, the schedule).
 */
const RULE_PREVIEW_RULE_DEFAULTS = {
  type: 'esql',
  language: 'esql',
  name: 'Ad-hoc rule preview',
  description: 'Ad-hoc rule preview generated by the run_rule_preview tool.',
  risk_score: 21,
  severity: 'low',
} as const;

/**
 * Minimal subset of `EsqlRuleCreateProps` the agent needs to supply to run a preview.
 * Derived from the full type via `.pick` so field shapes/validation stay in sync, then
 * `.extend`ed to add per-field descriptions and defaults for the optional schedule fields.
 * Each override references the picked `.shape` so the underlying validation (e.g. `from`'s
 * datemath check) is preserved.
 */
const previewRuleBaseSchema = EsqlRuleCreateProps.pick({
  query: true,
  interval: true,
  from: true,
  to: true,
});

const previewRuleSchema = previewRuleBaseSchema
  .extend({
    query: previewRuleBaseSchema.shape.query.describe(
      'The ES|QL query for the detection rule to preview (for example the query produced by the create_detection_rule tool).'
    ),
    interval: previewRuleBaseSchema.shape.interval
      .default(DEFAULT_INTERVAL)
      .describe(
        `How often the rule would run, as a duration (e.g. "5m", "1h"). Determines how many times the rule is evaluated across the preview time range. Defaults to "${DEFAULT_INTERVAL}".`
      ),
    from: previewRuleBaseSchema.shape.from
      .default(DEFAULT_RULE_FROM)
      .describe(
        `Start of each run's look-back window relative to the run time, as datemath (e.g. "now-6m"). Defaults to "${DEFAULT_RULE_FROM}".`
      ),
    to: previewRuleBaseSchema.shape.to
      .default(DEFAULT_RULE_TO)
      .describe(
        `End of each run's look-back window, as datemath (e.g. "now"). Defaults to "${DEFAULT_RULE_TO}".`
      ),
  })
  .describe(
    'The ES|QL detection rule to preview, e.g. the rule produced by the create_detection_rule tool. Only the ES|QL `query` is required; `interval`, `from`, and `to` control the rule schedule/look-back and default if omitted. The rule is evaluated but never saved.'
  );

const runRulePreviewSchema = z.object({
  rule: previewRuleSchema,
  timeframeStart: z
    .string()
    .default(DEFAULT_TIMEFRAME_START)
    .describe(
      'Start of the preview time range as datemath (e.g. "now-1h", "now-24h", "now-7d"). The rule is evaluated over the range [timeframeStart, timeframeEnd]. Defaults to "now-1h".'
    ),
  timeframeEnd: z
    .string()
    .default(DEFAULT_TIMEFRAME_END)
    .describe('End of the preview time range as datemath (e.g. "now"). Defaults to "now".'),
  enableLoggedRequests: z
    .boolean()
    .default(false)
    .describe(
      'When true, includes the Elasticsearch queries executed during the preview in the response logs. Defaults to false.'
    ),
});

export function runRulePreviewTool(
  deps: RunRulePreviewDeps
): StaticToolRegistration<typeof runRulePreviewSchema> {
  const toolDefinition: BuiltinToolDefinition<typeof runRulePreviewSchema> = {
    id: SECURITY_RUN_RULE_PREVIEW_TOOL_ID,
    type: ToolType.builtin,
    description: `Runs a security detection rule preview over a time range without saving the rule, then stores the result as a rule preview attachment.

Call this whenever a detection rule is created or modified (for example a rule produced or edited by the create_detection_rule tool) to validate the change before saving, and inspect the alerts it would have generated. The timeframe defaults to the last hour but can be overridden with datemath via timeframeStart and timeframeEnd.

The tool returns the generated previewId and the attachment metadata. Use the returned attachmentId and version with <render_attachment id="..." version="..."> to display it.`,
    schema: runRulePreviewSchema,
    tags: ['security', 'detection', 'rule-preview', 'attachment'],
    handler: async (
      { rule, timeframeStart, timeframeEnd, enableLoggedRequests },
      { request, spaceId, savedObjectsClient, attachments, prompts, callContext }
    ) => {
      const start = dateMath.parse(timeframeStart);
      const end = dateMath.parse(timeframeEnd, { roundUp: true });

      if (!start?.isValid() || !end?.isValid()) {
        return {
          results: [
            {
              type: ToolResultType.error,
              data: {
                message: `Invalid timeframe. Could not parse timeframeStart "${timeframeStart}" or timeframeEnd "${timeframeEnd}" as datemath.`,
              },
            },
          ],
        };
      }

      if (end.valueOf() <= start.valueOf()) {
        return {
          results: [
            {
              type: ToolResultType.error,
              data: {
                message: `Invalid timeframe. timeframeEnd must be after timeframeStart.`,
              },
            },
          ],
        };
      }

      if (deps.config.experimentalFeatures.esqlRulesDisabled) {
        return {
          results: [
            {
              type: ToolResultType.error,
              data: { message: 'ES|QL rule previews are not supported on this cluster.' },
            },
          ],
        };
      }

      const intervalMs = parseDuration(rule.interval ?? DEFAULT_INTERVAL);
      if (!intervalMs) {
        return {
          results: [
            {
              type: ToolResultType.error,
              data: { message: `Invalid rule interval "${rule.interval ?? DEFAULT_INTERVAL}".` },
            },
          ],
        };
      }

      const invocationCount = Math.max(
        Math.ceil((end.valueOf() - start.valueOf()) / intervalMs),
        1
      );

      if (invocationCount > INVOCATION_COUNT_CONFIRMATION_THRESHOLD) {
        const promptId = `run_rule_preview.high_invocation_count.${callContext.toolCallId}`;
        const { status } = prompts.checkConfirmationStatus(promptId);

        if (status === ConfirmationStatus.unprompted) {
          return prompts.askForConfirmation({
            id: promptId,
            title: 'Large rule preview',
            message: `This preview will run the rule **${invocationCount}** times (${timeframeStart} → ${timeframeEnd} at \`${
              rule.interval ?? DEFAULT_INTERVAL
            }\` intervals). Large previews can take a while. Do you want to continue?`,
            confirm_text: 'Run preview',
            cancel_text: 'Cancel',
            color: 'warning',
          });
        }

        if (status === ConfirmationStatus.rejected) {
          return {
            results: [
              {
                type: ToolResultType.error,
                data: {
                  message: [
                    'The user explicitly rejected this rule preview — it was not run.',
                    'Tell the user the preview did not run because they cancelled it.',
                    'If they still want to run it, they can confirm the preview with:',
                    `  timeframeStart: ${timeframeStart}`,
                    `  timeframeEnd: ${timeframeEnd}`,
                    `  interval: ${rule.interval ?? DEFAULT_INTERVAL}`,
                    `  query: ${rule.query}`,
                  ].join('\n'),
                },
              },
            ],
          };
        }
      }

      const body = {
        ...RULE_PREVIEW_RULE_DEFAULTS,
        ...rule,
        invocationCount,
        timeframeEnd: end.toISOString(),
      } as RulePreviewRequestBody;

      const [coreStart, startPlugins] = await deps.getStartServices();
      const uiSettingsClient = coreStart.uiSettings.asScopedToClient(savedObjectsClient);
      const actionsClient = await startPlugins.actions.getActionsClientWithRequest(request);
      const license = await startPlugins.licensing.getLicense();

      const previewResponse = await runRulePreview(deps, {
        body,
        enableLoggedRequests,
        request,
        spaceId,
        actionsClient,
        license,
        savedObjectsClient,
        uiSettingsClient,
      });

      if (!previewResponse.previewId) {
        const errors = previewResponse.logs.flatMap((log) => log.errors);
        return {
          results: [
            {
              type: ToolResultType.error,
              data: {
                message:
                  errors.length > 0
                    ? `Rule preview did not produce a preview: ${errors.join('; ')}`
                    : 'Rule preview did not produce a preview.',
                logs: previewResponse.logs,
              },
            },
          ],
        };
      }

      const { previewId } = previewResponse;
      const created = await attachments.add({
        id: `security-rule-preview-${previewId}`,
        type: SecurityAgentBuilderAttachments.rulePreview,
        data: {
          previewId,
        },
        description: `Rule preview: ${previewId}`,
      });

      return {
        results: [
          {
            type: ToolResultType.other,
            data: {
              attachmentId: created.id,
              type: created.type,
              version: created.current_version,
              previewId,
              invocationCount,
              logs: previewResponse.logs,
            },
          },
        ],
      };
    },
  };

  return toolDefinition;
}
