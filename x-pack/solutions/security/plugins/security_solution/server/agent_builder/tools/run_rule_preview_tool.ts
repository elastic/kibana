/**
 * Per-type minimal schemas for previewing a detection rule. Each branch picks
 * only the fields that affect preview results plus the shared schedule fields.
 * Boilerplate fields (name, description, risk_score, severity) are defaulted
 * in the handler so the LLM only has to supply type-specific fields.
 */
const previewRuleSchema = z.discriminatedUnion('type', [
  esqlRulePreviewSchema,
  eqlRulePreviewSchema,
  queryRulePreviewSchema,
  savedQueryRulePreviewSchema,
  thresholdRulePreviewSchema,
  threatMatchRulePreviewSchema,
  mlRulePreviewSchema,
  newTermsRulePreviewSchema,
]);

const runRulePreviewSchema = z.object({
  rule: previewRuleSchema.describe(
    'The detection rule to preview. Set `rule.type` to select the rule type — each type has its own required fields. The rule is evaluated but never saved.'
  ),
  interval: z.string().default(DEFAULT_INTERVAL).describe(INTERVAL_DESC),
  from: z.string().default(DEFAULT_RULE_FROM).describe(FROM_DESC),
  to: z.string().default(DEFAULT_RULE_TO).describe(TO_DESC),
  timeframeStart: z
    .string()
    .default(DEFAULT_TIMEFRAME_START)
    .describe(
      'Start of the preview time range as datemath (e.g. "now-1h", "now-24h", "now-7d"). The rule is evaluated over [timeframeStart, timeframeEnd]. Defaults to "now-1h".'
    ),
  timeframeEnd: z
    .string()
    .default(DEFAULT_TIMEFRAME_END)
    .describe('End of the preview time range as datemath (e.g. "now"). Defaults to "now".'),
  enableLoggedRequests: z
    .boolean()
    .default(false)
    .describe(
      'When true, includes the Elasticsearch queries executed during the preview in the response logs. Defaults to false.'
    ),
});

export function runRulePreviewTool(
  deps: RunRulePreviewDeps
): StaticToolRegistration<typeof runRulePreviewSchema> {
  const toolDefinition: BuiltinToolDefinition<typeof runRulePreviewSchema> = {
    id: SECURITY_RUN_RULE_PREVIEW_TOOL_ID,
    type: ToolType.builtin,
    description: `Runs a security detection rule preview over a time range without saving the rule, then stores the result as a rule preview attachment.

Supports all previewable rule types: esql, eql, query, saved_query, threshold, threat_match, machine_learning, and new_terms. Call this whenever a rule is created or modified to validate the change before saving and inspect the alerts it would have generated. The timeframe defaults to the last hour but can be overridden via timeframeStart and timeframeEnd.

The tool returns the generated previewId and the attachment metadata. Use the returned attachmentId and version with <render_attachment id="..." version="..."> to display it.`,
    schema: runRulePreviewSchema,
    tags: ['security', 'detection', 'rule-preview', 'attachment'],
    handler: async (
      { rule, interval, from, to, timeframeStart, timeframeEnd, enableLoggedRequests },
      { request, spaceId, savedObjectsClient, attachments, prompts, callContext }
    ) => {
      const start = dateMath.parse(timeframeStart);
      const end = dateMath.parse(timeframeEnd, { roundUp: true });

      if (!start?.isValid() || !end?.isValid()) {
        return {
          results: [
            {
              type: ToolResultType.error,
              data: {
                message: `Invalid timeframe. Could not parse timeframeStart "${timeframeStart}" or timeframeEnd "${timeframeEnd}" as datemath.`,
              },
            },
          ],
        };
      }

      if (end.valueOf() <= start.valueOf()) {
        return {
          results: [
            {
              type: ToolResultType.error,
              data: {
                message: `Invalid timeframe. timeframeEnd must be after timeframeStart.`,
              },
            },
          ],
        };
      }

      if (rule.type === 'esql' && deps.config.experimentalFeatures.esqlRulesDisabled) {
        return {
          results: [
            {
              type: ToolResultType.error,
              data: { message: 'ES|QL rule previews are not supported on this cluster.' },
            },
          ],
        };
      }

      const intervalMs = parseDuration(interval);
      if (!intervalMs) {
        return {
          results: [
            {
              type: ToolResultType.error,
              data: { message: `Invalid rule interval "${interval}".` },
            },
          ],
        };
      }

      const invocationCount = Math.max(
        Math.ceil((end.valueOf() - start.valueOf()) / intervalMs),
        1
      );

      if (invocationCount > INVOCATION_COUNT_CONFIRMATION_THRESHOLD) {
        const promptId = `run_rule_preview.high_invocation_count.${callContext.toolCallId}`;
        const { status } = prompts.checkConfirmationStatus(promptId);

        if (status === ConfirmationStatus.unprompted) {
          return prompts.askForConfirmation({
            id: promptId,
            title: 'Large rule preview',
            message: `This preview will run the rule **${invocationCount}** times (${timeframeStart} → ${timeframeEnd} at \`${interval}\` intervals). Large previews can take a while. Do you want to continue?`,
            confirm_text: 'Run preview',
            cancel_text: 'Cancel',
            color: 'warning',
          });
        }

        if (status === ConfirmationStatus.rejected) {
          const queryLine =
            'query' in rule && rule.query !== undefined ? `  query: ${rule.query}` : null;

          return {
            results: [
              {
                type: ToolResultType.error,
                data: {
                  message: [
                    'The user explicitly rejected this rule preview — it was not run.',
                    'Tell the user the preview did not run because they cancelled it.',
                    'If they still want to run it, they can confirm the preview with:',
                    `  timeframeStart: ${timeframeStart}`,
                    `  timeframeEnd: ${timeframeEnd}`,
                    `  interval: ${interval}`,
                    ...(queryLine ? [queryLine] : []),
                  ].join('\n'),
                },
              },
            ],
          };
        }
      }

      const body = {
        ...RULE_PREVIEW_SHARED_DEFAULTS,
        ...rule,
        interval,
        from,
        to,
        invocationCount,
        timeframeEnd: end.toISOString(),
      } as RulePreviewRequestBody;

      const [coreStart, startPlugins] = await deps.getStartServices();
      const uiSettingsClient = coreStart.uiSettings.asScopedToClient(savedObjectsClient);
      const actionsClient = await startPlugins.actions.getActionsClientWithRequest(request);
      const license = await startPlugins.licensing.getLicense();

      const previewResponse = await runRulePreview(deps, {
        body,
        enableLoggedRequests,
        request,
        spaceId,
        actionsClient,
        license,
        savedObjectsClient,
        uiSettingsClient,
      });

      if (!previewResponse.previewId) {
        const errors = previewResponse.logs.flatMap((log) => log.errors);
        return {
          results: [
            {
              type: ToolResultType.error,
              data: {
                message:
                  errors.length > 0
                    ? `Rule preview did not produce a preview: ${errors.join('; ')}`
                    : 'Rule preview did not produce a preview.',
                logs: previewResponse.logs,
              },
            },
          ],
        };
      }

      const { previewId } = previewResponse;
      const created = await attachments.add({
        id: `security-rule-preview-${previewId}`,
        type: SecurityAgentBuilderAttachments.rulePreview,
        data: {
          previewId,
        },
        description: `Rule preview: ${previewId}`,
      });

      return {
        results: [
          {
            type: ToolResultType.other,
            data: {
              attachmentId: created.id,
              type: created.type,
              version: created.current_version,
              previewId,
              invocationCount,
              logs: previewResponse.logs,
            },
          },
        ],
      };
    },
  };

  return toolDefinition;
}
