import type { z } from '@kbn/zod/v4';
import type { RuleResponse } from './rule_schemas.gen';
import {
  RuleCreateProps as RuleCreatePropsGen,
  RulePatchProps as RulePatchPropsGen,
  RuleUpdateProps as RuleUpdatePropsGen,
} from './rule_schemas.gen';

export function isCustomizedPrebuiltRule(rule: RuleResponse): boolean {
  return rule.rule_source?.type === 'external' && rule.rule_source.is_customized;
}

export function isNonCustomizedPrebuiltRule(rule: RuleResponse): boolean {
  return rule.rule_source?.type === 'external' && rule.rule_source.is_customized === false;
}

/**
 * Cross-field validation for `alert_suppression`: when suppression is enabled, at least one of
 * `group_by` or `group_by_v2` must be provided with at least one field. The generated
 * `AlertSuppression` schema cannot express this on its own (both fields are individually
 * optional), so we attach a `superRefine` here. Threshold rules are excluded because that
 * variant strips both group_by fields before reaching this refine.
 *
 * Lives outside `rule_schemas.gen.ts` because that file is regenerated by
 * `yarn openapi:generate`; this wrapper survives regeneration.
 */
const requireSuppressionGroupBy = (data: unknown, ctx: z.core.$RefinementCtx) => {
  const value = data as
    | { type?: string; alert_suppression?: { group_by?: unknown; group_by_v2?: unknown } }
    | undefined;
  if (value == null || value.type === 'threshold') {
    return;
  }
  const suppression = value.alert_suppression;
  if (suppression == null) {
    return;
  }
  const hasGroupBy = Array.isArray(suppression.group_by) && suppression.group_by.length > 0;
  const hasGroupByV2 = Array.isArray(suppression.group_by_v2) && suppression.group_by_v2.length > 0;
  if (!hasGroupBy && !hasGroupByV2) {
    ctx.addIssue({
      code: 'custom',
      path: ['alert_suppression'],
      message: 'Either group_by or group_by_v2 must be provided with at least one field',
    });
  }
};

export const RuleCreatePropsWithSuppressionRefinement = (
  RuleCreatePropsGen as unknown as z.ZodType
).superRefine(requireSuppressionGroupBy) as unknown as typeof RuleCreatePropsGen;

export const RuleUpdatePropsWithSuppressionRefinement = (
  RuleUpdatePropsGen as unknown as z.ZodType
).superRefine(requireSuppressionGroupBy) as unknown as typeof RuleUpdatePropsGen;

export const RulePatchPropsWithSuppressionRefinement = (
  RulePatchPropsGen as unknown as z.ZodType
).superRefine(requireSuppressionGroupBy) as unknown as typeof RulePatchPropsGen;
