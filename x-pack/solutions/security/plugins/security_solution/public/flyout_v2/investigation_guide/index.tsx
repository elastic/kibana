import { css } from '@emotion/react';
import React, { memo, useMemo } from 'react';
import {
  EuiCallOut,
  EuiFlyoutBody,
  EuiFlyoutHeader,
  EuiSkeletonText,
  useEuiTheme,
} from '@elastic/eui';
import { FormattedMessage } from '@kbn/i18n-react';
import { i18n } from '@kbn/i18n';
import type { DataTableRecord } from '@kbn/discover-utils';
import { getFieldValue } from '@kbn/discover-utils';
import { useRuleWithFallback } from '../../detection_engine/rule_management/logic/use_rule_with_fallback';
import { ToolsFlyoutHeader } from '../shared/components/tools_flyout_header';
import { InvestigationGuideView } from './components/investigation_guide_view';

export interface InvestigationGuideProps {
  /**
   * Alert/event document
   */
  hit: DataTableRecord;
}

const TITLE = i18n.translate('xpack.securitySolution.flyout.investigationGuide.title', {
  defaultMessage: 'Investigation guide',
});

/**
 * Displays the investigation guide for a given alert/event document. The investigation guide is retrieved from the rule note field, so it will only be displayed if the alert/event was generated by a detection rule with a note.
 * This component is meant to be used in a tools flyout, with the new EUI flyout system.
 */
export const InvestigationGuide = memo(({ hit }: InvestigationGuideProps) => {
  const { euiTheme } = useEuiTheme();
  const isAlert = useMemo(
    () => Boolean(getFieldValue(hit, 'kibana.alert.rule.uuid') as string),
    [hit]
  );

  const ruleId = useMemo(
    () =>
      (isAlert
        ? (getFieldValue(hit, 'kibana.alert.rule.uuid') as string)
        : (getFieldValue(hit, 'signal.rule.id') as string)) ?? '',
    [hit, isAlert]
  );

  const { loading, error, rule } = useRuleWithFallback(ruleId);
  const ruleNote = rule?.note;

  return (
    <>
      <EuiFlyoutHeader
        hasBorder
        css={css`
          padding-block-end: ${euiTheme.size.m} !important;
        `}
      >
        <ToolsFlyoutHeader hit={hit} title={TITLE} />
      </EuiFlyoutHeader>
      <EuiFlyoutBody>
        {loading ? (
          <EuiSkeletonText
            contentAriaLabel={i18n.translate(
              'xpack.securitySolution.flyout.document.investigationGuide.loadingAriaLabel',
              { defaultMessage: 'investigation guide' }
            )}
          />
        ) : !error && ruleId && ruleNote ? (
          <InvestigationGuideView hit={hit} investigationGuide={ruleNote} />
        ) : (
          <EuiCallOut
            announceOnMount
            iconType="documentation"
            size="s"
            title={
              <FormattedMessage
                id="xpack.securitySolution.flyout.document.investigationGuide.noDataTitle"
                defaultMessage="Investigation guide"
              />
            }
            aria-label={i18n.translate(
              'xpack.securitySolution.flyout.document.investigationGuide.noDataAriaLabel',
              { defaultMessage: 'Investigation guide' }
            )}
          >
            <FormattedMessage
              id="xpack.securitySolution.flyout.document.investigationGuide.noDataDescription"
              defaultMessage="There's no investigation guide for this rule."
            />
          </EuiCallOut>
        )}
      </EuiFlyoutBody>
    </>
  );
});

InvestigationGuide.displayName = 'InvestigationGuide';
