/**
 * Generates individual Cypress spec files from parameterized test suites.
 *
 * Instead of manually maintaining one .cy.ts file per (artifact × siem_version)
 * combination, this script auto-generates them from a declarative config.
 * Developers maintain only the runner (test logic) and fixture (test data);
 * this script produces the thin wrappers that Cypress discovers and distributes
 * across parallel CI agents.
 *
 * Usage:
 *   node scripts/generate_cypress_split_specs.js          # generate all
 *   node scripts/generate_cypress_split_specs.js --dry-run # preview without writing
 *   node scripts/generate_cypress_split_specs.js --clean   # delete generated files
 */

const fs = require('fs');
const path = require('path');

const CYPRESS_ROOT = path.resolve(__dirname, '..');
const DRY_RUN = process.argv.includes('--dry-run');
const CLEAN = process.argv.includes('--clean');

const LICENSE_HEADER = `/*
 * Copyright Elasticsearch B.V. and/or licensed to Elasticsearch B.V. under one
 * or more contributor license agreements. Licensed under the Elastic License
 * 2.0; you may not use this file except in compliance with the Elastic License
 * 2.0.
 */`;

const GENERATED_BANNER = `// THIS FILE IS AUTO-GENERATED by scripts/generate_cypress_split_specs.js
// DO NOT EDIT MANUALLY — changes will be overwritten on CI.
// To regenerate: node public/management/cypress/scripts/generate_cypress_split_specs.js`;

// ---------------------------------------------------------------------------
// Artifact types — source of truth for all artifact-based splits
// ---------------------------------------------------------------------------

const ARTIFACTS = [
  { id: 'trustedApps', title: 'Trusted applications', slug: 'trusted_apps' },
  { id: 'eventFilters', title: 'Event Filters', slug: 'event_filters' },
  { id: 'blocklists', title: 'Blocklist', slug: 'blocklist' },
  {
    id: 'hostIsolationExceptions',
    title: 'Host isolation exceptions',
    slug: 'host_isolation_exceptions',
  },
  {
    id: 'trustedDevices',
    title: 'Trusted devices',
    slug: 'trusted_devices',
    firstSiemVersion: 'siemV3',
  },
  {
    id: 'endpointExceptions',
    title: 'Endpoint exceptions',
    slug: 'endpoint_exceptions',
    firstSiemVersion: 'siemV4',
    experimentalFlags: ['endpointExceptionsMovedUnderManagement'],
  },
];

// Ordered list — a new version should be appended here and in common/constants.ts
const SIEM_VERSIONS = ['siem', 'siemV2', 'siemV3', 'siemV4', 'siemV5'];

// On serverless, the RBAC runner ignores siemVersionFilter and always tests
// SECURITY_FEATURE_ID (the latest version). Only the latest-version RBAC spec
// needs to run on serverless; older versions would just redundantly re-run the
// same siemV5 tests.
const LATEST_SIEM_VERSION = SIEM_VERSIONS[SIEM_VERSIONS.length - 1];

function siemVersionIndex(version) {
  const idx = SIEM_VERSIONS.indexOf(version);
  return idx === -1 ? 0 : idx;
}

// ---------------------------------------------------------------------------
// Split configurations
// ---------------------------------------------------------------------------

const SPLIT_CONFIGS = [
  // 1. Artifact tabs in policy details — one file per artifact type
  {
    outputDir: 'e2e/artifacts',
    specs: ARTIFACTS.map((artifact) => ({
      filename: `artifact_tabs_in_policy_details_${artifact.slug}.cy.ts`,
      content: [
        LICENSE_HEADER,
        '',
        GENERATED_BANNER,
        '',
        "import { getArtifactsListTestDataForArtifact } from '../../fixtures/artifacts_page';",
        "import { getArtifactTabsTests } from '../../support/artifact_tabs_in_policy_details_runner';",
        '',
        'describe(',
        `  'Artifact tabs in Policy Details - ${artifact.title}',`,
        '  {',
        '    env: {',
        '      ftrConfig: {',
        '        kbnServerArgs: [',
        '          `--xpack.securitySolution.enableExperimental=${JSON.stringify([',
        "            'endpointExceptionsMovedUnderManagement',",
        '          ])}`,',
        '        ],',
        '      },',
        '    },',
        "    tags: ['@ess', '@serverless', '@skipInServerlessMKI'],",
        '  },',
        `  getArtifactTabsTests([getArtifactsListTestDataForArtifact('${artifact.id}')])`,
        ');',
        '',
      ].join('\n'),
    })),
  },

  // 2. RBAC tests — one file per (artifact × siem_version) combination
  {
    outputDir: 'e2e/artifacts',
    specs: ARTIFACTS.flatMap((artifact) => {
      const firstIdx = siemVersionIndex(artifact.firstSiemVersion || 'siem');
      return SIEM_VERSIONS.filter((_, i) => i >= firstIdx).map((version) => {
        // 'siem' → '', 'siemV2' → '_v2', 'siemV3' → '_v3', etc.
        const versionNumber = version.replace('siem', '').replace('V', '');
        const filenameSuffix = version === 'siem' ? '' : `_v${versionNumber}`;
        const filename = `${artifact.slug}_rbac_siem${filenameSuffix}.cy.ts`;

        const hasExperimentalFlags =
          artifact.experimentalFlags && artifact.experimentalFlags.length > 0;

        // On serverless, the RBAC runner always tests SECURITY_FEATURE_ID (latest
        // version) regardless of siemVersionFilter. Non-latest RBAC specs would
        // redundantly re-run the same tests, so we skip them on serverless.
        const isLatestVersion = version === LATEST_SIEM_VERSION;
        const tags = isLatestVersion
          ? "['@ess', '@serverless', '@skipInServerlessMKI']"
          : "['@ess', '@skipInServerlessMKI']";

        const describeConfig = hasExperimentalFlags
          ? [
              '  {',
              '    env: {',
              '      ftrConfig: {',
              '        kbnServerArgs: [',
              `          \`--xpack.securitySolution.enableExperimental=\${JSON.stringify([`,
              ...artifact.experimentalFlags.map((flag) => `            '${flag}',`),
              '          ])}`,',
              '        ],',
              '      },',
              '    },',
              `    tags: ${tags},`,
              '  },',
            ]
          : [`  { tags: ${tags} },`];

        return {
          filename,
          content: [
            LICENSE_HEADER,
            '',
            GENERATED_BANNER,
            '',
            "import { getArtifactsListTestDataForArtifact } from '../../fixtures/artifacts_page';",
            "import { getArtifactMockedDataTests } from '../../support/artifacts_rbac_runner';",
            '',
            'describe(',
            `  '${artifact.title} RBAC (${version})',`,
            ...describeConfig,
            `  getArtifactMockedDataTests(getArtifactsListTestDataForArtifact('${artifact.id}'), {`,
            `    siemVersionFilter: (versions) => versions.filter((v) => v === '${version}'),`,
            '  })',
            ');',
            '',
          ].join('\n'),
        };
      });
    }),
  },
];

// ---------------------------------------------------------------------------
// Execution
// ---------------------------------------------------------------------------

let totalGenerated = 0;
let totalDeleted = 0;

for (const config of SPLIT_CONFIGS) {
  const outputDir = path.resolve(CYPRESS_ROOT, config.outputDir);

  for (const spec of config.specs) {
    const filePath = path.join(outputDir, spec.filename);
    const relativePath = path.relative(CYPRESS_ROOT, filePath);

    if (CLEAN) {
      if (fs.existsSync(filePath)) {
        if (DRY_RUN) {
          console.log(`[dry-run] Would delete: ${relativePath}`);
        } else {
          fs.unlinkSync(filePath);
          console.log(`Deleted: ${relativePath}`);
        }
        totalDeleted++;
      }
    } else {
      if (DRY_RUN) {
        console.log(`[dry-run] Would generate: ${relativePath}`);
      } else {
        fs.mkdirSync(outputDir, { recursive: true });
        fs.writeFileSync(filePath, spec.content, 'utf8');
        console.log(`Generated: ${relativePath}`);
      }
      totalGenerated++;
    }
  }
}

if (CLEAN) {
  console.log(`\n${DRY_RUN ? 'Would delete' : 'Deleted'} ${totalDeleted} file(s).`);
} else {
  console.log(`\n${DRY_RUN ? 'Would generate' : 'Generated'} ${totalGenerated} file(s).`);
}
