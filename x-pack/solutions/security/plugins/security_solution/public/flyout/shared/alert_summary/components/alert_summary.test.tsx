import React from 'react';
import { fireEvent, render, screen } from '@testing-library/react';
import {
  ALERT_SUMMARY_TEST_ID,
  AlertSummary,
  COPY_INSIGHTS_BUTTON_TEST_ID,
  GENERATE_INSIGHTS_BUTTON_TEST_ID,
  REGENERATE_INSIGHTS_BUTTON_TEST_ID,
} from './alert_summary';
import type { PromptContext } from '@kbn/elastic-assistant';
import { useAlertSummary } from '../hooks/use_alert_summary';

jest.mock('../hooks/use_alert_summary');
jest.mock('@kbn/security-solution-navigation', () => ({
  useNavigateTo: () => ({ navigateTo: jest.fn() }),
}));

const promptContext: PromptContext = {
  category: 'alert',
  description: 'Alert summary',
  getPromptContext: jest
    .fn()
    .mockResolvedValue('{ host.name: "test-host", more.data: 123, "user.name": "test-user"}'),
  id: '_promptContextId',
  suggestedUserPrompt: '_suggestedUserPrompt',
  tooltip: '_tooltip',
  replacements: { 'host.name': '12345' },
};
const defaultProps = {
  alertId: 'test-alert-id',
  canSeeAdvancedSettings: true,
  defaultConnectorId: 'test-connector-id',
  promptContext,
  setHasAlertSummary: jest.fn(),
  showAnonymizedValues: false,
};

const baseHookReturn = {
  alertSummary: '',
  generatedAt: null as number | null,
  recommendedActions: '',
  hasAlertSummary: false,
  fetchAISummary: jest.fn(),
  isConnectorMissing: false,
  isLoading: false,
  messageAndReplacements: {
    message: '',
    replacements: {},
  },
};

describe('AlertSummary', () => {
  beforeEach(() => {
    jest.clearAllMocks();
    (useAlertSummary as jest.Mock).mockReturnValue(baseHookReturn);
  });

  it('renders the loading panel when `isLoading` is true', () => {
    (useAlertSummary as jest.Mock).mockReturnValue({
      ...baseHookReturn,
      hasAlertSummary: true,
      isLoading: true,
    });

    render(<AlertSummary {...defaultProps} />);

    expect(screen.getByTestId(ALERT_SUMMARY_TEST_ID)).toHaveTextContent(
      'Generating AI summary and recommended actions...'
    );
  });

  it('renders the alert summary, recommended actions, footer timestamp, and footer icons when `hasAlertSummary` is true', () => {
    (useAlertSummary as jest.Mock).mockReturnValue({
      ...baseHookReturn,
      alertSummary: 'Test alert summary',
      recommendedActions: 'Test recommended actions',
      generatedAt: Date.parse('2024-01-15T10:00:00Z'),
      hasAlertSummary: true,
    });

    render(<AlertSummary {...defaultProps} />);

    expect(screen.getByText('Test alert summary')).toBeInTheDocument();
    expect(screen.getByRole('heading', { name: 'Recommended actions' })).toBeInTheDocument();
    expect(screen.getByText('Test recommended actions')).toBeInTheDocument();
    expect(screen.getByText(/Generated by AI on/)).toBeInTheDocument();
    expect(screen.getByTestId(REGENERATE_INSIGHTS_BUTTON_TEST_ID)).toBeInTheDocument();
    expect(screen.getByTestId(COPY_INSIGHTS_BUTTON_TEST_ID)).toBeInTheDocument();
  });

  it('omits the footer timestamp when `generatedAt` is null', () => {
    (useAlertSummary as jest.Mock).mockReturnValue({
      ...baseHookReturn,
      alertSummary: 'Test alert summary',
      recommendedActions: '',
      hasAlertSummary: true,
      generatedAt: null,
    });

    render(<AlertSummary {...defaultProps} />);

    expect(screen.queryByText(/Generated by AI on/)).not.toBeInTheDocument();
  });

  it('renders the pre-generation panel with description and Generate button when `hasAlertSummary` is false', () => {
    const fetchAISummary = jest.fn();
    (useAlertSummary as jest.Mock).mockReturnValue({
      ...baseHookReturn,
      hasAlertSummary: false,
      fetchAISummary,
    });

    render(<AlertSummary {...defaultProps} />);

    expect(
      screen.getByText(
        'Create AI summary of the alert to better understand its key characteristics and see recommended actions.'
      )
    ).toBeInTheDocument();

    fireEvent.click(screen.getByTestId(GENERATE_INSIGHTS_BUTTON_TEST_ID));
    expect(fetchAISummary).toHaveBeenCalled();
  });

  it('clicking the footer regenerate icon triggers a refetch when `hasAlertSummary` is true', () => {
    const fetchAISummary = jest.fn();
    (useAlertSummary as jest.Mock).mockReturnValue({
      ...baseHookReturn,
      alertSummary: 'Test alert summary',
      recommendedActions: 'Test recommended actions',
      hasAlertSummary: true,
      fetchAISummary,
    });

    render(<AlertSummary {...defaultProps} />);

    fireEvent.click(screen.getByTestId(REGENERATE_INSIGHTS_BUTTON_TEST_ID));
    expect(fetchAISummary).toHaveBeenCalled();
  });

  it('renders the connector missing callout when no defaultConnectorId is provided', () => {
    render(<AlertSummary {...defaultProps} defaultConnectorId={undefined} />);

    expect(screen.queryByTestId(ALERT_SUMMARY_TEST_ID)).not.toBeInTheDocument();
    expect(screen.queryByTestId(GENERATE_INSIGHTS_BUTTON_TEST_ID)).not.toBeInTheDocument();
  });
});
