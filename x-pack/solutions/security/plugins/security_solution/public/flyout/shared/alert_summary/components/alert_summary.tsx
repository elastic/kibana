import React, { memo, useEffect, useMemo } from 'react';
import {
  EuiButtonIcon,
  EuiCopy,
  EuiFlexGroup,
  EuiFlexItem,
  EuiHorizontalRule,
  EuiIcon,
  EuiMarkdownFormat,
  EuiPanel,
  EuiSkeletonText,
  EuiSpacer,
  EuiText,
  EuiTitle,
} from '@elastic/eui';
import type { PromptContext } from '@kbn/elastic-assistant';
import { AiButton } from '@kbn/shared-ux-ai-components';
import { i18n } from '@kbn/i18n';
import moment from 'moment';
import { ConnectorMissingCallout } from './connector_missing_callout';
import { useAlertSummary } from '../hooks/use_alert_summary';

export const ALERT_SUMMARY_TEST_ID = 'alert-flyout-ai-summary';
export const GENERATE_INSIGHTS_BUTTON_TEST_ID = 'alert-flyout-ai-summary-generate-insights-button';
export const REGENERATE_INSIGHTS_BUTTON_TEST_ID =
  'alert-flyout-ai-summary-regenerate-insights-button';
export const COPY_INSIGHTS_BUTTON_TEST_ID = 'alert-flyout-ai-summary-copy-insights-button';

const RECOMMENDED_ACTIONS = i18n.translate(
  'xpack.securitySolution.alertSummary.recommendedActions',
  {
    defaultMessage: 'Recommended actions',
  }
);
const GENERATING = i18n.translate('xpack.securitySolution.alertSummary.generating', {
  defaultMessage: 'Generating AI summary and recommended actions...',
});
const GENERATE = i18n.translate('xpack.securitySolution.alertSummary.generate', {
  defaultMessage: 'Generate',
});
const GENERATE_DESCRIPTION = i18n.translate(
  'xpack.securitySolution.alertSummary.generateDescription',
  {
    defaultMessage:
      'Create AI summary of the alert to better understand its key characteristics and see recommended actions.',
  }
);
const REGENERATE_ARIA_LABEL = i18n.translate(
  'xpack.securitySolution.alertSummary.regenerateAriaLabel',
  {
    defaultMessage: 'Regenerate summary',
  }
);
const COPY_ARIA_LABEL = i18n.translate('xpack.securitySolution.alertSummary.copyAriaLabel', {
  defaultMessage: 'Copy summary',
});
const generatedAtLabel = (timestamp: string) =>
  i18n.translate('xpack.securitySolution.alertSummary.generatedAt', {
    defaultMessage: 'Generated by AI on {timestamp}',
    values: { timestamp },
  });

export interface AlertSummaryProps {
  /**
   * Id of the alert for which we will generate the summary
   */
  alertId: string;
  /**
   * Value of useKibana.services.application.capabilities.management.kibana.settings
   */
  canSeeAdvancedSettings: boolean;
  /**
   * Value of securitySolution:defaultAIConnector
   */
  defaultConnectorId: string | undefined;
  /**
   * The context for the prompt
   */
  promptContext: PromptContext;
  /**
   * Callback to set the value to the parent to be able to control the menu options
   */
  setHasAlertSummary: React.Dispatch<React.SetStateAction<boolean>>;
  /**
   * If true we'll show anonymized values
   */
  showAnonymizedValues?: boolean;
}

/**
 * Joins the markdown summary and recommended-actions strings into a single
 * plain-text blob suitable for `EuiCopy`. Mirrors the helper of the same
 * name in the entity-summary section so the copy UX matches.
 */
const formatTextToCopy = (summary: string, recommendedActions: string | undefined): string => {
  const parts = [summary.trim()];
  if (recommendedActions && recommendedActions.trim().length > 0) {
    parts.push(`\n${RECOMMENDED_ACTIONS}:\n${recommendedActions.trim()}`);
  }
  return parts.join('\n');
};

/**
 * Body of the AI summary section. Visually mirrors the "Entity summary"
 * section (`EntityHighlightsAccordion` + `EntityHighlightsResult`):
 *
 * - Pre-generation: bordered panel with description + AI button.
 * - Loading: bordered panel with loading text + skeleton.
 * - Generated: bordered panel with summary markdown, divider, recommended
 *   actions (icon + h4 header + markdown), divider, and a footer row with
 *   the generation timestamp on the left and refresh + copy icon buttons
 *   on the right.
 *
 * The single deviation from the entity section is the Generate button:
 * we use `AiButton` (the new AI-styled button) instead of the entity's
 * plain `EuiButton`. Falls back to a connector-missing callout if no
 * default AI connector is configured.
 *
 * Shared across the EASE flyout, the legacy expandable flyout, and the v2
 * flyout. Stateless w.r.t. flyout context — all required values are
 * passed in as props by the host wrapper.
 */
export const AlertSummary = memo(
  ({
    alertId,
    canSeeAdvancedSettings,
    defaultConnectorId,
    promptContext,
    setHasAlertSummary,
    showAnonymizedValues,
  }: AlertSummaryProps) => {
    const {
      alertSummary,
      generatedAt,
      recommendedActions,
      hasAlertSummary,
      fetchAISummary,
      isConnectorMissing,
      isLoading,
      messageAndReplacements,
    } = useAlertSummary({
      alertId,
      defaultConnectorId: defaultConnectorId || '',
      promptContext,
      showAnonymizedValues,
    });

    useEffect(() => {
      setHasAlertSummary(hasAlertSummary);
    }, [hasAlertSummary, setHasAlertSummary]);

    const textToCopy = useMemo(
      () => formatTextToCopy(alertSummary, recommendedActions),
      [alertSummary, recommendedActions]
    );

    if (!defaultConnectorId) {
      return <ConnectorMissingCallout canSeeAdvancedSettings={canSeeAdvancedSettings} />;
    }

    if (!hasAlertSummary) {
      return (
        <EuiPanel hasBorder={true}>
          <EuiFlexGroup justifyContent="spaceBetween" alignItems="center" gutterSize="s">
            <EuiFlexItem grow={4}>
              <EuiText size="xs" textAlign="left">
                {GENERATE_DESCRIPTION}
              </EuiText>
            </EuiFlexItem>
            <EuiFlexItem grow={false}>
              <AiButton
                onClick={fetchAISummary}
                size="m"
                iconType="aiAssistantLogo"
                data-test-subj={GENERATE_INSIGHTS_BUTTON_TEST_ID}
                isLoading={messageAndReplacements == null}
              >
                {GENERATE}
              </AiButton>
            </EuiFlexItem>
          </EuiFlexGroup>
        </EuiPanel>
      );
    }

    if (isLoading) {
      return (
        <EuiPanel hasBorder={true}>
          <EuiText size="xs" color="subdued" data-test-subj={ALERT_SUMMARY_TEST_ID}>
            {GENERATING}
          </EuiText>
          <EuiSpacer size="xs" />
          <EuiSkeletonText lines={2} size="xs" />
        </EuiPanel>
      );
    }

    return (
      <>
        {isConnectorMissing && (
          <>
            <ConnectorMissingCallout canSeeAdvancedSettings={canSeeAdvancedSettings} />
            <EuiSpacer size="m" />
          </>
        )}
        <EuiPanel hasBorder={true}>
          <EuiMarkdownFormat textSize="xs">{alertSummary}</EuiMarkdownFormat>
          {recommendedActions && (
            <>
              <EuiHorizontalRule margin="m" />
              <EuiFlexGroup alignItems="center" gutterSize="xs">
                <EuiFlexItem grow={false}>
                  <EuiIcon type="documentation" size="m" aria-hidden={true} />
                </EuiFlexItem>
                <EuiFlexItem grow={false}>
                  <EuiTitle size="xxs">
                    <h4>{RECOMMENDED_ACTIONS}</h4>
                  </EuiTitle>
                </EuiFlexItem>
              </EuiFlexGroup>
              <EuiSpacer size="s" />
              <EuiMarkdownFormat textSize="xs">{recommendedActions}</EuiMarkdownFormat>
            </>
          )}
          <EuiSpacer size="xs" />
          <EuiHorizontalRule margin="m" />
          <EuiFlexGroup alignItems="center" justifyContent="spaceBetween" responsive={false}>
            <EuiFlexItem grow={false}>
              {generatedAt != null && (
                <EuiText size="xs" color="subdued">
                  {generatedAtLabel(moment(generatedAt).format('MMM DD, YYYY [at] HH:mm'))}
                </EuiText>
              )}
            </EuiFlexItem>
            <EuiFlexItem grow={false}>
              <EuiFlexGroup gutterSize="xs" responsive={false}>
                <EuiFlexItem grow={false}>
                  <EuiButtonIcon
                    iconType="refresh"
                    aria-label={REGENERATE_ARIA_LABEL}
                    onClick={fetchAISummary}
                    isDisabled={messageAndReplacements == null}
                    size="xs"
                    data-test-subj={REGENERATE_INSIGHTS_BUTTON_TEST_ID}
                  />
                </EuiFlexItem>
                <EuiFlexItem grow={false}>
                  <EuiCopy textToCopy={textToCopy}>
                    {(copy) => (
                      <EuiButtonIcon
                        iconType="copy"
                        aria-label={COPY_ARIA_LABEL}
                        onClick={copy}
                        size="xs"
                        data-test-subj={COPY_INSIGHTS_BUTTON_TEST_ID}
                      />
                    )}
                  </EuiCopy>
                </EuiFlexItem>
              </EuiFlexGroup>
            </EuiFlexItem>
          </EuiFlexGroup>
        </EuiPanel>
      </>
    );
  }
);

AlertSummary.displayName = 'AlertSummary';
