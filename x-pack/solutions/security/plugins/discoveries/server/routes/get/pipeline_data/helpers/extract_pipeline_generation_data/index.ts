import type { WorkflowExecutionDto } from '@kbn/workflows';

import { GenerateStepTypeId } from '../../../../../../common/step_types/generate_step';

/**
 * Represents a single attack discovery output from the generation step.
 * All fields are snake_case (Elasticsearch API convention).
 */
export interface AttackDiscoveryOutput {
  /** The alert IDs that the attack discovery is based on */
  alert_ids: string[];
  /** Details of the attack with bulleted markdown */
  details_markdown: string;
  /** A short summary featuring host.name and user.name fields */
  entity_summary_markdown?: string;
  /** UUID of attack discovery */
  id?: string;
  /** An array of MITRE ATT&CK tactics */
  mitre_attack_tactics?: string[];
  /** A markdown summary of the attack discovery */
  summary_markdown: string;
  /** The time the attack discovery was generated */
  timestamp?: string;
  /** A title for the attack discovery, in plain text */
  title: string;
}

/**
 * Data extracted from the generation step of a generation workflow execution.
 * All fields are snake_case.
 */
export interface PipelineGenerationData {
  /** The attack discoveries generated by the pipeline */
  attack_discoveries: AttackDiscoveryOutput[];
  /** Unique identifier for this generation execution */
  execution_uuid: string;
  /** Map of anonymized values to original values */
  replacements: Record<string, string>;
}

/**
 * Shape of the raw output stored in the generate step's `output` field.
 * The workflow engine serialises output as `JsonValue`, so we cast to
 * this narrower type after the null-guard.
 */
interface GenerateStepRawOutput {
  attack_discoveries?: AttackDiscoveryOutput[];
  execution_uuid?: string;
  replacements?: Record<string, string> | string;
}

/**
 * Parses the `replacements` value from the generate step output.
 *
 * Replacements may arrive as:
 * - a `Record<string, string>` (already an object)
 * - a JSON-encoded string (needs `JSON.parse`)
 * - `undefined` / missing (returns empty object)
 *
 * If the string cannot be parsed, an empty object is returned.
 */
const parseReplacements = (
  raw: Record<string, string> | string | undefined
): Record<string, string> => {
  if (raw == null) {
    return {};
  }

  if (typeof raw === 'string') {
    try {
      return JSON.parse(raw) as Record<string, string>;
    } catch {
      return {};
    }
  }

  return raw;
};

/**
 * Extracts generation output (attack discoveries, execution_uuid, replacements)
 * from a generation workflow execution by finding the
 * `attack-discovery.generate` step and reading its output.
 *
 * Returns `null` if the generation execution is not available or
 * the generate step is not found / has no output.
 */
export const extractPipelineGenerationData = ({
  generationExecution,
}: {
  generationExecution: WorkflowExecutionDto | null;
}): PipelineGenerationData | null => {
  if (generationExecution == null) {
    return null;
  }

  const generateStep = generationExecution.stepExecutions.find(
    (step) => step.stepType === GenerateStepTypeId
  );

  if (generateStep == null || generateStep.output == null) {
    return null;
  }

  const output = generateStep.output as GenerateStepRawOutput;

  return {
    attack_discoveries: output.attack_discoveries ?? [],
    execution_uuid: output.execution_uuid ?? '',
    replacements: parseReplacements(output.replacements),
  };
};
