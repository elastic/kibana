import { test, expect, tags } from '../../../fixtures';
import { createRule } from '../../../common/api_helpers';
import { SECURITY_ARCHIVES } from '../../../common/es_helpers';
import { OVERVIEW_URL } from '../../../common/urls';

test.describe('Alerts generated by building block rules', { tag: tags.deploymentAgnostic }, () => {
  test.beforeAll(async ({ esArchiver }) => {
    await esArchiver.loadIfNeeded(SECURITY_ARCHIVES.AUDITBEAT_MULTIPLE);
  });

  test.afterAll(async ({ esArchiver }) => {
    // no-op: Scout EsArchiverFixture does not support unload;
  });

  test.beforeEach(async ({ browserAuth, apiServices }) => {
    await browserAuth.loginAsAdmin();

    const buildingBlockRule = {
      type: 'query' as const,
      query: 'host.name: *',
      index: ['auditbeat-*'],
      name: 'Building Block Rule',
      description: 'A building block rule',
      severity: 'low' as const,
      risk_score: 5,
      tags: ['test'],
      references: [],
      false_positives: [],
      threat: [],
      interval: '100m',
      from: '1900-01-01T00:00:00.000Z',
      building_block_type: 'default',
    };
    await createRule(apiServices, buildingBlockRule);
  });

  test('Alerts should be visible on Rule Detail page and not on Overview page', async ({
    page,
  }) => {
    await test.step('Verify alerts on Rule Details page', async () => {
      await page.testSubj.locator('expand-event').waitFor({ state: 'visible', timeout: 120_000 });

      const highlightedRows = page.locator('.euiDataGridRow--highlighted');
      await expect(highlightedRows).toBeVisible();
    });

    await test.step('Verify no alerts on Overview page', async () => {
      await page.goto(OVERVIEW_URL);
      const emptyPrompt = page.testSubj.locator('euiEmptyPrompt');
      await expect(emptyPrompt).toContainText('No results found');
    });
  });
});
