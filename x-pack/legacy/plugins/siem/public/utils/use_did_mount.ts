import { useEffect, useRef } from 'react';

export const useDidMountEffect = (fn: () => void, inputs: unknown[]) => {
  const didMountRef = useRef(false);

  useEffect(() => {
    if (didMountRef.current) fn();
    else didMountRef.current = true;
  }, inputs);
};
