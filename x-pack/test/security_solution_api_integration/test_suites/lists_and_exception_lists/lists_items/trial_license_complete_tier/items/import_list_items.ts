import expect from '@kbn/expect';
import type { ListSchema, ListItemSchema } from '@kbn/securitysolution-io-ts-list-types';
import { LIST_ITEM_URL } from '@kbn/securitysolution-list-constants';
import { getListItemResponseMockWithoutAutoGeneratedValues } from '@kbn/lists-plugin/common/schemas/response/list_item_schema.mock';
import { getListResponseMockWithoutAutoGeneratedValues } from '@kbn/lists-plugin/common/schemas/response/list_schema.mock';
import { getImportListItemAsBuffer } from '@kbn/lists-plugin/common/schemas/request/import_list_item_schema.mock';

import {
  createListsIndex,
  deleteListsIndex,
  removeListServerGeneratedProperties,
  removeListItemServerGeneratedProperties,
  waitFor,
} from '../../../utils';
import { FtrProviderContext } from '../../../../../ftr_provider_context';

export default ({ getService }: FtrProviderContext): void => {
  const supertest = getService('supertest');
  const log = getService('log');
  const utils = getService('securitySolutionUtils');

  describe('@ess @serverless import_list_items', () => {
    describe('importing list items without an index', () => {
      it('should not import a list item if the index does not exist yet', async () => {
        const { body } = await supertest
          .post(`${LIST_ITEM_URL}/_import?type=ip`)
          .set('kbn-xsrf', 'true')
          .attach('file', getImportListItemAsBuffer(['127.0.0.1', '127.0.0.2']), 'list_items.txt')
          .expect('Content-Type', 'application/json; charset=utf-8')
          .expect(400);

        expect(body).to.eql({
          status_code: 400,
          message:
            'To import a list item, the data steam must exist first. Data stream ".lists-default" does not exist',
        });
      });
    });

    describe('importing lists with an index', () => {
      beforeEach(async () => {
        await createListsIndex(supertest, log);
      });

      afterEach(async () => {
        await deleteListsIndex(supertest, log);
      });

      it('should not import a list item if the imported file is not .txt or .csv', async () => {
        const { body } = await supertest
          .post(`${LIST_ITEM_URL}/_import?type=ip`)
          .set('kbn-xsrf', 'true')
          .attach('file', getImportListItemAsBuffer(['127.0.0.1', '127.0.0.2']), 'list_items.pdf')
          .expect('Content-Type', 'application/json; charset=utf-8')
          .expect(415);

        expect(body).to.eql({
          status_code: 415,
          message: 'Unsupported media type. File must be one of the following types: [.csv, .txt]',
        });
      });

      it('should not import a list item if the imported file exceed the file size limit', async () => {
        const { body } = await supertest
          .post(`${LIST_ITEM_URL}/_import?type=ip`)
          .set('kbn-xsrf', 'true')
          .attach(
            'file',
            getImportListItemAsBuffer(Array(1000000).fill('127.0.0.1')),
            'list_items.txt'
          )
          .expect('Content-Type', 'application/json; charset=utf-8')
          .expect(413);

        expect(body).to.eql({
          statusCode: 413,
          error: 'Request Entity Too Large',
          message: 'Payload content length greater than maximum allowed: 9000000',
        });
      });

      it('should set the response content types to be expected when importing two items', async () => {
        await supertest
          .post(`${LIST_ITEM_URL}/_import?type=ip`)
          .set('kbn-xsrf', 'true')
          .attach('file', getImportListItemAsBuffer(['127.0.0.1', '127.0.0.2']), 'list_items.txt')
          .expect('Content-Type', 'application/json; charset=utf-8')
          .expect(200);
      });

      it('should report that it imported a simple list successfully', async () => {
        const { body } = await supertest
          .post(`${LIST_ITEM_URL}/_import?type=ip`)
          .set('kbn-xsrf', 'true')
          .attach('file', getImportListItemAsBuffer(['127.0.0.1', '127.0.0.2']), 'list_items.txt')
          .expect('Content-Type', 'application/json; charset=utf-8')
          .expect(200);

        const bodyToCompare = removeListServerGeneratedProperties(body);
        const outputtedList: Partial<ListSchema> = {
          ...getListResponseMockWithoutAutoGeneratedValues(await utils.getUsername()),
          name: 'list_items.txt',
          description: 'File uploaded from file system of list_items.txt',
        };
        expect(bodyToCompare).to.eql(outputtedList);
      });

      it('should be able to read imported list items back out correctly', async () => {
        await supertest
          .post(`${LIST_ITEM_URL}/_import?type=ip`)
          .set('kbn-xsrf', 'true')
          .attach('file', getImportListItemAsBuffer(['127.0.0.1', '127.0.0.2']), 'list_items.txt')
          .expect(200);

        // Although we try to be aggressive with waitFor in the lists code base, there is still not guarantees
        // that we will have the data just yet so we have to do a waitFor here for when it shows up
        await waitFor(
          async () => {
            const { status } = await supertest
              .get(`${LIST_ITEM_URL}?list_id=list_items.txt&value=127.0.0.1`)
              .send();
            return status !== 404;
          },
          `${LIST_ITEM_URL}?list_id=list_items.txt&value=127.0.0.1`,
          log
        );
        const { body } = await supertest
          .get(`${LIST_ITEM_URL}?list_id=list_items.txt&value=127.0.0.1`)
          .send()
          .expect(200);

        const bodyToCompare = removeListItemServerGeneratedProperties(body[0]);
        const outputtedList: Partial<ListItemSchema> = {
          ...getListItemResponseMockWithoutAutoGeneratedValues(await utils.getUsername()),
          list_id: 'list_items.txt',
        };
        expect(bodyToCompare).to.eql(outputtedList);
      });
    });
  });
};
