export interface BootstrapTemplateData {
  themeTagName: string;
  colorMode: 'light' | 'dark' | 'system';
  coreRemoteEntry: string;
  plugins: Array<{
    id: string;
    remoteEntry: string;
  }>;
}

/**
 * Generate the bootstrap HTML template for Module Federation
 * 
 * This replaces the legacy template that used __kbnBundles__
 */
export function generateBootstrapTemplate(data: BootstrapTemplateData): string {
  const { themeTagName, colorMode, coreRemoteEntry, plugins } = data;

  // Theme tag logic
  const themeTagScript =
    colorMode === 'system'
      ? `window.__kbnThemeTag__ = window.matchMedia('(prefers-color-scheme: dark)').matches ? '${themeTagName}dark' : '${themeTagName}light';`
      : `window.__kbnThemeTag__ = '${themeTagName}${colorMode}';`;

  // Plugin registration
  const pluginRegistrations = plugins
    .map((p) => `  __kbnMF__.registerPlugin('${p.id}', '${p.remoteEntry}');`)
    .join('\n');

  return `
// Kibana Module Federation Bootstrap
// Generated by @kbn/rspack-optimizer

(function() {
  'use strict';

  // Theme configuration
  ${themeTagScript}

  // Module Federation Runtime
  var loadedContainers = new Map();
  var loadingPromises = new Map();
  var pluginRegistry = new Map();
  var shareScope = {};

  window.__kbnMF__ = {
    registerPlugin: function(id, remoteEntry) {
      pluginRegistry.set(id, remoteEntry);
    },

    loadPlugin: async function(id) {
      var container = await this._loadContainer(id);
      var factory = await container.get('./public');
      var module = factory();
      return module.default || module.plugin || module;
    },

    loadPluginExport: async function(id, exportName) {
      var container = await this._loadContainer(id);
      var modulePath = exportName === 'public' ? './public' : './' + exportName;
      var factory = await container.get(modulePath);
      var module = factory();
      return module.default || module[exportName] || module;
    },

    isPluginLoaded: function(id) {
      return loadedContainers.has(id);
    },

    getLoadedPlugins: function() {
      return Array.from(loadedContainers.keys());
    },

    _loadContainer: async function(id) {
      if (loadedContainers.has(id)) {
        return loadedContainers.get(id);
      }

      if (loadingPromises.has(id)) {
        return loadingPromises.get(id);
      }

      var remoteEntry = pluginRegistry.get(id);
      if (!remoteEntry) {
        throw new Error('Plugin "' + id + '" is not registered');
      }

      var loadPromise = this._doLoadContainer(id, remoteEntry);
      loadingPromises.set(id, loadPromise);

      try {
        return await loadPromise;
      } finally {
        loadingPromises.delete(id);
      }
    },

    _doLoadContainer: async function(id, remoteEntry) {
      await this._loadScript(remoteEntry);

      var containerName = 'plugin_' + id.replace(/-/g, '_');
      var container = window[containerName];

      if (!container) {
        throw new Error('Failed to load container for plugin "' + id + '"');
      }

      await container.init(shareScope);
      loadedContainers.set(id, container);
      return container;
    },

    _loadScript: function(url) {
      return new Promise(function(resolve, reject) {
        var existing = document.querySelector('script[src="' + url + '"]');
        if (existing) {
          resolve();
          return;
        }

        var script = document.createElement('script');
        script.src = url;
        script.async = true;
        script.onload = resolve;
        script.onerror = function() {
          reject(new Error('Failed to load script: ' + url));
        };
        document.head.appendChild(script);
      });
    }
  };

  // Register all plugins
${pluginRegistrations}

  // Bootstrap function
  window.__kbnBootstrap__ = async function() {
    try {
      // Load core first
      var core = await __kbnMF__.loadPlugin('core');
      
      // Core will handle loading plugins in dependency order
      await core.__kbnBootstrap__();
    } catch (error) {
      console.error('Failed to bootstrap Kibana:', error);
      throw error;
    }
  };

  // Auto-bootstrap when DOM is ready
  if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', function() {
      window.__kbnBootstrap__();
    });
  } else {
    window.__kbnBootstrap__();
  }
})();
`;
}
