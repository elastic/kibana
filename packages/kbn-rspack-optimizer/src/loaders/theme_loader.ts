import type { LoaderContext } from '@rspack/core';

interface ThemeLoaderOptions {
  themeTags: string[];
}

/**
 * RSPack loader that generates a theme switcher for SCSS files
 *
 * Instead of bundling the SCSS directly, this loader generates JavaScript
 * that imports the correct theme-specific CSS based on window.__kbnThemeTag__
 */
export default function themeLoader(this: LoaderContext<ThemeLoaderOptions>): string {
  const options = this.getOptions();
  const { themeTags } = options;
  const resourcePath = this.resourcePath;

  // Generate switch statement that loads the correct theme
  const cases = themeTags.map((tag) => {
    const themedImport = `${resourcePath}?${tag}`;
    return `
    case '${tag}':
      return require(${JSON.stringify(themedImport)});`;
  });

  // Generate fallback for unknown themes
  const fallbackTag = themeTags[0]; // Use first theme as fallback
  const fallback = `
    default:
      if (typeof console !== 'undefined' && console.error) {
        console.error(
          new Error(
            'SCSS file "' + ${JSON.stringify(resourcePath)} + '" was not built for theme "' +
            window.__kbnThemeTag__ + '". Using fallback theme "${fallbackTag}".'
          )
        );
      }
      return require(${JSON.stringify(`${resourcePath}?${fallbackTag}`)});`;

  return `
// Theme switcher for ${resourcePath}
// Generated by @kbn/rspack-optimizer theme_loader
module.exports = (function() {
  switch (window.__kbnThemeTag__) {${cases.join('')}${fallback}
  }
})();
`;
}

// Mark as cacheable
export const raw = false;
