import { z } from '@kbn/zod/v4';
import type { JsonSchema } from './schema/common/json_model_shape_schema';

/**
 * Prefix for built-in workflow input JSON Schemas resolved by {@link resolveRef}
 * in `field_conversion.ts`. Workflow YAML may use:
 * `$ref: '#/kibana/definitions/<id>'` where `<id>` is a key in
 * {@link builtinWorkflowInputDefinitions}.
 *
 * The same subtree is merged into the workflow root JSON Schema used by Monaco YAML
 * so `#/kibana/definitions/<id>` resolves for validation and completion.
 */
export const KIBANA_WORKFLOW_INPUT_DEFINITION_REF_PREFIX = '#/kibana/definitions/' as const;

/**
 * Alerting v2 notification group payload dispatched to workflows.
 * Use as a workflow **input** when a step should receive the grouped notification context.
 */
const alertingV2NotificationGroup: JsonSchema = {
  type: 'object',
  title: 'Alerting v2 notification group',
  description:
    'Notification group dispatched by an alerting v2 policy: group identity, policy reference, and alert episodes.',
  properties: {
    id: {
      type: 'string',
      description: 'Unique identifier of the notification group',
    },
    policyId: {
      type: 'string',
      description: 'Identifier of the notification policy that matched and dispatched the group',
    },
    groupKey: {
      type: 'object',
      description:
        'Grouping key for the notification group. This is currently generated by the dispatcher and may vary depending on grouping behavior.',
      additionalProperties: true,
    },
    episodes: {
      type: 'array',
      description: 'Alert episodes included in this notification group',
      items: {
        type: 'object',
        properties: {
          last_event_timestamp: {
            type: 'string',
            format: 'date-time',
            description: 'Timestamp of the latest event seen for this episode',
          },
          rule_id: {
            type: 'string',
            description: 'Identifier of the rule that produced this episode',
          },
          group_hash: {
            type: 'string',
            description: 'Hash identifying the alert series/group this episode belongs to',
          },
          episode_id: {
            type: 'string',
            description: 'Unique identifier of the alert episode',
          },
          episode_status: {
            type: 'string',
            description: 'Current lifecycle status of the alert episode',
            enum: ['inactive', 'pending', 'active', 'recovering'],
          },
          data: {
            type: 'object',
            description: 'Data of the alert episode',
            additionalProperties: true,
          },
        },
        required: ['last_event_timestamp', 'rule_id', 'group_hash', 'episode_id', 'episode_status'],
        additionalProperties: false,
      },
    },
  },
  required: ['id', 'policyId', 'groupKey', 'episodes'],
  additionalProperties: false,
};

/**
 * Central registry of reusable input shapes. Keys are the `<id>` segment only
 * (e.g. `alertingV2NotificationGroup` for `#/kibana/definitions/alertingV2NotificationGroup`).
 *
 * Domain plugins may register additional entries at startup (assign into this object).
 * Note: YAML editor `z.enum` completion for `$ref` only includes keys present at bundle time;
 * runtime-only keys still resolve at execution via {@link resolveRef} if added before validate.
 */
export const builtinWorkflowInputDefinitions: Record<string, JsonSchema> = {
  alertingV2NotificationGroup,
};

const builtinRefList = Object.keys(builtinWorkflowInputDefinitions).map(
  (id) => `${KIBANA_WORKFLOW_INPUT_DEFINITION_REF_PREFIX}${id}`
);

/**
 * Literal values suggested for `$ref` under workflow `inputs` JSON Schema (Monaco YAML).
 * Kept in sync with {@link builtinWorkflowInputDefinitions} keys.
 */
export const builtinWorkflowInputDefinitionRefValuesForZod = builtinRefList as [
  string,
  ...string[]
];

/**
 * Zod schema for `$ref` under workflow inputs: suggests known built-in refs
 */
export const builtinWorkflowInputDefinitionRefSchema =
  builtinRefList.length > 0
    ? z.union([z.enum(builtinWorkflowInputDefinitionRefValuesForZod), z.string().max(512)])
    : z.string().max(512);

/**
 * Merges {@link builtinWorkflowInputDefinitions} under `kibana.definitions` on the workflow
 * root JSON Schema document so `#/kibana/definitions/<id>` resolves for Monaco YAML and other
 * schema consumers that walk the document root.
 */
export function mergeKibanaBuiltinWorkflowInputDefinitionsIntoRootSchema<T extends object>(
  root: T | null
): T | null {
  if (!root || typeof root !== 'object') {
    return root;
  }
  if (builtinRefList.length === 0) {
    return root;
  }

  const existingKibana = (root as { kibana?: Record<string, unknown> }).kibana;
  const kibanaObject =
    existingKibana && typeof existingKibana === 'object' && !Array.isArray(existingKibana)
      ? existingKibana
      : {};

  const existingDefinitions = kibanaObject.definitions;
  const definitionsObject =
    existingDefinitions &&
    typeof existingDefinitions === 'object' &&
    !Array.isArray(existingDefinitions)
      ? (existingDefinitions as Record<string, JsonSchema>)
      : {};

  return {
    ...root,
    kibana: {
      ...kibanaObject,
      // Built-ins win on key collision so canonical Kibana shapes stay authoritative for Monaco $ref.
      definitions: {
        ...definitionsObject,
        ...builtinWorkflowInputDefinitions,
      },
    },
  };
}
