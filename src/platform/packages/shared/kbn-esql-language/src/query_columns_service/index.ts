import type { ESQLCallbacks, ESQLFieldWithMetadata } from '@kbn/esql-types';
import type { ESQLAstQueryExpression } from '../..';
import { BasicPrettyPrinter, SOURCE_COMMANDS } from '../..';
import { UnmappedFieldsStrategy } from '../commands/registry/types';
import { type ESQLColumnData, type ESQLPolicy } from '../commands/registry/types';
import { getCurrentQueryAvailableColumns, getFieldsFromES } from './helpers';
import { getUnmappedFieldsStrategy } from '../commands/definitions/utils/settings';

export const NOT_SUGGESTED_TYPES = ['unsupported'];

// Helper to check if a command is a source command that should be cached
function isSourceCommand(commandName: string): boolean {
  return SOURCE_COMMANDS.has(commandName.toLowerCase());
}

/**
 * Responsible for efficiently computing the list of columns generated by a specific ESQL query.
 */
export class QueryColumns {
  private static readonly cache = new Map<string, ESQLColumnData[]>();
  // Adding a max size to the cache to prevent unbounded memory growth
  private static readonly MAX_CACHE_SIZE = 100;

  /** Strategy to handle unmapped fields
   * FAIL, not mapped columns are treated as errors.
   * LOAD, not mapped columns are loaded from _source as a keyword field if possible.
   * NULLIFY, not mapped columns are treated as null.
   */
  private unmappedFieldsStrategy: UnmappedFieldsStrategy = UnmappedFieldsStrategy.FAIL;

  /**
   * Retrieves from cache the columns for a given query, ignoring case.
   * @param query
   * @returns
   */
  static fromCache(query: string) {
    const key = query.toLowerCase();
    const result = QueryColumns.cache.get(key);
    if (result) {
      // Move it to the end to mark it as recently used
      QueryColumns.cache.delete(key);
      QueryColumns.cache.set(key, result);
    }
    return result;
  }

  /**
   * Remove from the cache the least recently used when cache is full.
   */
  static setCache(key: string, value: ESQLColumnData[]) {
    const normalizedKey = key.toLowerCase();

    if (QueryColumns.cache.has(normalizedKey)) {
      QueryColumns.cache.delete(normalizedKey);
    }

    // Remove oldest entry if cache is full
    while (QueryColumns.cache.size >= QueryColumns.MAX_CACHE_SIZE) {
      const oldestKey = QueryColumns.cache.keys().next().value;
      if (oldestKey) {
        QueryColumns.cache.delete(oldestKey);
      }
    }

    // Add new entry at the end
    QueryColumns.cache.set(normalizedKey, value);
  }

  constructor(
    private readonly query: ESQLAstQueryExpression,
    private readonly originalQueryText: string,
    private readonly resourceRetriever?: ESQLCallbacks,
    private readonly options?: {
      invalidateColumnsCache?: boolean;
    }
  ) {
    this.unmappedFieldsStrategy = getUnmappedFieldsStrategy(query.header);
  }

  /**
   * Returns columns for this query, filtered by type and optionally ignoring some names.
   */
  async byType(
    expectedType: Readonly<string> | Readonly<string[]> = 'any',
    ignored: string[] = []
  ): Promise<ESQLColumnData[]> {
    const types = Array.isArray(expectedType) ? expectedType : [expectedType];
    const fields = await this.getColumnsForQuery(this.query, this.getFields, this.getPolicies);
    return (
      fields?.filter(({ name, type }) => {
        const ts = Array.isArray(type) ? type : [type];
        return (
          !ignored.includes(name) &&
          (types[0] === 'any' || ts.some((t) => types.includes(t))) &&
          !NOT_SUGGESTED_TYPES.includes(type)
        );
      }) || []
    );
  }

  /**
   * Returns a map of column name to column metadata for this query.
   */
  async asMap(): Promise<Map<string, ESQLColumnData>> {
    return this.getColumnsAsMap();
  }

  /**
   * Internal method to get columns as map.
   */
  private async getColumnsAsMap(): Promise<Map<string, ESQLColumnData>> {
    const fields = await this.getColumnsForQuery(this.query, this.getFields, this.getPolicies);
    const result = new Map<string, ESQLColumnData>();
    for (let i = 0; i < fields.length; i++) {
      const field = fields[i];
      result.set(field.name, field);
    }
    return result;
  }

  private getFields = async (queryToES: string) => {
    if (!this.options?.invalidateColumnsCache) {
      const cached = QueryColumns.fromCache(queryToES);

      if (cached) {
        return cached as ESQLFieldWithMetadata[];
      }
    }

    const fields = await getFieldsFromES(queryToES, this.resourceRetriever);
    QueryColumns.setCache(queryToES, fields);
    return fields;
  };

  private getPolicies = async () => {
    const policies = (await this.resourceRetriever?.getPolicies?.()) ?? [];
    return new Map(policies.map((p) => [p.name, p]));
  };

  /**
   * Get fields from previous commands building up the chain.
   */
  private async getFieldsFromPreviousCommands(
    query: ESQLAstQueryExpression,
    fetchFields: (query: string) => Promise<ESQLFieldWithMetadata[]>,
    getPolicies: () => Promise<Map<string, ESQLPolicy>>
  ): Promise<ESQLColumnData[]> {
    if (query.commands.length <= 1) {
      return [];
    }

    let fields: ESQLColumnData[] = [];

    for (let i = 0; i < query.commands.length - 1; i++) {
      const subQuery = {
        ...query,
        commands: query.commands.slice(0, i + 1),
      };

      const currentCommand = query.commands[i];
      if (!currentCommand?.name) {
        continue;
      }

      const isSource = isSourceCommand(currentCommand.name);

      if (isSource) {
        // For source commands, check cache first
        const queryString = BasicPrettyPrinter.print(subQuery);
        const cachedFields = QueryColumns.fromCache(queryString);
        if (cachedFields) {
          fields = cachedFields;
          continue;
        }
      }

      // Build fields using the command's columnsAfter method
      fields = await getCurrentQueryAvailableColumns(
        subQuery.commands,
        fields,
        fetchFields,
        getPolicies,
        this.originalQueryText,
        this.unmappedFieldsStrategy
      );
    }

    return fields;
  }

  /**
   * Returns the fields for a given query.
   * @param query
   * @param fetchFields
   * @param getPolicies
   */
  private async getColumnsForQuery(
    query: ESQLAstQueryExpression,
    fetchFields: (query: string) => Promise<ESQLFieldWithMetadata[]>,
    getPolicies: () => Promise<Map<string, ESQLPolicy>>
  ) {
    const fieldsAvailableAfterPreviousCommand = await this.getFieldsFromPreviousCommands(
      query,
      fetchFields,
      getPolicies
    );

    const availableFields = await getCurrentQueryAvailableColumns(
      query.commands,
      fieldsAvailableAfterPreviousCommand,
      fetchFields,
      getPolicies,
      this.originalQueryText,
      this.unmappedFieldsStrategy
    );

    return availableFields;
  }
}
