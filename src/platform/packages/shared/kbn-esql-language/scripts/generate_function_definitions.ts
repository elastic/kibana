(async function main() {
  const pathToElasticsearch = process.argv[2];
  if (!pathToElasticsearch) {
    throw new Error('Path to Elasticsearch is required');
  }

  const ESFunctionDefinitionsDirectory = join(
    pathToElasticsearch,
    '/docs/reference/query-languages/esql/kibana/definition/functions'
  );

  const ESOperatorsDefinitionsDirectory = join(
    pathToElasticsearch,
    '/docs/reference/query-languages/esql/kibana/definition/operators'
  );

  // read all ES function definitions (the directory is full of JSON files) and create an array of definitions
  const ESFunctionDefinitions = readdirSync(ESFunctionDefinitionsDirectory).map((file) =>
    JSON.parse(readFileSync(`${ESFunctionDefinitionsDirectory}/${file}`, 'utf-8'))
  );

  const ESFOperatorDefinitions = readdirSync(ESOperatorsDefinitionsDirectory).map((file) =>
    JSON.parse(readFileSync(`${ESOperatorsDefinitionsDirectory}/${file}`, 'utf-8'))
  );

  const allFunctionDefinitions = ESFunctionDefinitions.concat(ESFOperatorDefinitions);

  const functionNames = allFunctionDefinitions.map((def) => def.name.toUpperCase());

  const functionsEnum = `export enum EsqlFunctionNames {
  ${allFunctionDefinitions
    .map(
      (func) =>
        `  ${func.name.toUpperCase()} = '${
          func.operator?.toLowerCase() ?? func.name.toLowerCase()
        }',`
    )
    .join('\n')}
  }`;

  await writeFile(
    join(__dirname, '../src/commands/definitions/generated/function_names.ts'),
    `/**
 * __AUTOGENERATED FILE. DO NOT EDIT THIS FILE DIRECTLY.__
 *
 * @note This file is generated by the \`generate_function_definitions.ts\`
 * script. Do not edit it manually.
 *
 *
 *
 *
 *
 *
 *
 *
 *
 *
 *
 *
 */

export const esqlFunctionNames = ${JSON.stringify(functionNames, null, 2)};

${functionsEnum}
`
  );

  // Copies docs/reference/esql/functions/kibana/inline_cast.json from ES.
  // It holds a mapping of inline casts to the respective function name that performs the cast.
  // Inline casting, `field::int`, is sugar syntax for `to_integer(field)`, so it's usefull to know this mapping
  // to perform validations.
  const ESInlineCastsFilePath = join(
    pathToElasticsearch,
    '/docs/reference/query-languages/esql/kibana/definition/inline_cast.json'
  );
  const inlineCastsDefinition = JSON.parse(readFileSync(ESInlineCastsFilePath, 'utf-8'));
  const castsMap = JSON.stringify(inlineCastsDefinition, null, 2);
  await writeFile(
    join(__dirname, '../src/commands/definitions/generated/inline_casts_mapping.ts'),
    `${FILE_HEADER}
export const inlineCastsMapping = ${castsMap} as const;
`
  );

  const scalarFunctionDefinitions: FunctionDefinition[] = [];
  const aggFunctionDefinitions: FunctionDefinition[] = [];
  const timeSeriesFunctionDefinitions: FunctionDefinition[] = [];
  const operatorDefinitions: FunctionDefinition[] = [];
  const groupingFunctionDefinitions: FunctionDefinition[] = [];

  for (const ESDefinition of allFunctionDefinitions) {
    if (aliases.has(ESDefinition.name) || excludedFunctions.has(ESDefinition.name)) {
      continue;
    }

    const functionDefinition = getFunctionDefinition(ESDefinition);
    const arePredicates = functionDefinition.name.toLowerCase().includes('predicates');
    if (arePredicates) {
      continue;
    }

    if (functionDefinition.name.toLowerCase() === 'match') {
      scalarFunctionDefinitions.push({
        ...functionDefinition,
        type: FunctionDefinitionTypes.SCALAR,
      });
      continue;
    }

    if (functionDefinition.type === FunctionDefinitionTypes.OPERATOR) {
      operatorDefinitions.push(functionDefinition);
    }
    if (functionDefinition.type === FunctionDefinitionTypes.SCALAR) {
      scalarFunctionDefinitions.push(functionDefinition);
    } else if (functionDefinition.type === FunctionDefinitionTypes.AGG) {
      aggFunctionDefinitions.push(functionDefinition);
    } else if (functionDefinition.type === FunctionDefinitionTypes.GROUPING) {
      groupingFunctionDefinitions.push(functionDefinition);
    } else if (functionDefinition.type === FunctionDefinitionTypes.TIME_SERIES_AGG) {
      timeSeriesFunctionDefinitions.push(functionDefinition);
    }
  }

  scalarFunctionDefinitions.push(...extraFunctions);

  await writeFile(
    join(__dirname, '../src/commands/definitions/generated/scalar_functions.ts'),
    printGeneratedFunctionsFile(scalarFunctionDefinitions, FunctionDefinitionTypes.SCALAR)
  );
  await writeFile(
    join(__dirname, '../src/commands/definitions/generated/aggregation_functions.ts'),
    printGeneratedFunctionsFile(aggFunctionDefinitions, FunctionDefinitionTypes.AGG)
  );
  await writeFile(
    join(__dirname, '../src/commands/definitions/generated/time_series_agg_functions.ts'),
    printGeneratedFunctionsFile(
      timeSeriesFunctionDefinitions,
      FunctionDefinitionTypes.TIME_SERIES_AGG
    )
  );
  await writeFile(
    join(__dirname, '../src/commands/definitions/generated/operators.ts'),
    printGeneratedFunctionsFile(
      enrichOperators(operatorDefinitions),
      FunctionDefinitionTypes.OPERATOR
    )
  );
  await writeFile(
    join(__dirname, '../src/commands/definitions/generated/grouping_functions.ts'),
    printGeneratedFunctionsFile(
      enrichGrouping(groupingFunctionDefinitions),
      FunctionDefinitionTypes.GROUPING
    )
  );

  // Generate PromQL function definitions
  const promqlFunctionssDirectory = join(pathToElasticsearch, ELASTICSEARCH_PROMQL_FUNCTIONS_PATH);

  const promqlDefinitions: PromQLESFunctionDefinition[] = readdirSync(promqlFunctionssDirectory)
    .filter((file) => file.endsWith('.json'))
    .map((file) => {
      const content = readFileSync(join(promqlFunctionssDirectory, file), 'utf-8');
      const parsed = JSON.parse(content);
      delete parsed.comment;
      return parsed as PromQLESFunctionDefinition;
    })
    .sort((definitionA, definitionB) => definitionA.name.localeCompare(definitionB.name));

  await writeFile(
    join(__dirname, GENERATED_PROMQL_OUTPUT_PATH),
    printPromQLFunctionsGeneratedFile(promqlDefinitions)
  );

  // Generate PromQL operator and label matcher definitions
  const promqlOperatorsDirectory = join(pathToElasticsearch, ELASTICSEARCH_PROMQL_OPERATORS_PATH);

  const allPromqlOperatorDefs: PromQLESFunctionDefinition[] = readdirSync(promqlOperatorsDirectory)
    .filter((file) => file.endsWith('.json'))
    .map((file) => {
      const content = readFileSync(join(promqlOperatorsDirectory, file), 'utf-8');
      const parsed = JSON.parse(content);
      delete parsed.comment;
      return parsed as PromQLESFunctionDefinition;
    })
    .sort((definitionA, definitionB) => definitionA.name.localeCompare(definitionB.name));

  const promqlOperatorDefinitions = allPromqlOperatorDefs.filter(
    (def) => def.type === PromQLFunctionDefinitionTypes.OPERATOR
  );
  const promqlLabelMatcherDefinitions = allPromqlOperatorDefs.filter(
    (def) => def.type === PromQLFunctionDefinitionTypes.LABEL_MATCHING_OPERATOR
  );

  await writeFile(
    join(__dirname, GENERATED_PROMQL_OPERATORS_OUTPUT_PATH),
    printPromQLOperatorsGeneratedFile(promqlOperatorDefinitions)
  );

  await writeFile(
    join(__dirname, GENERATED_PROMQL_LABEL_MATCHERS_OUTPUT_PATH),
    printPromQLLabelMatchersGeneratedFile(promqlLabelMatcherDefinitions)
  );
})();
