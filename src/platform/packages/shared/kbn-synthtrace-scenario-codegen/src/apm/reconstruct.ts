import { get } from 'lodash';
import { Scrubber } from '../scrubber';
import { collectNumericLeaves, collectStringLeaves } from '../leaf_fields';

/**
 * A raw APM document `_source` as returned by Elasticsearch (nested object form,
 * e.g. `{ service: { name }, transaction: { id } }`).
 */
export type CapturedSource = Record<string, unknown>;

export interface ServiceRef {
  /** Stable key used to correlate nodes back to a service/instance pair. */
  key: string;
  /** Identifier used for the generated `const` declaration. */
  varName: string;
  name: string;
  environment: string;
  agentName: string;
  instance: string;
  /**
   * The (possibly scrubbed) `host.name` of the service instance, preserved separately from the
   * instance name so replayed APM docs carry the correct host and surface in the Hosts view. A
   * service whose `service.node.name` differs from its `host.name` would otherwise replay under the
   * wrong host (the instance builder sets `host.name` from the instance name).
   */
  hostName?: string;
}

export interface ErrorNode {
  serviceKey: string;
  offsetMs: number;
  message: string;
  type?: string;
  culprit?: string;
  groupingKey?: string;
  /** The id of the transaction/span this error was attached to, if any. */
  parentId?: string;
  transactionId?: string;
}

export interface TraceNode {
  id: string;
  kind: 'transaction' | 'span';
  serviceKey: string;
  name: string;
  transactionType?: string;
  spanType?: string;
  spanSubtype?: string;
  offsetMs: number;
  durationMs: number;
  outcome?: 'success' | 'failure' | 'unknown';
  /** Curated extra fields emitted via `.overrides(...)`. */
  overrides: Record<string, unknown>;
  children: TraceNode[];
  errors: ErrorNode[];
}

/**
 * A single captured application metric document (`metricset.name: app`), holding
 * the numeric metric values (CPU, memory, JVM/runtime, ...) for one service
 * instance at one point in time. Emitted via the synthtrace `.appMetrics(...)` API.
 */
export interface MetricSample {
  serviceKey: string;
  offsetMs: number;
  /** Numeric metric fields keyed by their ES field path, e.g. `system.cpu.total.norm.pct`. */
  metrics: Record<string, number>;
}

export interface ReconstructedTrace {
  traceId: string;
  services: ServiceRef[];
  roots: TraceNode[];
  /** Errors that could not be attached to a captured transaction. */
  rootErrors: ErrorNode[];
  /** Application/system/runtime metric samples captured alongside the traces. */
  metrics: MetricSample[];
  startMs: number;
  endMs: number;
  documentCount: number;
}

export interface ReconstructOptions {
  /**
   * Anonymize service names, instances, hostnames and URLs so captured
   * production data is safe to commit.
   */
  scrub?: boolean;
  /**
   * Scrub map shared across captures so the same raw value maps to the same synthetic value
   * everywhere (e.g. a host captured both as APM data and host metrics yields a single synthetic
   * host name). Defaults to a fresh, capture-local scrubber.
   */
  scrubber?: Scrubber;
}

/**
 * Fields the synthtrace builders (and serializer) set themselves, so they must NOT be echoed back
 * via `.overrides(...)`/`.appMetrics(...)`. Re-emitting them would either duplicate a structural
 * value or clobber a regenerated id and break the rebuilt trace tree. Everything else on a captured
 * document is preserved verbatim, so generated scenarios reproduce the source faithfully instead of
 * echoing only a curated subset.
 */
const MANAGED_FIELDS = new Set<string>([
  '@timestamp',
  'timestamp.us',
  // ids are regenerated by the builder; reapplying captured ids breaks parent/child linking.
  'trace.id',
  'parent.id',
  // structural transaction/span fields set from the reconstructed node.
  'transaction.id',
  'transaction.name',
  'transaction.type',
  'transaction.duration.us',
  'transaction.sampled',
  'span.id',
  'span.name',
  'span.type',
  'span.subtype',
  'span.duration.us',
  // identity fields set by the service/instance builders.
  'service.name',
  'service.environment',
  'service.node.name',
  'agent.name',
  'host.name',
  // set by the processor/outcome helpers.
  'processor.event',
  'processor.name',
  'event.outcome',
]);

/**
 * Prefixes for groups of fields managed by synthtrace's serializer or the ingest pipeline
 * (`event.*` outcome/ingested/success_count, `observer.*`, `ecs.*`, `data_stream.*`). They carry no
 * scenario-meaningful data and are regenerated on replay, so we never echo them back.
 */
const MANAGED_PREFIXES = ['event.', 'observer.', 'ecs.', 'data_stream.'] as const;

const isManagedField = (field: string): boolean =>
  MANAGED_FIELDS.has(field) || MANAGED_PREFIXES.some((prefix) => field.startsWith(prefix));

/**
 * Predicates that decide how a captured field is treated when scrubbing. With collect-all, free-form
 * fields (URLs, request bodies, headers, IPs, db statements, user data) flow through unless dropped,
 * so these patterns replace the old curated sensitive/anonymized allowlists.
 */
const isSensitiveDropField = (field: string): boolean =>
  field.startsWith('url.') ||
  field.includes('.headers.') ||
  field.startsWith('http.request.body') ||
  field.startsWith('http.response.body') ||
  field.startsWith('user.') ||
  field.startsWith('user_agent.') ||
  field.startsWith('client.') ||
  field.startsWith('source.') ||
  field === 'host.ip' ||
  field.endsWith('.ip') ||
  field.includes('db.statement') ||
  field === 'message';

/**
 * Fields kept (so the scenario stays meaningful) but whose string value is replaced with a
 * deterministic, non-identifying label when scrubbing, since they can embed real hostnames,
 * dependency/resource names, or other arbitrary user data.
 */
const anonymizeCategory = (field: string): string | undefined => {
  if (field === 'destination.address' || field === 'server.address') {
    return 'host';
  }
  if (field === 'span.destination.service.resource' || field === 'service.target.name') {
    return 'dependency';
  }
  if (field.startsWith('labels.')) {
    return 'label';
  }
  return undefined;
};

const toEpochMs = (value: unknown): number => {
  if (typeof value === 'number') {
    return value;
  }
  if (typeof value === 'string') {
    const parsed = Date.parse(value);
    return Number.isNaN(parsed) ? 0 : parsed;
  }
  return 0;
};

const usToMs = (value: unknown): number => {
  const us = typeof value === 'number' ? value : Number(value);
  return Number.isFinite(us) ? Math.round(us / 1000) : 0;
};

const normalizeOutcome = (value: unknown): TraceNode['outcome'] => {
  return value === 'success' || value === 'failure' ? value : 'unknown';
};

/**
 * Collects every captured leaf field (numeric or string), minus the structural/managed fields the
 * builder sets itself, to be re-emitted via `.overrides(...)`. When scrubbing, known-sensitive
 * fields are dropped and a few host/dependency/label fields are anonymized, so collect-all stays
 * safe to commit.
 */
const collectExtraFields = (
  source: CapturedSource,
  scrub: boolean,
  scrubber: Scrubber
): Record<string, unknown> => {
  const leaves: Record<string, unknown> = {
    ...collectNumericLeaves(source),
    ...collectStringLeaves(source),
  };
  const overrides: Record<string, unknown> = {};
  for (const [field, value] of Object.entries(leaves)) {
    if (isManagedField(field)) {
      continue;
    }
    if (scrub) {
      if (isSensitiveDropField(field)) {
        continue;
      }
      const category = anonymizeCategory(field);
      if (category !== undefined && typeof value === 'string') {
        overrides[field] = scrubber.label(category, value);
        continue;
      }
    }
    overrides[field] = value;
  }
  return overrides;
};

const camelCase = (input: string): string =>
  input
    .replace(/[^a-zA-Z0-9]+(.)/g, (_, chr: string) => chr.toUpperCase())
    .replace(/[^a-zA-Z0-9]/g, '');

const getString = (source: CapturedSource, path: string): string | undefined => {
  const value = get(source, path);
  return typeof value === 'string' ? value : undefined;
};

/**
 * Reconstructs a single distributed trace from its raw Elasticsearch documents
 * into an in-memory tree that can be rendered as a synthtrace scenario.
 */
export const reconstructTrace = (
  docs: CapturedSource[],
  options: ReconstructOptions = {}
): ReconstructedTrace => {
  const scrub = options.scrub ?? false;
  const scrubber = options.scrubber ?? new Scrubber();

  const traceDocs = docs.filter((doc) => {
    const event = get(doc, 'processor.event');
    return event === 'transaction' || event === 'span' || event === 'error';
  });

  const metricDocs = docs.filter(
    (doc) => get(doc, 'processor.event') === 'metric' && getString(doc, 'metricset.name') === 'app'
  );

  if (traceDocs.length === 0 && metricDocs.length === 0) {
    throw new Error(
      'No transaction, span, error or application-metric documents found for the provided capture.'
    );
  }

  // Compute the min/max timestamp with a single pass. `Math.min(...timestamps)` would
  // spread the whole array as call arguments and overflow the stack on large captures.
  let startMs = Infinity;
  let endMs = -Infinity;
  for (const doc of traceDocs) {
    const ts = toEpochMs(get(doc, '@timestamp'));
    if (ts < startMs) startMs = ts;
    if (ts > endMs) endMs = ts;
  }
  for (const doc of metricDocs) {
    const ts = toEpochMs(get(doc, '@timestamp'));
    if (ts < startMs) startMs = ts;
    if (ts > endMs) endMs = ts;
  }

  const services = new Map<string, ServiceRef>();
  const usedVarNames = new Set<string>();

  const resolveService = (source: CapturedSource): string => {
    const rawName = getString(source, 'service.name') ?? 'unknown-service';
    const rawHostName = getString(source, 'host.name');
    const rawInstance =
      getString(source, 'service.node.name') ?? rawHostName ?? 'instance';

    const rawEnvironment = getString(source, 'service.environment') ?? 'production';

    const name = scrub ? scrubber.label('service', rawName) : rawName;
    const instance = scrub ? scrubber.label('instance', rawInstance) : rawInstance;
    const environment = scrub ? scrubber.label('environment', rawEnvironment) : rawEnvironment;
    // `host.name` is scrubbed via the shared `host` category (matching the infra capture) so the
    // same raw host yields the same synthetic name across both captures.
    let hostName: string | undefined;
    if (rawHostName !== undefined) {
      hostName = scrub ? scrubber.label('host', rawHostName) : rawHostName;
    }
    // Agent name (e.g. "go", "java", "rum-js") describes instrumentation, not data, so it is kept.
    const agentName = getString(source, 'agent.name') ?? 'unknown';

    // Key on environment and host too: the same service/instance reported under different
    // environments or hosts must stay distinct so every captured host is preserved.
    const key = `${name}::${instance}::${environment}::${hostName ?? ''}`;
    if (!services.has(key)) {
      let varName = `svc${camelCase(`${name}-${instance}`).replace(/^[a-z]/, (c) =>
        c.toUpperCase()
      )}`;
      let suffix = 2;
      const base = varName;
      while (usedVarNames.has(varName)) {
        varName = `${base}${suffix++}`;
      }
      usedVarNames.add(varName);
      services.set(key, { key, varName, name, environment, agentName, instance, hostName });
    }
    return key;
  };

  const nodes = new Map<string, TraceNode>();
  // node id -> parent.id, captured up front so we don't re-scan all docs per node later.
  const parentIdByNodeId = new Map<string, string | undefined>();
  const errors: ErrorNode[] = [];

  for (const source of traceDocs) {
    const event = get(source, 'processor.event');
    const offsetMs = toEpochMs(get(source, '@timestamp')) - startMs;
    const serviceKey = resolveService(source);

    if (event === 'transaction') {
      const id = getString(source, 'transaction.id');
      if (!id) continue;
      parentIdByNodeId.set(id, getString(source, 'parent.id'));
      const rawName = getString(source, 'transaction.name') ?? 'unknown';
      nodes.set(id, {
        id,
        kind: 'transaction',
        serviceKey,
        // Transaction names can embed URLs/ids, so anonymize them when scrubbing.
        name: scrub ? scrubber.label('transaction', rawName) : rawName,
        transactionType: getString(source, 'transaction.type') ?? 'request',
        offsetMs,
        durationMs: usToMs(get(source, 'transaction.duration.us')),
        outcome: normalizeOutcome(get(source, 'event.outcome')),
        overrides: collectExtraFields(source, scrub, scrubber),
        children: [],
        errors: [],
      });
    } else if (event === 'span') {
      const id = getString(source, 'span.id');
      if (!id) continue;
      parentIdByNodeId.set(id, getString(source, 'parent.id'));
      const rawName = getString(source, 'span.name') ?? 'unknown';
      nodes.set(id, {
        id,
        kind: 'span',
        serviceKey,
        // Span names can embed hostnames/queries, so anonymize them when scrubbing.
        name: scrub ? scrubber.label('span', rawName) : rawName,
        spanType: getString(source, 'span.type') ?? 'custom',
        spanSubtype: getString(source, 'span.subtype') ?? 'unknown',
        offsetMs,
        durationMs: usToMs(get(source, 'span.duration.us')),
        outcome: normalizeOutcome(get(source, 'event.outcome')),
        overrides: collectExtraFields(source, scrub, scrubber),
        children: [],
        errors: [],
      });
    } else {
      const rawMessage =
        getString(source, 'error.exception.0.message') ??
        getString(source, 'error.log.message') ??
        getString(source, 'error.grouping_name') ??
        'unknown error';
      const rawCulprit = getString(source, 'error.culprit');
      errors.push({
        serviceKey,
        offsetMs,
        // Error messages and culprits routinely contain real data/paths, so anonymize them.
        message: scrub ? scrubber.label('error-message', rawMessage) : rawMessage,
        type: getString(source, 'error.exception.0.type'),
        culprit: scrub && rawCulprit ? scrubber.label('culprit', rawCulprit) : rawCulprit,
        groupingKey: getString(source, 'error.grouping_key'),
        parentId: getString(source, 'parent.id'),
        transactionId: getString(source, 'transaction.id'),
      });
    }
  }

  const metrics: MetricSample[] = [];
  for (const source of metricDocs) {
    const serviceKey = resolveService(source);
    const offsetMs = toEpochMs(get(source, '@timestamp')) - startMs;
    // Capture every numeric metric value on the document (system/JVM/runtime/faas/...), minus the
    // structural fields the builder manages, rather than a hardcoded subset. Transaction/span/
    // breakdown metrics are already excluded upstream (only `metricset.name: app` docs reach here).
    const sample: Record<string, number> = {};
    for (const [field, value] of Object.entries(collectNumericLeaves(source))) {
      if (!isManagedField(field)) {
        sample[field] = value;
      }
    }
    // Skip metric docs that carry none of the fields we can replay.
    if (Object.keys(sample).length === 0) {
      continue;
    }
    metrics.push({ serviceKey, offsetMs, metrics: sample });
  }

  const roots: TraceNode[] = [];
  for (const node of nodes.values()) {
    const parentId = parentIdByNodeId.get(node.id);
    const parent = parentId ? nodes.get(parentId) : undefined;
    if (parent && parent !== node) {
      parent.children.push(node);
    } else {
      roots.push(node);
    }
  }

  const rootErrors: ErrorNode[] = [];
  for (const error of errors) {
    const parent =
      (error.parentId ? nodes.get(error.parentId) : undefined) ??
      (error.transactionId ? nodes.get(error.transactionId) : undefined);
    if (parent && parent.kind === 'transaction') {
      parent.errors.push(error);
    } else if (parent && parent.kind === 'span') {
      // The DSL attaches errors to transactions; fall back to the owning transaction.
      const tx = error.transactionId ? nodes.get(error.transactionId) : undefined;
      if (tx && tx.kind === 'transaction') {
        tx.errors.push(error);
      } else {
        rootErrors.push(error);
      }
    } else {
      rootErrors.push(error);
    }
  }

  const sortByOffset = <T extends { offsetMs: number }>(items: T[]): T[] =>
    [...items].sort((a, b) => a.offsetMs - b.offsetMs);

  // Sort each node's children/errors. Done iteratively (explicit stack) so a deeply nested
  // trace - a long parent->child span chain - can't overflow the call stack here.
  const sortStack: TraceNode[] = [...roots];
  while (sortStack.length > 0) {
    const node = sortStack.pop()!;
    node.children = sortByOffset(node.children);
    node.errors = sortByOffset(node.errors);
    for (const child of node.children) {
      sortStack.push(child);
    }
  }

  return {
    traceId: (traceDocs[0] && getString(traceDocs[0], 'trace.id')) ?? 'unknown-trace',
    services: [...services.values()],
    roots: sortByOffset(roots),
    rootErrors: sortByOffset(rootErrors),
    metrics: sortByOffset(metrics),
    startMs,
    endMs,
    documentCount: traceDocs.length + metricDocs.length,
  };
};
