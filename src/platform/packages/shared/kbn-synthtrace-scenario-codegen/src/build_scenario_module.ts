import { LICENSE_HEADER } from './constants';

export interface BuildScenarioModuleOptions {
  /** Synthtrace fields type used to parameterize `Scenario<...>`, e.g. `'ApmFields'`. Must be imported via `imports`. */
  fieldsType: string;
  /** Synthtrace ES client destructured from `clients`, e.g. `'apmEsClient'`. */
  clientVarName: string;
  /** Domain-specific import statements (the fields type + the DSL entrypoint), placed after the standard imports. */
  imports: string[];
  /** Absolute epoch (ms) of the earliest captured document; all events anchor to it. */
  startMs: number;
  /** Absolute epoch (ms) of the latest captured document; used for provenance + degenerate detection. */
  endMs: number;
  /** Number of captured source documents; used for degenerate detection + provenance. */
  documentCount: number;
  /** Domain-specific provenance lines, already prefixed with `' * '`. Falsy entries are dropped. */
  provenanceLines: Array<string | undefined>;
  /**
   * Whether to declare `const ENVIRONMENT = getSynthtraceEnvironment(__filename)` (and import it).
   * APM service builders need it; host-only scenarios don't. Defaults to `true`.
   */
  includeEnvironment?: boolean;
  /**
   * The body of the `generate(...)` function (indented for inside it). It runs after `const start`
   * is declared and must define an array/iterable named `rootsVar` of synthtrace serializables to
   * emit. Domains render their capture as plain data + a small fixed builder loop here, so the
   * module declares a handful of variables regardless of capture size (no per-event `const`, which
   * would otherwise hit the JS engine's per-function locals ceiling on large captures).
   */
  body: string;
  /** Name of the iterable defined by `body` to `yield*` from the generator. Defaults to `rootEvents`. */
  rootsVar?: string;
}

/**
 * Assembles a complete, runnable `@kbn/synthtrace` scenario module from a domain-provided,
 * data-driven `body`. Centralizes the parts that must be identical across domains for
 * deterministic, multi-worker-safe replay:
 *  - absolute epoch anchoring (`const start = <ms>`), so replayed data always lands in the
 *    same wall-clock window regardless of the CLI `--from/--to`;
 *  - a first-worker-bucket guard, so a wide multi-worker run emits each event exactly once;
 *  - a degenerate-source warning when many documents collapse onto a sub-second span.
 */
export const buildScenarioModule = (options: BuildScenarioModuleOptions): string => {
  const {
    fieldsType,
    clientVarName,
    imports,
    startMs,
    endMs,
    documentCount,
    provenanceLines,
    includeEnvironment = true,
    body,
    rootsVar = 'rootEvents',
  } = options;

  const capturedSpanMs = Math.max(0, endMs - startMs);
  const spanMinutes = Math.max(1, Math.ceil(capturedSpanMs / 60000));
  const startIso = new Date(startMs).toISOString();
  const endIso = new Date(endMs).toISOString();
  const degenerate = capturedSpanMs < 1000 && documentCount > 10;

  const provenance = [
    ...provenanceLines,
    ` * The capture spans ~${spanMinutes} minute(s) at a FIXED absolute time window:`,
    ` *   from ${startIso}`,
    ` *   to   ${endIso}`,
    ' * Set your Kibana time range to that exact window to see (or re-capture) all of it.',
    degenerate
      ? ` * WARNING: the captured data spans only ${capturedSpanMs}ms across ${documentCount} documents. ` +
        'The source data looks collapsed/degenerate (commonly caused by replaying a scenario ' +
        'without --workers=1). Re-seed your cluster with real data before relying on this.'
      : undefined,
    ' *',
    ' * Generated by the Observability "Capture page as synthtrace scenario" developer tool.',
    ' * Replay it with (multi-worker safe; each event is emitted exactly once):',
    ' *   node scripts/synthtrace <this-file> --clean',
  ]
    .filter((line): line is string => Boolean(line))
    .join('\n');

  const synthtraceImport = includeEnvironment
    ? "import { getSynthtraceEnvironment, withClient } from '@kbn/synthtrace';"
    : "import { withClient } from '@kbn/synthtrace';";
  const environmentPreamble = includeEnvironment
    ? '\nconst ENVIRONMENT = getSynthtraceEnvironment(__filename);\n'
    : '';

  return `${LICENSE_HEADER}

/**
${provenance}
 */

import type { Scenario } from '@kbn/synthtrace';
${synthtraceImport}
${imports.join('\n')}
${environmentPreamble}
const scenario: Scenario<${fieldsType}> = async ({ from }) => {
  return {
    generate: ({ range, clients: { ${clientVarName} } }) => {
      // Absolute epoch (ms) of the earliest captured document. Every event is anchored to
      // this fixed point and emitted exactly once, so the replayed data always occupies the
      // same wall-clock window (${startIso} - ${endIso})
      // no matter when it is replayed. The CLI --from/--to range is intentionally ignored.
      const start = ${startMs};

${body}

      // Because timestamps are absolute (independent of the replay range), every worker
      // thread would otherwise re-emit the entire dataset, producing N duplicate copies on a
      // wide --from/--to run. Emit only from the worker that owns the first bucket so the
      // capture is replayed exactly once regardless of --workers.
      const isFirstWorkerBucket = range.from.getTime() === from;

      return withClient(
        ${clientVarName},
        (function* generateCapturedEvents() {
          if (!isFirstWorkerBucket) {
            return;
          }
          yield* ${rootsVar};
        })()
      );
    },
  };
};

export default scenario;
`;
};
