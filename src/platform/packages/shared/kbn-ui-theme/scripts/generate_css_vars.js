/**
 * Generates CSS custom properties from EUI theme tokens.
 *
 * Produces two sets of variables:
 * 1. --eui-<key>: Flat tokens from euiThemeVars (used by ~66 files via euiThemeVars.X)
 * 2. --euiTheme-<path>: Computed theme paths (used by ~3,945 files via useEuiTheme().euiTheme.X)
 *
 * Usage: node scripts/generate_css_vars.js
 * Output: src/platform/packages/shared/kbn-ui-theme/eui_theme_vars.css
 */

const fs = require('fs');
const path = require('path');

const borealisLight = require('@elastic/eui-theme-borealis/lib/eui_theme_borealis_light.json');
const borealisDark = require('@elastic/eui-theme-borealis/lib/eui_theme_borealis_dark.json');
const { EuiThemeBorealis } = require('@elastic/eui-theme-borealis');
const { getComputed } = require('@elastic/eui');

function flatten(obj, prefix = '') {
  const result = {};
  for (const [key, value] of Object.entries(obj)) {
    const p = prefix ? `${prefix}-${key}` : key;
    if (value && typeof value === 'object' && !Array.isArray(value)) {
      Object.assign(result, flatten(value, p));
    } else {
      result[p] = value;
    }
  }
  return result;
}

function generateVarBlock(flatTokens, prefix, indent = '  ') {
  return Object.entries(flatTokens)
    .filter(([, v]) => typeof v === 'string')
    .map(([key, value]) => `${indent}--${prefix}-${key}: ${value};`)
    .join('\n');
}

function generateDiffBlock(flatLight, flatDark, prefix, indent = '  ') {
  return Object.entries(flatDark)
    .filter(([key, value]) => typeof value === 'string' && value !== flatLight[key])
    .map(([key, value]) => `${indent}--${prefix}-${key}: ${value};`)
    .join('\n');
}

// --- Part 1: euiThemeVars flat tokens ---
const flatLight = flatten(borealisLight);
const flatDark = flatten(borealisDark);

// --- Part 2: Computed theme tokens ---
const computedLight = flatten(getComputed(EuiThemeBorealis, {}, 'LIGHT', false));
const computedDark = flatten(getComputed(EuiThemeBorealis, {}, 'DARK', false));

const css = `/* Generated by scripts/generate_css_vars.js — do not edit manually */

/* === euiThemeVars flat tokens (--eui-*) === */
:root, [data-theme="light"] {
${generateVarBlock(flatLight, 'eui')}
}

[data-theme="dark"] {
${generateDiffBlock(flatLight, flatDark, 'eui')}
}

/* === Computed theme tokens (--euiTheme-*) === */
:root, [data-theme="light"] {
${generateVarBlock(computedLight, 'euiTheme')}
}

[data-theme="dark"] {
${generateDiffBlock(computedLight, computedDark, 'euiTheme')}
}
`;

const outputPath = path.resolve(__dirname, '../eui_theme_vars.css');
fs.writeFileSync(outputPath, css, 'utf-8');

const lightVarCount = Object.values(flatLight).filter((v) => typeof v === 'string').length;
const darkDiffCount = Object.entries(flatDark).filter(
  ([k, v]) => typeof v === 'string' && v !== flatLight[k]
).length;
const computedLightCount = Object.values(computedLight).filter((v) => typeof v === 'string').length;
const computedDarkDiffCount = Object.entries(computedDark).filter(
  ([k, v]) => typeof v === 'string' && v !== computedLight[k]
).length;

process.stdout.write(
  `Generated ${outputPath}\n` +
    `  euiThemeVars: ${lightVarCount} light vars, ${darkDiffCount} dark overrides\n` +
    `  Computed theme: ${computedLightCount} light vars, ${computedDarkDiffCount} dark overrides\n`
);
