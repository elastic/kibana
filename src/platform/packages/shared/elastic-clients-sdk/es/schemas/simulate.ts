import { z } from '@kbn/zod/v4'

import { SpecUtilsStringified } from './_spec_utils'
import { ErrorCause, Id, IndexName, PipelineName, RequestBase } from './_types'
import { MappingTypeMapping } from './_types.mapping'
import { ClusterComponentTemplateNode } from './cluster'
import { IndicesIndexTemplate } from './indices'
import { IngestDocument, IngestPipeline } from './ingest'

/**
 * The results of ingest simulation on a single document. The _source of the document contains
 * the results after running all pipelines listed in executed_pipelines on the document. The
 * list of executed pipelines is derived from the pipelines that would be executed if this
 * document had been ingested into _index.
 */
export const SimulateIngestIngestDocumentSimulation = z.object({
  _id: Id.describe('Identifier for the document.'),
  _index: IndexName.describe('Name of the index that the document would be indexed into if this were not a simulation.'),
  _source: z.record(z.string(), z.any()).describe('JSON body for the document.'),
  _version: SpecUtilsStringified.describe(''),
  executed_pipelines: z.array(z.string()).describe('A list of the names of the pipelines executed on this document.'),
  ignored_fields: z.array(z.record(z.string(), z.string())).describe('A list of the fields that would be ignored at the indexing step. For example, a field whose value is larger than the allowed limit would make it through all of the pipelines, but would not be indexed into Elasticsearch.').optional(),
  error: z.lazy(() => ErrorCause).describe('Any error resulting from simulatng ingest on this doc. This can be an error generated by executing a processor, or a mapping validation error when simulating indexing the resulting doc.').optional(),
  effective_mapping: z.lazy(() => MappingTypeMapping).optional()
}).catchall(z.any()).meta({ id: 'SimulateIngestIngestDocumentSimulation' })
export type SimulateIngestIngestDocumentSimulation = z.infer<typeof SimulateIngestIngestDocumentSimulation>

export const SimulateIngestMergeType = z.enum(['index', 'template']).meta({ id: 'SimulateIngestMergeType' })
export type SimulateIngestMergeType = z.infer<typeof SimulateIngestMergeType>

/**
 * Simulate data ingestion.
 *
 * Run ingest pipelines against a set of provided documents, optionally with substitute pipeline definitions, to simulate ingesting data into an index.
 *
 * This API is meant to be used for troubleshooting or pipeline development, as it does not actually index any data into Elasticsearch.
 *
 * The API runs the default and final pipeline for that index against a set of documents provided in the body of the request.
 * If a pipeline contains a reroute processor, it follows that reroute processor to the new index, running that index's pipelines as well the same way that a non-simulated ingest would.
 * No data is indexed into Elasticsearch.
 * Instead, the transformed document is returned, along with the list of pipelines that have been run and the name of the index where the document would have been indexed if this were not a simulation.
 * The transformed document is validated against the mappings that would apply to this index, and any validation error is reported in the result.
 *
 * This API differs from the simulate pipeline API in that you specify a single pipeline for that API, and it runs only that one pipeline.
 * The simulate pipeline API is more useful for developing a single pipeline, while the simulate ingest API is more useful for troubleshooting the interaction of the various pipelines that get applied when ingesting into an index.
 *
 * By default, the pipeline definitions that are currently in the system are used.
 * However, you can supply substitute pipeline definitions in the body of the request.
 * These will be used in place of the pipeline definitions that are already in the system. This can be used to replace existing pipeline definitions or to create new ones. The pipeline substitutions are used only within this request.
 */
export const SimulateIngestRequest = z.object({
  ...RequestBase.shape,
  index: IndexName.describe('The index to simulate ingesting into. This value can be overridden by specifying an index on each document. If you specify this parameter in the request path, it is used for any documents that do not explicitly specify an index argument.').optional().meta({ found_in: 'path' }),
  pipeline: PipelineName.describe('The pipeline to use as the default pipeline. This value can be used to override the default pipeline of the index.').optional().meta({ found_in: 'query' }),
  merge_type: SimulateIngestMergeType.describe('The mapping merge type if mapping overrides are being provided in mapping_addition. The allowed values are one of index or template. The index option merges mappings the way they would be merged into an existing index. The template option merges mappings the way they would be merged into a template.').optional().meta({ found_in: 'query' }),
  docs: z.array(IngestDocument).describe('Sample documents to test in the pipeline.').meta({ found_in: 'body' }),
  component_template_substitutions: z.record(z.string(), ClusterComponentTemplateNode).describe('A map of component template names to substitute component template definition objects.').optional().meta({ found_in: 'body' }),
  index_template_substitutions: z.record(z.string(), IndicesIndexTemplate).describe('A map of index template names to substitute index template definition objects.').optional().meta({ found_in: 'body' }),
  mapping_addition: z.lazy(() => MappingTypeMapping).optional().meta({ found_in: 'body' }),
  pipeline_substitutions: z.record(z.string(), IngestPipeline).describe('Pipelines to test. If you don’t specify the `pipeline` request path parameter, this parameter is required. If you specify both this and the request path parameter, the API only uses the request path parameter.').optional().meta({ found_in: 'body' })
}).meta({ id: 'SimulateIngestRequest' })
export type SimulateIngestRequest = z.infer<typeof SimulateIngestRequest>

export const SimulateIngestSimulateIngestDocumentResult = z.object({
  doc: SimulateIngestIngestDocumentSimulation.optional()
}).meta({ id: 'SimulateIngestSimulateIngestDocumentResult' })
export type SimulateIngestSimulateIngestDocumentResult = z.infer<typeof SimulateIngestSimulateIngestDocumentResult>

export const SimulateIngestResponse = z.object({
  docs: z.array(SimulateIngestSimulateIngestDocumentResult)
}).meta({ id: 'SimulateIngestResponse' })
export type SimulateIngestResponse = z.infer<typeof SimulateIngestResponse>
