/*
 * Manifest-driven loader for Elasticsearch API definitions.
 *
 * `apiManifest` is the single source of metadata for every endpoint (name,
 * namespace, method, path, description, etc.).  Importing this file is cheap:
 * no schema data is loaded transitively.
 *
 * `loadEsApi(meta)` builds a full `EsApiDefinition` on demand by combining
 * the manifest entry with the corresponding JSON schema file read from disk via
 * `fs.readFileSync` + `JSON.parse`.  The parsed object is returned to the
 * caller and is NOT memoised, so it is eligible for garbage collection once the
 * caller drops its reference.
 *
 * Schema files live at `es/schemas/<namespaceFile>.json` (one file per
 * endpoint, generated by elastic/elastic-client-generator-js).  Endpoints that
 * accept no parameters have no schema file; `input` is omitted from their
 * definition.
 */

import * as fs from 'fs';
import * as path from 'path';

import type { JsonSchemaObject } from '../lib/json_schema';
import type { EsApiDefinition } from './types';
import type { EsApiMeta } from './api-manifest';

export { apiManifest } from './api-manifest';
export type { EsApiMeta } from './api-manifest';

const SCHEMAS_DIR = path.join(__dirname, 'schemas');

/**
 * Builds a full `EsApiDefinition` from a manifest entry.
 *
 * The `input` JSON Schema is read from `es/schemas/<meta.namespaceFile>.json`
 * and parsed on every call — it is intentionally not cached so the parsed
 * object can be garbage-collected once the caller is done with it.
 *
 * Accepts any object that is structurally compatible with `EsApiMeta`; in
 * practice callers pass an item directly from `apiManifest`.
 */
export async function loadEsApi(meta: EsApiMeta): Promise<EsApiDefinition> {
  let input: JsonSchemaObject | undefined;

  const schemaPath = path.join(SCHEMAS_DIR, `${meta.namespaceFile}.json`);
  try {
    const raw = fs.readFileSync(schemaPath, 'utf8');
    input = JSON.parse(raw) as JsonSchemaObject;
  } catch {
    // No schema file for this endpoint — it accepts no parameters.
  }

  const def: EsApiDefinition = {
    name: meta.name,
    description: meta.description,
    method: meta.method,
    path: meta.path,
  };

  if (meta.namespace != null) def.namespace = meta.namespace;
  if (input !== undefined) def.input = input;
  if (meta.responseType !== undefined) def.responseType = meta.responseType;
  if (meta.bodyFormat !== undefined) def.bodyFormat = meta.bodyFormat;

  return def;
}
