interface ScaleOpts {
  /** Number of child streams (apps) to fork and feed. */
  streamCount: number;
  /** Per-stream service-graph generations per 1-minute tick (passed to `range.rate`). Keep modest. */
  rpm: number;
  /** Reproducible fleet seed (drives identities, content, incident selection). */
  seed: number;
  /** Fraction of streams [0–1] running an active incident vs a healthy baseline. */
  incidentRatio: number;
  /** Minutes of healthy baseline before an incident starts (offset 0 of the cycle). */
  baselineMinutes: number;
  /** Optional subset of archetype ids to draw from; defaults to all. */
  archetypes?: string[];
  /** Optional incident scenario id to force on every incident-active stream. */
  scenario?: string;
}

const requirePositiveInt = (value: unknown, name: string, fallback: number): number => {
  if (value === undefined) return fallback;
  const parsed = Number(value);
  if (!Number.isFinite(parsed) || !Number.isInteger(parsed) || parsed < 1) {
    throw new Error(
      `Invalid scenario option "${name}": expected a positive integer, got "${value}"`
    );
  }
  return parsed;
};

const requirePositiveNumber = (value: unknown, name: string, fallback: number): number => {
  if (value === undefined) return fallback;
  const parsed = Number(value);
  if (!Number.isFinite(parsed) || parsed <= 0) {
    throw new Error(
      `Invalid scenario option "${name}": expected a positive number, got "${value}"`
    );
  }
  return parsed;
};

const parseScaleOpts = (raw: Record<string, unknown> | undefined): ScaleOpts => {
  const opts = raw ?? {};

  const seed = opts.seed !== undefined ? Number(opts.seed) : Math.floor(Math.random() * 100000);
  if (!Number.isFinite(seed) || !Number.isInteger(seed)) {
    throw new Error(
      `Invalid scenario option "seed": expected a finite integer, got "${opts.seed}"`
    );
  }

  const incidentRatio = opts.incidentRatio !== undefined ? Number(opts.incidentRatio) : 0.5;
  if (!Number.isFinite(incidentRatio) || incidentRatio < 0 || incidentRatio > 1) {
    throw new Error(
      `Invalid scenario option "incidentRatio": expected a number between 0 and 1, got "${opts.incidentRatio}"`
    );
  }

  const baselineMinutes = opts.baselineMinutes !== undefined ? Number(opts.baselineMinutes) : 0;
  if (!Number.isFinite(baselineMinutes) || baselineMinutes < 0) {
    throw new Error(
      `Invalid scenario option "baselineMinutes": expected a non-negative number, got "${opts.baselineMinutes}"`
    );
  }

  // Accept a JSON array (`--scenarioOpts='{"archetypes":["banking","iot_telemetry"]}'`)
  // or a `+`-delimited string — NOT comma, which the CLI uses as the opt separator.
  const archetypes = Array.isArray(opts.archetypes)
    ? opts.archetypes.map((id) => String(id).trim()).filter(Boolean)
    : typeof opts.archetypes === 'string'
    ? opts.archetypes
        .split(/[+,]/)
        .map((id) => id.trim())
        .filter(Boolean)
    : undefined;

  if (archetypes) {
    const unknown = archetypes.filter((id) => !ARCHETYPES.some((a) => a.id === id));
    if (unknown.length > 0) {
      throw new Error(
        `Unknown archetype(s): "${unknown.join(', ')}". Available: ${ARCHETYPES.map(
          (a) => a.id
        ).join(', ')}`
      );
    }
  }

  return {
    streamCount: requirePositiveInt(opts.streamCount, 'streamCount', 10),
    rpm: requirePositiveNumber(opts.rpm, 'rpm', 1),
    seed,
    incidentRatio,
    baselineMinutes,
    archetypes,
    scenario: typeof opts.scenario === 'string' ? opts.scenario : undefined,
  };
};

/** One provisioned stream: which archetype, identity, active incident, and content seed. */
interface FleetMember {
  appId: string;
  streamName: string;
  archetype: Archetype;
  scenarioId?: string;
  seed: number;
}

/**
 * Deterministically plans the fleet from the opts. Archetypes are assigned
 * round-robin (only `claims` exists today); incident activation and scenario
 * choice are drawn from a per-stream `mulberry32(seed)` so a `seed` reproduces an
 * exact fleet.
 */
const buildFleetPlan = (opts: ScaleOpts): FleetMember[] => {
  const pool = opts.archetypes
    ? ARCHETYPES.filter((a) => opts.archetypes!.includes(a.id))
    : ARCHETYPES;

  return Array.from({ length: opts.streamCount }, (_, i): FleetMember => {
    const appId = `app${String(i).padStart(3, '0')}`;
    const archetype = pool[i % pool.length];
    const rng = mulberry32(opts.seed + i + 1);

    const incidentActive = opts.incidentRatio > 0 && rng() < opts.incidentRatio;
    let scenarioId: string | undefined;
    if (incidentActive) {
      if (opts.scenario) {
        if (!archetype.scenarioIds.includes(opts.scenario)) {
          throw new Error(
            `Unknown scenario "${opts.scenario}" for archetype "${
              archetype.id
            }". Available: ${archetype.scenarioIds.join(', ')}`
          );
        }
        scenarioId = opts.scenario;
      } else {
        scenarioId =
          archetype.incidentScenarios[Math.floor(rng() * archetype.incidentScenarios.length)];
      }
    }

    return {
      appId,
      streamName: `${ROOT_STREAM}.${appId}`,
      archetype,
      scenarioId,
      seed: opts.seed + i,
    };
  });
};

const scenario: Scenario<LogDocument> = async (runOptions) => {
  const { logger, from } = runOptions;
  const opts = parseScaleOpts(runOptions.scenarioOpts);
  const fleet = buildFleetPlan(opts);

  return {
    bootstrap: async ({ streamsClient }) => {
      await streamsClient.enable();

      logger.info(
        `sigevents_scale: forking ${fleet.length} stream(s) off ${ROOT_STREAM} on ${APP_ID_FIELD}`
      );

      for (const member of fleet) {
        await streamsClient.forkStream(
          ROOT_STREAM,
          {
            stream: { name: member.streamName },
            where: { field: APP_ID_FIELD, eq: member.appId },
          },
          // Idempotent: re-runs against an already-provisioned fleet skip existing children.
          { ignore: [409] }
        );
      }

      const incidentCount = fleet.filter((m) => m.scenarioId).length;
      logger.info(
        `sigevents_scale: provisioned ${fleet.length} stream(s), ${incidentCount} with an active incident`
      );
    },

    // Replace the stock replicate-to-3 routing transform: we stamp `_index` ourselves
    // and let the per-child fork conditions route by `attributes.app.id`.
    setupPipeline: ({ streamsClient }) => {
      streamsClient.setPipeline((base: Readable) =>
        pipeline(base, getSerializeTransform<LogDocument>(), (err) => {
          if (err) {
            throw err;
          }
        })
      );
    },

    generate: ({ range, clients: { streamsClient } }) => {
      const ticks = fleet.map((member) => ({
        appId: member.appId,
        tick: member.archetype.buildTick({
          scenarioId: member.scenarioId,
          seed: member.seed,
          from,
          baselineMinutes: opts.baselineMinutes,
        }),
      }));

      return withClient(
        streamsClient,
        logger.perf('generating_sigevents_scale', () =>
          range
            .interval('1m')
            .rate(opts.rpm)
            .generator((timestamp, index) =>
              ticks.flatMap(({ appId, tick }) =>
                tick(timestamp, index).map((entry) => {
                  // Route to the wired root, then carry the per-app key so the reroute
                  // pipeline fans this doc out to `logs.ecs.<appId>` (proven 1:1 live).
                  entry.fields._index = ROOT_STREAM;
                  (entry.fields as Record<string, unknown>).attributes = { app: { id: appId } };
                  return entry;
                })
              )
            )
        )
      );
    },
  };
};

export default scenario;
