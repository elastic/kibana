/**
 * SigEvents scalability scenario — provisions N rational, KI-rich wired streams
 * for the stream-COUNT axis (10 → 600) of the SigEvents scalability effort.
 *
 * Each stream is a distinct synthetic application generated by the service-graph
 * engine (`lib/service_graph_logs`). Streams are forked off the wired root
 * `logs.ecs` and routed by an injected, unmapped custom field `attributes.app.id`
 * (one exact-match value per stream) — the routing mechanic proven live in the
 * scalability design (see aj/performance and scalability/scalability/README.md).
 *
 * This scenario owns its own document routing: it stamps `_index: 'logs.ecs'` and
 * the per-app `attributes.app.id` on every doc and lets the streams reroute
 * pipeline fan each doc out to its child. It therefore replaces the stock
 * `streamsRoutingTransform` (which replicates every doc to `logs`/`logs.otel`/
 * `logs.ecs`) via `setupPipeline`.
 *
 *   node scripts/synthtrace.js sigevents_scale \
 *     --scenarioOpts="streamCount=50,rpm=1,seed=42,incidentRatio=0.5" --live
 *
 * To restrict to a subset of archetypes, pass JSON (the CLI splits the plain form
 * on commas, so a multi-value `archetypes` must be a JSON array or `+`-delimited):
 *
 *   --scenarioOpts='{"streamCount":4,"archetypes":["banking","iot_telemetry"]}'
 *
 * Goal is orchestration/COUNT load, not per-stream volume — keep `rpm` modest and
 * constant across tiers (volume/ES-cost is owned by the performance effort).
 */

