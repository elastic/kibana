import Path from 'path';
import Fsp from 'fs/promises';
import zlib from 'zlib';
import { promisify } from 'util';
import { availableParallelism } from 'os';

import globby from 'globby';
import { REPO_ROOT } from '@kbn/repo-info';
import { runBuild } from '@kbn/rspack-optimizer';
import { asyncForEachWithLimit } from '@kbn/std';

import type { Task } from '../lib';
import { write } from '../lib';

const brotliCompressAsync = promisify(zlib.brotliCompress);

// [rspack-transition] This task replaces BuildKibanaPlatformPlugins when
// KBN_USE_RSPACK=true. When the legacy optimizer is removed, rename this
// to BuildBundles and delete the conditional in build_distributables.ts.
export const BuildRspackBundles: Task = {
  description: 'Building distributable versions of Kibana bundles with RSPack',
  async run(buildConfig, log, build) {
    const result = await runBuild({
      repoRoot: REPO_ROOT,
      outputRoot: build.resolvePath(),
      dist: true,
      cache: false,
      watch: false,
      hmr: false,
      examples: buildConfig.pluginSelector.examples,
      testPlugins: buildConfig.pluginSelector.testPlugins,
      log,
    });

    if (!result.success) {
      throw new Error(`RSPack build failed: ${result.errors?.join(', ') ?? 'unknown error'}`);
    }

    // Brotli-compress rspack bundles for faster serving in production.
    // The server's selectCompressedFile in dynamic_asset_response.ts already
    // serves .br sidecars when they exist, so no server changes are needed.
    const bundlesDir = Path.resolve(build.resolvePath(), 'target/public/bundles');
    const brotliQuality = buildConfig.isRelease ? zlib.constants.BROTLI_MAX_QUALITY : 9;
    const jsFiles = await globby(['**/*.js'], { cwd: bundlesDir, absolute: true });
    log.info(`Brotli-compressing ${jsFiles.length} bundle files...`);
    await asyncForEachWithLimit(jsFiles, availableParallelism(), async (file) => {
      const content = await Fsp.readFile(file);
      const compressed = await brotliCompressAsync(content, {
        params: { [zlib.constants.BROTLI_PARAM_QUALITY]: brotliQuality },
      });
      await Fsp.writeFile(file + '.br', compressed);
    });

    // Read metrics.json emitted by BundleMetricsPlugin (contains all plugins' combined metrics)
    const metricsPath = Path.resolve(build.resolvePath(), 'target/public/bundles/metrics.json');
    const metrics = JSON.parse(await Fsp.readFile(metricsPath, 'utf-8'));
    await write(
      buildConfig.resolveFromTarget('optimizer_bundle_metrics.json'),
      JSON.stringify(metrics, null, 2)
    );
    // Clean up the per-build metrics file (not needed in the distributable)
    await Fsp.unlink(metricsPath);

    // Clean up build-time-only entry wrappers -- these are generated by the
    // rspack config to create the unified entry point but are not needed at
    // runtime and should not ship in the distributable.
    const entryWrappersDir = Path.resolve(build.resolvePath(), 'target/.rspack-entry-wrappers');
    await Fsp.rm(entryWrappersDir, { recursive: true, force: true });
  },
};
