import { existsSync } from 'fs';

import globby from 'globby';

import { REPO_ROOT } from '@kbn/repo-info';
import { run } from '@kbn/dev-cli-runner';
import { File } from './file';
import { checkFileCasing } from './precommit_hook/check_file_casing';

const IGNORE_PATTERNS = [
  // the gitignore: true option makes sure that we don't
  // include files from node_modules in the result, but it still
  // loads all of the files from node_modules before filtering
  // so it's still super slow. This prevents loading the files
  // and still relies on gitignore to final ignores
  '**/node_modules',
  // temporarily allow non-snake case for module names
  // during relocation packages and plugins
  // in the context of Sustainable Kibana Architecture
  'src/core/**',
  'src/dev/packages/**',
  'src/platform/**',
  'x-pack/platform/**',
  'x-pack/solutions/**',

  // ignore autogenerated snapshots
  'x-pack/solutions/observability/test/api_integration_deployment_agnostic/apis/ai_assistant/knowledge_base/snapshots',
  'x-pack/platform/test/api_integration_deployment_agnostic/apis/streams/snapshots',
];

run(
  async ({ log, flagsReader }) => {
    const filesArg = flagsReader.string('files');

    let paths: string[];

    if (filesArg) {
      // Filter to only the specified files
      const targetFileList = filesArg
        .split(',')
        .map((f) => f.trim())
        .filter(Boolean);
      log.info(`Checking file casing for ${targetFileList.length} specified file(s)`);

      // Filter out files that don't exist and files matching ignore patterns
      paths = targetFileList.filter((filePath) => {
        // Check if file exists
        if (!existsSync(filePath)) {
          log.debug(`Skipping non-existent file: ${filePath}`);
          return false;
        }

        // Check if file matches any ignore pattern
        for (const pattern of IGNORE_PATTERNS) {
          const regex = new RegExp(
            '^' + pattern.replace(/\*\*/g, '.*').replace(/\*/g, '[^/]*') + '$'
          );
          if (regex.test(filePath)) {
            log.debug(`Skipping ignored file: ${filePath}`);
            return false;
          }
        }

        return true;
      });
    } else {
      // Default: scan all files in the repo
      paths = await globby('**/*', {
        cwd: REPO_ROOT,
        onlyFiles: true,
        gitignore: true,
        ignore: IGNORE_PATTERNS,
      });
    }

    const files = paths.map((path) => new File(path));

    await checkFileCasing(log, files);
  },
  {
    flags: {
      string: ['files'],
      help: `
        --files    Optional comma-separated list of files to check. If not provided,
                   checks all files in the repository.
      `,
    },
  }
);
