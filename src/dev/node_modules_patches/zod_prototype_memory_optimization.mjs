/**
 * Apply all Zod memory optimization patches.
 * @param {object} options
 * @param {import('../../src/dev/kbn_pm/src/lib/log.mjs').Log} [options.log]
 */
export async function applyZodMemoryPatches({ log } = {}) {
  const zodPath = resolve(process.cwd(), 'node_modules/zod');

  // Verify the Zod version matches what we're patching
  const zodPackageJson = JSON.parse(readFileSync(resolve(zodPath, 'package.json'), 'utf8'));
  const zodVersion = zodPackageJson.version;
  if (zodVersion !== ZOD_PATCH_VERSION) {
    const msg = `Zod version mismatch: expected ${ZOD_PATCH_VERSION}, found ${zodVersion}. ` +
      `The memory optimization patch in src/dev/node_modules_patches/zod_prototype_memory_optimization.mjs ` +
      `may need to be updated or removed.`;
    if (log) {
      log.warning(msg);
    } else {
      console.warn(`[WARNING] ${msg}`);
    }
    return;
  }

  const coreApplied = patchCoreModule(zodPath);
  const schemasApplied = patchSchemasModule(zodPath);
  const totalApplied = coreApplied + schemasApplied;

  if (totalApplied > 0) {
    const msg = `Applied Zod v4 memory optimization patch (${totalApplied} replacements). ` +
      `Schema instances now use ~2KB instead of ~13KB of heap.`;
    if (log) {
      log.success(msg);
    } else {
      console.log(`[INFO] ${msg}`);
    }
  } else {
    const msg = 'Zod memory optimization patch already applied, skipping.';
    if (log) {
      log.info(msg);
    } else {
      console.log(`[INFO] ${msg}`);
    }
  }
}
