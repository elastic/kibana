/**
 * Zod v4 memory optimization patch.
 *
 * PROBLEM:
 *   Zod v4 schema instances assign every method as an own property (e.g. `inst.parse`,
 *   `inst.optional`, `inst.email`, etc.), resulting in ~91 own properties per instance.
 *   V8 switches objects to slow "dictionary mode" (hash-table-based) when they exceed
 *   ~27 own properties, which is significantly slower to access and wastes memory.
 *   In practice this causes ~13KB of heap per schema instance.
 *
 * FIX:
 *   - Patch `node_modules/zod/v4/classic/schemas.cjs` to remove method assignments
 *     from initializers, leaving only instance-specific data as own properties.
 *   - Add a `_setupZodPrototypes()` call at the end of the module that places all
 *     shared methods on the prototype chain and sets up proper inheritance.
 *   - Patch `node_modules/zod/v4/core/core.cjs` to remove the "support prototype
 *     modifications" loop (which would otherwise re-copy prototype methods to instances).
 *
 * RESULT:
 *   Own property count per instance drops from ~91 to ~5-8, keeping objects in V8's
 *   fast-property mode. Memory footprint drops from ~13KB to ~2KB per schema instance
 *   (an ~85% reduction).
 *
 * UPSTREAM:
 *   This is a workaround for https://github.com/colinhacks/zod/issues/5760
 *   Once the upstream fix is released and Kibana upgrades, this patch can be removed.
 *
 * MAINTENANCE:
 *   This patch is applied by `scripts/apply_node_modules_patches.js` which is called
 *   during `yarn kbn bootstrap`. If the patch fails (e.g. after a Zod version bump),
 *   update the anchors in this file to match the new version.
 */

import { readFileSync, writeFileSync } from 'fs';
import { resolve } from 'path';
import { createRequire } from 'module';

const require = createRequire(import.meta.url);

export const ZOD_PATCH_VERSION = '4.3.6';
export const PATCH_DESCRIPTION = 'Zod v4 prototype chain memory optimization';

/**
 * Applies all changes to a file content, returning the patched content.
 * @param {string} content - The original file content
 * @param {Array<{from: string, to: string, description: string}>} replacements
 * @returns {{ patched: string, appliedCount: number }}
 */
function applyReplacements(content, replacements) {
  let patched = content;
  let appliedCount = 0;
  for (const { from, to, description } of replacements) {
    if (patched.includes(from)) {
      patched = patched.replace(from, to);
      appliedCount++;
    } else if (!patched.includes(to)) {
      // Neither the original nor the replacement is present - something changed
      throw new Error(`Patch anchor not found for: ${description}`);
    }
    // If `to` is already present, the patch was already applied - skip silently
  }
  return { patched, appliedCount };
}

/**
 * Patches zod/v4/core/core.cjs to remove the "support prototype modifications" loop.
 */
export function patchCoreModule(zodPath) {
  const corePath = resolve(zodPath, 'v4/core/core.cjs');
  const original = readFileSync(corePath, 'utf8');

  const { patched, appliedCount } = applyReplacements(original, [
    {
      description: 'Remove prototype modifications loop from $constructor',
      from: `        inst._zod.traits.add(name);
        initializer(inst, def);
        // support prototype modifications
        const proto = _.prototype;
        const keys = Object.keys(proto);
        for (let i = 0; i < keys.length; i++) {
            const k = keys[i];
            if (!(k in inst)) {
                inst[k] = proto[k].bind(inst);
            }
        }`,
      to: `        inst._zod.traits.add(name);
        initializer(inst, def);
        // NOTE: The "support prototype modifications" loop has been removed as part of
        // the memory optimization patch. Methods are now inherited from the prototype
        // chain (set up by _setupZodPrototypes() in schemas.cjs) rather than being
        // copied as bound own properties on every instance. This reduces own property
        // count from ~91 to ~8 per instance, keeping objects in V8's fast-property mode.`,
    },
  ]);

  if (appliedCount > 0) {
    writeFileSync(corePath, patched, 'utf8');
  }
  return appliedCount;
}

