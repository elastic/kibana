import { applyZodMemoryPatches } from '../../../../../dev/node_modules_patches/zod_prototype_memory_optimization.mjs';

/**
 * Applies all node_modules patches after yarn install.
 * Each patch is idempotent: running it on an already-patched module is safe.
 *
 * @param {import('../../lib/log.mjs').Log} log
 */
export async function applyNodeModulesPatches(log) {
  log.info('applying node_modules patches');
  await applyZodMemoryPatches({ log });
}
